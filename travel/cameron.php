<h3>Cameron Highlands </h3><br /><br />
<FONT face=tahoma color=black size=3>
Cameron Highlands (in the state of Pahang) is the largest hill resort in Malaysia and is famous for its tea plantations (photo on right).  If you should travel by road from Penang to Kuala Lumpur or vice versa, it would be a pity not to make a stopover at Cameron Highlands for a night, as it is about half-way between the two cities. <br />Most visitors stay at Tanah Rata, its biggest town, as it has all the amenities, though it is lower than Gunung Brinchang, which is 6666 feet (2,032 metres) above sea level.  When you arrive at the top of Gunung Brinchang you will be able to have a panoramic view of Ipoh from a wooden watch-out tower (provided it is not misty).  You will also come across pitcher plants in their natural surroundings during your walks here. 
<br />The best way to explore Cameron Highlands is to take one of the local excursion trips available.  These normally last 3-4 hours and you can take them either in the morning or early afternoon.

