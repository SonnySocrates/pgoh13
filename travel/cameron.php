<br />  A typical countryside excursion will take you up the hills to see the  tea plantations as well as a butterfly farm, a strawberry garden, a rose garden and an apiary farm.  And if you are adventurous enough you could even opt for an excursion which includes a jungle trek.
<br />Almost all the excursions will take you to the BOH tea factory to see the various processes through which tea leaves have to undergo before they are ready for your teacup.  After that you will have an opportunity to sip the various types of tea straight from the tea factory.  While walking round the tea plantations you might suddenly find yourself being "bombarded" with little white pellets by a light aircraft hovering above you.  Don't worry too much about them.  They are actually fertilizers for the tea plants.

