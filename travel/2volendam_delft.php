<h3><span style="background-color:midnightblue;float:left;"> &nbsp;DELFT&nbsp; </span></h3>
<br />Another quaint little town that can be visited from Amsterdam is Delft, which is situated halfway between The Hague and Rotterdam.  A leisurely stroll down the little streets behind the city hall will reveal some interesting facets of Dutch life in the 17th century, which has been well-preserved here.
<br />Delft is also closely associated with the House of Orange-Nassau.  This started when the assassinated William of Orange was buried in 1584 in the <a href="http://www.nieuwekerk-delft.nl/eng/kerkgebouw/index.html" target=_blank>New Church</a> which is just opposite to the city hall on the Markt.  Although known as the New Church it nevertheless dates back to the 14th century.
<br />Go to the official tourist information website for Delft <a href="http://www.delft.nl/delften/Tourists/" target=_blank>here.</a>
<br /><u>Note:</u> It is possible to go to Delft by train from Amsterdam Centraal via Leiden in about an hour.
