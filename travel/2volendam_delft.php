<h3> &nbsp;VOLENDAM&nbsp; </h3>
<br />
<FONT face=tahoma color=black size=3>Volendam is a fishing village that can be visited from Amsterdam as a day trip.  It is quite a popular tourist destination even for the Dutch themselves and has served as a retreat for artists such as Picasso and Renoir.  Not surprising indeed, as the coastal town (but it's actually a huge river, not the sea) is quite scenic and touristy and an ideal spot to spend a relaxing day.  From Volendam harbour there is a regular boat crossing to Marken, famous for its wooden houses.  The trip takes about half an hour.
<br /><u>Note:</u>  You can go to Volendam from Amsterdam by bus. Take the bus at the bus station near the Centraal Station in Amsterdam.  Allow yourself a whole day for this trip and try out some of the cooked fish and prawns from one of the open stalls around the harbour area.  As it is a fishing village you can be sure they are fresh!

