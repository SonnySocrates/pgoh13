<h3><span style="background-color:midnightblue;float:left;"> &nbsp;THE ZAANSE SCHANS&nbsp; </span></h3>
<br />The Zaanse Schans, 10 miles northwest of Amsterdam, is another of the Netherlands' many attractions for tourists.  It is a life-like replica of a typical Zaanse village of the seventeenth and eighteenth century. 
<br />The best public transport to go there is by Bus 91 from the Amsterdam Centraal Station which will stop at the St. Michael College next to the Zaans Museum and Zaanse Schans.  A train trip is possible but you will need to walk for another 10-15 minutes to arrive there from the Koog-Zaandijk station.
