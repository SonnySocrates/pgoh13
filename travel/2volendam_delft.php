<h3><span style="background-color:midnightblue;float:left;"> &nbsp;ROTTERDAM&nbsp; </span></h3>
<br />Rotterdam, nicknamed "Manhattan on the Maas", is the third largest port in the world after Shanghai and Singapore.  Needless to say, it is a very modern city and famous for its "cube houses".  A boat cruise along the Maas River (Meuse River) will bring out Rotterdam's ultra-modern skyline.  Here are four photos that will give an idea of Rotterdam's vibrant architecture that earned it the "city of architecture" title.
<br /><u>Note:</u>  It is possible to travel from Amsterdam to Rotterdam in 45 minutes with Fyra's fast-speed train.
