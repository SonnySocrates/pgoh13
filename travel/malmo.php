<h1 style="vertical-align:top"><span style="background-color:midnightblue;">&nbsp;&nbsp;Visiting Malmo&nbsp;&nbsp;</span></h1>
<br>
<table width=980><tr><td width=480 valign=top><img alt="The Turning Torso, Malmo's pride." title="The Turning Torso, Malmo's pride." src=copenhagen/malmo_torso1.jpg width=480></td><td width=480><img alt="The Turning Torso from another angle." title="The Turning Torso from another angle." src=copenhagen/malmo_torso2.jpg width=480></td></tr><tr><td colspan=2>
<FONT face=tahoma color=black size=3>If you are in Copenhagen it would be a pity not to make a day trip to Malmo, the third largest city in Sweden, as it is only 35 minutes away by train.  Besides there is a train from either city's central station every 20 minutes.  In fact the Oresund Bridge, 7.845km long and inaugurated in July 2000, has helped to bring the two cities so much closer together in travelling time.
<br>One of the must-see places in Malmo, if only for its uniqueness, is the 190m high Turning Torso, a structure of twisting cubes, designed by Santiago Calatrava, a Spanish architect and which was open in 2005.  It is but a short bus ride from the central station.  Take Bus No. 2 from near the station to go there.
<br />However, before you board the bus you need to buy the bus tickets from the <em>Skanetrafiken</em> in the railway station as without them you will not be able to board any of the buses in Malmo as the bus conductors are not allowed to accept cash.
<br />There is an alternative though, and that is to pay with your cellphone.  You do this before the bus conductor by typing the letters MAV, validating it, then keying in the number 72040.
<br />By the way you can only admire the Turning Torso from the outside.  As it is a private residential building, public access is normally not allowed though I hear that it might be possible to visit the top of the building on certain days in summer.
<br />However after having admired the architectural wonder from the outside you could turn left and head for the seafront which will give you plenty of invigorating sea breeze plus an overall view of the Oresund Bridge that spans the two Scandinavian countries.  There are some shops here for you to take a drink as well before taking the bus back.
<br />
If you have to return to Copenhagen to catch your plane, there is a train from Malmo every 20 minutes that drops you at the Kalstrup airport in hardly 25 minutes.  There is no need for you to go to the Copenhagen central station first as it will be another three stops further down after the airport station.
<br />Like Denmark, Sweden does not use the Euro but has its own currency, the Swedish krona.  However, if it is only for a short visit, there is no need for you to buy Swedish krona as many shops in Malmo will accept your Danish kroner or Euro though they will give you the change in Swedish krona.
</font></font></td></tr></table>


<h3 style="float:left">Malmo Central Station</h3>
<br>
<table width=980><tr><td>
<FONT face=tahoma color=black size=3>What is great about Malmo is that the main places of interest are all not too far from the central station so a day's visit will give you ample time for a leisurely stroll of the city.
<br />Upon arrival at the Malmo central station you can take the southward exit (you will see a canal before you) and walk across the bridge on the right-hand side as it will lead you to where all the interesting sights are.  But before you do that, you might want to make a stop at the Tourism Office first.  In such a case turn right when exiting the central station and when you approach the road, just look on its opposite side and you should be able to see the word "Tourism" in huge letters.</td></tr></table>
