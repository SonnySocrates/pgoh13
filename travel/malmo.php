<FONT face=tahoma color=black size=3>Now when you come out of the Tourism Office turn right and walk across the bridge and continue walking ahead.  Very soon you will arrive at Stortorget, Malmo's oldest square, with the city hall to the left.  As you walk across the square you can either take the street to the left or the one to your right as both have to be visited, being the very pulse of Malmo's commercial and leisure life. 
<br />In fact the street to the right will lead you to Lilla Torg, an animated little square full of typical Swedish restaurants, cafes and shops and where the locals and tourists gather.  If you should be here at around lunch time you might want to stop here first for lunch.
<br />Otherwise take the street to your left which will lead you to Sodergatan, which in turn is the gateway to the principal pedestrian shopping area of the city.  
<br />The Moderna Museet Malmo (Modern Museum of Malmo), inaugurated on December 26, 2009, is also in the vicinity at No. 22 Gasverksgatan.  It has a collection of modern and contemporary art and is hardly three minutes walk to the left as you go down Sodergatan street or 5 minutes from the other popular square called Gustav Adolfs Torg.  There is a similar museum in Stockholm which carries the same name (Moderna Museet).</td></tr></table>
