<center><font face="arial" size="5">Hapuskan mentaliti sempit 'Melayu sahaja Islam'</font></font></center>
<br />
<font face="comic sans ms" size="2"><b>From <a href=http://www.utusan.com.my/ target="_blank">Utusan Malaysia Online</a>    of November 3, 2006</b></font>
<br /><br /><b>Oleh SAIFUL BAHRI SAIDIN</b>
<p><font face="arial" size="2">KHILAF dalam pemikiran orang Melayu bahawa Islam di Malaysia hanya merujuk kepada orang Melayu sahaja amat menyedihkan. Mungkin didikan dan pengajaran yang kita terima sejak kecil lagi dengan hanya mengaitkan agama Islam di negara kita dengan orang Melayu sahaja.
<br />
Akibatnya pengetahuan agama kita menjadi cetek dan disogokkan dengan yang memakai serban, bersongkok, berjubah dan bertudung itu alim dan tinggi ilmu agamanya. 
<br />
Jarang sekali semasa didikan awal kita mengaitkan Islam itu suatu agama yang universal dan dianuti oleh berbagai-bagai bangsa di dunia ini. Lebih celaru lagi apabila seseorang itu keturunan Arab, terus kita fokuskan bahawa beliau adalah beragama Islam. Sedangkan ramai yang berketurunan Arab bukan beragama Islam.
<br />
Eloklah kita mendidik anak-anak yang masih kecil dan muda remaja agar jangan sempit pemikiran tentang takrif orang yang beragama Islam supaya mereka lebih terbuka pemikirannya.
<br />
Dalam dunia yang laju bergerak arus kemodenannya, perkembangan dan tamadun Islam juga perlu seiring dengan kecanggihan komunikasi dan teknologi. Kita jangan sempitkan diri dan keluarga kita dengan hanya menitikberatkan soal fardu ain dan kifayah sahaja. Asas agama itu memang perlu ada di hati dan sanubari setiap insan yang mengaku beragama Islam.
<br />
Namun dalam pergelutan dan pergaulan kita seharian, ia tidak boleh lari dari pertembungan dan perbandingan dengan agama lain. Sedang rakyat negara kita ramai yang berulang-alik ke luar negara, bekerja malah ada yang menetap di luar negara, ditambah pula dengan limpahan pekerja asing yang berbagai agama ke negara kita, orang Islam terutamanya generasi muda mula terdedah dengan fenomena baru.
<br />
Jika dahulu, kita takrifkan setiap orang Melayu itu beragama Islam, setiap orang India itu beragama Hindu, setiap orang Cina beragama Buddha atau Confucius kini banyak telah berubah. 
<br />
Didikan terhadap anak-anak harus ditekankan bahawa di Malaysia ini agama Islam itu bukan hanya merujuk kepada orang Melayu sahaja. Bangsa lain pun ada yang menganut agama Islam. Kita jangan menyempitkan fikiran anak-anak kita. Didiklah mereka supaya menganggap Islam itu agama universal.
<br />
Kekesalan pemikiran sempit tentang Islam telah berlaku kepada penulis semasa berhari raya di rumah jiran baru-baru ini. Jiran kami mengadakan rumah terbuka dengan menjemput rakan sekerjanya, jiran sekelilingnya dan sanak saudaranya untuk menjamu selera di Hari Raya. 
<br />

Isteri saya mempunyai raut wajah mirip orang Cina. Sedang kami menjamu selera, datang seorang anak kecil bertudung dengan ditemani oleh bapanya yang berserban dan berjubah dan ibunya yang bertudung penuh dan berjubah. Anak tersebut ingin duduk di meja saya kerana meja lain sudah penuh.
<br />
Amat terkejut kami berdua dan mereka yang berada di sekeliling kami apabila bapa kepada anak kecil itu melarang anaknya duduk di meja kami dengan kata-kata: Jangan duduk kat meja tu. Kita makan tak boleh campur dengan orang kafir. 
<br />
Saya amat sedih dan mengucap panjang apabila seorang yang penampilan mereka sekeluarga sebagai mengikut sunah dan tinggi Islamnya boleh mengeluarkan kata-kata seperti begitu. Sedih mengenangkan anak kecil sudah diberi pengajaran yang salah dan sempit tentang agama Islam.
<br />
Contoh kedua di rumah saya sendiri, apabila seorang rakan datang berhari raya. Walaupun berlainan ideologi politik, tetapi kami masih bersahabat. Sedang menjamu selera, tiba-tiba rakan saya itu seperti tersentak apabila melihat bingkai bergambarkan seni kaligrafi tulisan Cina. Dia terus menegur saya dengan nada yang tinggi: Apa nak jadi dengan kau ni. Kau gantung tulisan kafir di dalam rumah? Macam mana malaikat nak masuk rumah kau ni? Bertaubatlah. &nbsp;Begitu mudah beliau memberikan komen dan nasihat.
<br />
Apabila saya jelaskan kaligrafi Cina itu bermaksud mengucap dua kalimah syahadat dalam bahasa Mandarin dan ia direka oleh orang Islam China yang keturunannya sudah 700 tahun beragama Islam di wilayah Xian, China, rakan saya itu terus terdiam. Jelas sekali pengetahuan agamanya begitu cetek dan amat mudah untuk menjatuhkan hukum sama seperti parti politik yang diwakilinya. 
<br />
Masih terdapat umat Islam di negara kita ini yang rendah mentalitinya dan hanya menganggap orang Melayu sahaja yang beragama Islam di dunia ini. Apabila dikaji mengapa ini boleh terjadi, ternyata kurang ilmu pengetahuan dan malas membaca adalah punca utama terdapat minda orang Melayu yang beragama Islam bersikap sebegini. Mereka hanya menerima ajaran dan seruan dari satu kumpulan sahaja. 
<br />
Saya percaya rakan saya dan keluarga berjubah itu belum pernah ke tanah suci Mekah sama ada mengerjakan umrah ataupun menunaikan haji. Jika mereka ke sana, insya-Allah pemikiran mereka akan terbuka apabila melihat berbagai bangsa dan warna kulit mengelilingi mereka semasa mengerjakan ibadat.
<br />
Golongan sebegini agaknya akan merasa hairan dan mengucap panjang apabila berada di pulau Bali, Indonesia dan melihat seorang tempatan yang bersongkok dan berbaju batik makan daging babi di restoran. Lebih terkejut lagi jika dibawa ke dalam negara sendiri di rumah panjang suku kaum Kelabit di Miri dan mendapati ada surau di dalam rumah panjang tersebut. Walaupun penampilan suku kaum itu tidak menunjukkan identiti Islam, tetapi sebahagian daripada mereka telah memeluk agama Islam sejak 100 tahun dahulu lagi.
<br />
Sepatutnya golongan sebegini bersama-sama menegakkan syiar Islam dan menggalakkan perpaduan seagama walaupun berlainan warna kulit dan bangsa. Pemikiran sempit hanya orang Melayu sahaja yang beragama Islam perlulah dihindari. 
<br />
Ramai mualaf terpinggir di antara umat Islam di negara kita. Mahukah kita membiarkan mereka hilang arah kerana mentaliti sempit bahawa hanya Melayu sahaja yang beragama Islam di Malaysia ini?

