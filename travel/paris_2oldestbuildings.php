<FONT face=tahoma color=black size=3>This house (photo below, on left) at 51 rue Montmorency in the 3rd district of Paris which belonged to <a href="http://en.wikipedia.org/wiki/Nicholas_Flamel" target="_blank">Nicolas Flamel</a>, is reputed to be the oldest building in Paris (610 years old in 2017).  It  was built in the year 1407, as a plaque outside the building testifies.  As can be seen from the photo its neighbouring building has been renovated in keeping with the times while it remains intact (at least on the fa&ccedil;ade) to preserve its historical background.  You will be able to put your feet inside the building for that "Been there, done that" feeling as its ground floor is now used as a French restaurant under the name of <i>Auberge Nicolas Flamel</i>.  (Nicolas Flamel was a famous French alchemist whose name was mentioned in a number of books, among them <i>Harry Potter and the Philosopher's Stone</i> and <i>The Da Vinci Code</i>.)
<br>Another building put forward by many as the second oldest construction in Paris is the one at 3 rue Volta about 500 meters away (photo below, on right) which unfortunately has no official record of when it was built to support the claim.  As a matter of fact, an authoritative site on buildings has dismissed this claim completely, attributing its construction to the first half of the 17th century.  Read about it <a href="http://fr.structurae.de/structures/data/index.cfm?ID=s0013254" target="_blank">here</a> (it's in French).  By the way if your curiosity happens to take you here and you want to eat cheaply but well in a modest but authentic Chinese restaurant just take a look round and you'll have no problem finding one that's to your liking.
