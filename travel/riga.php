<h2 style="background-color:midnightblue;font-size:24px;">&nbsp;&nbsp;Day trip to Jurmala&nbsp;&nbsp;</h2>

<br>

You can easily get to Jurmala by train from Riga. The train ride to Majori, its most popular stop for tourists, takes about half an hour.  The Jurmala resort is still popular with Russians.  One rich dissident hit the headlines some years back when he disappeared from his <em>dacha</em> and hasn't been seen since!
