<td><img src=riga_canal.jpg width=320 title="The Riga Canal" alt="The Riga Canal"><br><em>The Riga Canal</em><br></td>
<td><img src=riga_flowermarket.jpg width=320 title="A flower market in Riga" alt="A flower market in Riga"><br><em>A flower market in Riga</em><br></td></tr></table>

<p>
Latvia has always been a lively trading country on the Baltic.  It became prosperous when it was part of the Hanseatic League (something like the EU without the squabbles) from 1300 right up until the mid 1800's.  They have done so much since the Russians left.    They have come through their own austerity and are now back on track so it is understandable that they are now voting solidly against giving Greece more money. Besides, their wages and pensions are much lower than those in Greece (less than half of the Greek pension).
<br>Riga, which stands on the broad Daugava River, really is a very attractive little city.  It has its own seaside playground at nearby Jurmala, which is still a favourite with well-heeled Russians today.
<br>After the Gorbachov Glasnost period Latvia became increasingly independent from 1989 and a few years later established its own parliament and asked the high proportion of Russians to choose between becoming Latvian ... or leave.
<br>The Latvian language became the main one and a museum is dedicated to the "Occupation".  It privatised industry and the collective farms, returning them to their owners or compensating them.  In 1994 Latvia became part of the EEC and has a strong, expanding economy.  As crossroads between Russia and the West it is in a fortunate position.

