<h1>Visiting Riga in Latvia</h1>
<br>
<em style="font-family:arial;font-size:1em">This article and photos are contributed by Keith Paterson, MBE*</em>

<br>
<br><FONT face=tahoma color=black size=4>Riga, the capital of Latvia, really amazed us for, far from being a threadbare Russian annex it has become a colourful modern city full of parks, statues and magnificent architecture.</p>

