<h1 style="color:yellow;background-color:black">What's New in Paris</h1>
<br>
<p style="max-width:750px;font-size:24px;"><b>Parc Rives de Seine (inaugurated on 2 April 2017)</b><br><em style="font-size:12px">Put mouse on photo to stop the slide display.</em></p>
<br>
<p style="max-width:750px;font-size:16px;">The latest attraction in Paris is a 4.5km promenade along the right bank of the Seine River starting from the Tuileries tunnel and ending at the Arsenal port near Place de la Bastille.  It was inaugurated by Anne Hidalgo, the Mayor of Paris, on April 2, 2017.<br>Called "Parc Rives de Seine", it is meant to link up with the existing <a href=http://pgoh13.com/paris_intro.php target=_blank>2.5km promenade along the left bank</a> via Pont Royal or the Passerelle Leopold-Sedar-Senghor (Pont Solf&eacute;rino in the older maps) to make a total of 7km in all. However much of it is not totally new, as the heavily-cobbled surfaces in some sections have not been tarred, making it difficult for a promenade if one is not wearing comfortable sneakers.  The innovative part really starts from the Pont Neuf side and ends at Pont Marie near the Notre-Dame Cathedral.  Note though that on a sunny day you should start your walk from the Pont Neuf side and not the Notre-Dame side if you do not want to have the sun's rays smack in your eyes!</p>  

  
