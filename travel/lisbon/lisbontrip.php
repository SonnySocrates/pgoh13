<h2 span style="background-color:midnightblue;width:200px;font-size:24px;display:inline-block;">SINTRA</h2>
<br><a href=http://www.sintraromantica.net/en target=_blank>Sintra</a> is about 30kms away from Lisbon City and is connected by train from the Rossio station.  The trip takes 35-40 minutes and there is a train every 15 minutes.  If you go by coach you are likely to be driven by an experienced driver as there are many tortuous turns and deep slopes and at times you will hear some little noises as the leafy tree tops that sprout out on the roadside brush against the roof of the coach.
<br>Among the places to visit in Sintra are the Pena Palace at 500m altitude and the Monserrate park with its palace.
<br>And if you should take a break at a bar in Sintra make sure you try the Queijada de Sintra (a pastry with cheese) and the ginjinha, an alcoholic drink made from Morello cherries!

