<h2 span style="background-color:midnightblue;width:300px;font-size:24px;display:inline-block;">BAIXA and CHIADO</h2>
<br>Baixa and Chiado form  the very heart of the city centre.  Within walking distance from each other, you can easily spend the entire day here visiting the Tourism Office, the Santa Justa ascensor, the very-animated Rua Augusta pedestrian street, the Commercial Square on the seafront and end up the day with dinner at one of the restaurants providing fado music in Rua do Norte in the Bairro Alto area.

