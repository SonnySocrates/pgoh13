<h2 span style="background-color:midnightblue;width:200px;font-size:24px;display:inline-block;">BELEM</h2>

<table width=750><tr><td width=250>
<img src=../lisbon/monastery_8columns250.jpg></td><td width=500 valign=top>But Tram 28 is not the only train of interest in Lisbon.  There is also Tram No. 15 which goes to Belem.  Although not in the city centre itself, Belem is one of the principal attractions of Lisbon.  Its  <a href="http://www.mosteirojeronimos.pt/en/" target=_blank>Jeronimos Monastery (Mosteiro dos Jeronimos)</a> is the main crowd-puller here.  The Monastery roof is supported by eight huge columns, as can be seen from the picture on the left.  Another much-visited monument here is the <a href="http://www.torrebelem.pt/en/" target=_blank>Belem Tower (Torre de Belem)</a>.  Built in the 16th century it was originally used as a fortress to defend the port of Lisbon.  Since then, an artificial lake has been created around the Tower.
<br>So much has been written about Belem's historical monuments.  But Belem is also reputed for its <em>pastéis de nata</em>, a custard tart that has spread as far as China (through its ancient colony of Macao)..  And the huge crowds that queue up patiently in front of the Pasteis de Belem shop, not far from the Jeronimos Monastery, attest to it.  This shop was established in 1837 and today produces between 25,000 to 30,000 custard tarts a day!  You won't believe how big the shop is until you go inside.</td></tr></table>

