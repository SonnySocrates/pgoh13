<FONT face=tahoma color=black size=3>Can you imagine a rickety-looking tramway line being famous and popular with tourists all over the world?  Well, Lisbon's Tramway 28 (or to give its local name <em>Electrico 28</em>) is one such.
<img src=../lisbon/tram28.jpg width=800>
In fact this humble, yellow-coloured tramway (but it has since been painted with other colours as well) that actually belong to an older epoch has become somewhat of an institution in this highly touristic city and no tourist to Lisbon should leave the city without taking a ride in it.  So even if you are the type who goes about everywhere in a taxi, make sure you leave your chic outfit in your hotel room, put on a pair of jeans to deter pickpockets and condescend to rub shoulders with the humble folks in this very ordinary tram if you want to return home with that authentic Lisbon experience!   In fact you can't really say you've "been there done that" in Lisbon without being able to talk about your experience in taking a ride in this tram!  At times it's as if you are riding a funicular train so steep are some roads along the way and it can also be very bumpy at times!   According to a conductor Tram No. 28 has been running for well over 80 years now (I read later that it was actually inaugurated in 1914, making it 100 years old in 2014).  The icing on the cake is that it stops at so many places of tourist interest along its 40-minute route that using it as a hop-on hop-off bus alone makes it worthwhile to buy a day's bus/tram/metro ticket for unlimited travel.  After all it costs 2.85 euros for a tram trip while a day's unlimited ticket costs only 6 euros.  The downside though is that you have to be wary of your wallet all the time as it is also a favourite haunt of pickpockets.
