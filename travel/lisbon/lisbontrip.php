<br>
About the most lively square in the Bixa area is the Rossio Square, also known as Pra&ccedil;a Dom Pedro IV.  It is a hive of activity both during the day and at night with bountiful seafood restaurants and souvenir shops everywhere, especially at the Rua das Portas de Santo Antao, a pedestrian street springing from it.
<br>When you are there don't miss the ginja (or ginjinha), a sweet alcoholic drink made with cherries and cannelle.
<br>Hardly 5 minutes walk from Rossio Square is Rua Garrett, the famous shopping street leading from Baixa to Chiado, the shopping area.   At 120 Rua Garrett, just outside the Baixa-Chiado metro station, is located the Caf&eacute; A Brasileira, Lisbon's reputed caf&eacute;.  You might want to go in or sit at the terrace for a taste of its famous <em>bica</em> (espresso).   You might want to plan your visit to coincide with the Lisbon Carnaval (Mardi Gras) held every February.

