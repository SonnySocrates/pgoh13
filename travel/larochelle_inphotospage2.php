      <h3 style="background-color:#007700;color:white;padding-left:15px">Motor-boat For Sale</h3>
      <p>At the old harbour (Vieux Port) you will sometimes see boats put up for sale.  You will know they are for sale if you see a signboard with the word "Occasion" on it ("occasion" means secondhand in French).  These boats are moored right at the very edge of the quay.  And if you think you've found your boat you might as well celebrate it at one of the restaurants nearby.  For this area is not only a hunting ground for old boats it is also a favorite dining place because of its concentration of seafood restaurants</p>
    </div>

<div class="col-sm-6 col-lg-4">
<img style="border-top:1px solid black" class="img-fluid" src=electriccars_forhire.jpg>
      <h3 style="background-color:#007700;color:white;padding-left:15px">Electric Cars For Hire</h3>
      <p>Non-polluting electric cars for hire.  Once you pay a monthly subscription fee (with a minimum period of 3 months) you will be allowed to make use of any of the cars. You pay according to the usage time and mileage totalled during the month.  You have to leave the cars in one of the seven stations for the purpose and collect them from there each time you need to use them (they're available round the clock throughout the year).  La Rochelle was the first French town experimenting with the use of electric cars.</p>
    </div>

<div class="col-sm-6 col-lg-4">
<img style="border-top:1px solid black" class="img-fluid" src=museemaritime_larochelle.jpg>
      <h3 style="background-color:#007700;color:white;padding-left:15px">Maritime Museum</h3>
      <p>The Mus&eacute;e Maritime is separated by a footbridge from the Aquarium.  This maritime museum is actually composed of two ships - the main one being France 1 which is 76 metres long.  Here the visitor can climb down the narrow stairs to visit the four decks (ponts in French). You will be able to inspect the machine room, the commandant's post, the kitchen, dining-room, cabins, etc.  France 1 was stationed in the North Atlantic Ocean for meteorological readings as well as rescue operations.</p>
