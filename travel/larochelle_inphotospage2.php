<h2>The 3 towers in Le Vieux Port</h2>
<br>
When they talk about the three towers in La Rochelle they are referring to the St Nicholas Tower (St. Nicolas), the Chain Tower (La Chaine) and the Lantern Tower (La Lanterne).  The last, however, looks more like a church steeple than a tower and was originally a lighthouse.  It is also near here that you'll find the beach of La Rochelle (<i>Plage de la Concurrence</i>).  These "three towers" are the remains of the original fortifications (the St Nicholas and the Lantern Towers were later used as prisons).   If you intend to visit all the three towers it is worth buying a multi-day pass (billet group&eacute;) as it also entitles you to take the "Passeur" boat to the other side of the harbour (where the Mediathèque is).
