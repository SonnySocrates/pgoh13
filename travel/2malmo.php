<h3>Malmo Central Station</h3>
<br>

<FONT face=tahoma color=black size=3>What is great about Malmo is that the main places of interest are all not too far from the central station so a day's visit will give you ample time for a leisurely stroll of the city.
<br />Upon arrival at the Malmo central station you can take the southward exit (you will see a canal before you) and walk across the bridge on the right-hand side as it will lead you to where all the interesting sights are.  But before you do that, you might want to make a stop at the Tourism Office first.  In such a case turn right when exiting the central station and when you approach the road, just look on its opposite side and you should be able to see the word "Tourism" in huge letters.
