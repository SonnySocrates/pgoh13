<p><table width=300 align=right><tr><td valign=top><img src=zhouzhuang1.jpg width=300></td></tr></table>There are quite a number of water towns in China but Zhouzhuang is reputed to be its "no. 1 water town".  This is another place not to be missed if you should be in Shanghai as it is only about 50 kms away and can easily be covered in a day.  The best (cheapest) way to go there is to book an excursion ticket from the bus station near the Shanghai Indoor Stadium.
<br />It makes a number of trips to Zhouzhuang in the morning (at half-hourly intervals) every day and the return trip to Shanghai is in the afternoon of the same day.  A round ticket costs 150 RMB but when you consider that it includes the admission fee of 100 RMB that anyone visiting the water town has to pay, then it is not expensive at all.

<br />Although it is considered as an excursion coach meant mainly for Chinese tourists (internal tourism is big business in China), non-Chinese speaking tourists need not be deterred by it as it is not one of those excursions where the participants follow the tour leader everywhere and listen to his explanations.  The one that I took left Shanghai Stadium at 08h30 and as there was little traffic we took only 1 1/2 hours to arrive.  We were then told to explore the town on our own and to report at the same place  at 15h00 for the return trip.  There is no danger of getting lost on your own as the canals are not that many.
<br />As the town is highly reputed among the Chinese you can expect to be overnumbered by local tourists.  In fact it is even recommended to buy your coach ticket in advance from the booking office near the Shanghai Indoor Stadium.
<br /><table width=300 align=left><tr><td valign=top><img src=woman_gondolier.jpg width=300></td></tr></table>

Apart from admiring the canals the visitor can also go on a gondola-style boat ride with the gondolier (most of them are women) serenading the tourists with traditional Chinese folk songs.  Here and there we see women scrubbing their clothes by the canal oblivious to the hordes of tourists that they have come to accept as part of their daily lives.
<br />One thing you might not know about the boat ride is that the price of a ticket shown on the window counter is actually not the price for one person but for a whole boat (it's mentioned that a boat can take a maximum of 8 passengers).  So unless you really want a whole boat to yourself you can always invite other visitors to join you (for once it won't cost you anything to be generous!)  I was not given a chance to be generous though.  As soon as I had bought my ticket a group of schoolgirls approached me and said there were 7 of them and asked if they could be with me in the same boat.  Thinking that the 80 RMB that I paid for my ticket was just for myself and that there might have to be 8 persons in a boat before it will take off, I willingly agreed.  Suddenly all of them started to give me a 10 RMB note each and it was only after much explaining by all of them all at the same time that I finally accepted the money without "protesting".
<br />As we boarded the boat each of us also contributed 10 RMB to the gondoliere as it seems that's the usual practice for her singing.  In fact we were told that that's her main source of income as she is only paid 15 RMB out of the 80 RMB the company charges for a trip.
<br /><center><table width=750><tr><td valign=top><img src=craftsman1.jpg width=375></td><td valign=top><img src=craftsman2.jpg width=375></td></tr></table></center>
Apart from the waterways there is much to see in the narrow alleys lined on both sides by shops selling all kinds of unimaginable objects that seem to belong to another age but make good souvenirs to bring home.  In fact as you linger along its narrow alleyways you will see craftsmen at work as they always have been throughout the ages.  In its early years the town was famous for its grain, silk and handicrafts.  There are also local snacks a-plenty if you are daring enough to try them (but go easy on them as they might not go well with your tummy)!

<br /><table width=300 align=right><tr><td valign=top><img src=choose_yourself.jpg width=300></td></tr></table>
One scene that cannot escape one's eyes in front of each and every restaurant in Zhouzhuang is a big aquarium with fish swimming in it as well as different types of vegetables displayed on the floor (see photo).  In fact they are not there just for the decoration.  When you order your dishes you also choose one of the fish swimming in the aquarium and the vegetables that you see displayed.
<br />But the main attraction of course are its canals.  As you cross one after another of the 14 stone bridges erected during past dynasties in Zhouzhuang you will undoubtedly notice the Twin Bridges (Shuangqiao) which was built in the Ming Dynasty.  Its reflections in the water are said to create perfectly harmonious full moon shapes.  In the course of your ramblings you are also likely to notice the Shen Family Hall, built in the Qing Dynasty and consisting of seven courtyards and five gateway arches.  Boats stop right in front of its porch.
