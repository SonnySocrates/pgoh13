<h3>NYHAVN</h3>
<br>
<table width=980><tr><td><a href=copenhagen/nyhavn_restos.jpg target="_blank"><img src=copenhagen/nyhavn_restos375.jpg width=480></a></td><td><a href=copenhagen/nyhavn_theatre.jpg target="_blank"><img src=copenhagen/nyhavn_theatre375.jpg width=480></a></td></tr><tr><td colspan=2>
<FONT face=tahoma color=black size=3>The little port of Nyhavn crammed with restaurants, cafes and colourful ancient houses all along the left side of the canal is a great place to chill out during the daytime as well as at night.  You can take one of the boat rides lasting a full hour from here either before or after a beer (preferably Carlsberg, seeing that you are in Denmark!)  From its humble and seedy beginnings as a place frequented by sailors, Nyhavn has become one of the trendy places to be in.  Even the avant-garde theatre (<em>Det Kongelige Teater</em>) is located here (photo on right).</td></tr></table>

