<FONT face=tahoma color=black size=3>The Tivoli Gardens amusement park is certainly one of the "must" places to visit while in Copenhagen.  However it is closed on certain months of the year so try not to come when it is closed.  You can find out the dates when it is open from its official website <a href=http://www.tivoli.dk/en/praktisk/aabningstider/ target=_blank> here</a>.  The photo on the left shows the Tivoli Gardens all dressed up and ready to greet its visitors for Halloween.  The park is just opposite the Bernstorffsgade side of the central station. The picture on the right shows some of the Tivoli Park structures as seen from the road. At night the multi-coloured illuminations turn it into a veritable fairyland.  Believe it or not it has been there since 1843!</td></tr></table>

