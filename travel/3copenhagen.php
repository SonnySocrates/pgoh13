<h1><span style="background-color:midnightblue;">&nbsp;&nbsp;Visiting Copenhagen (1)&nbsp;&nbsp;</span></h1>

<p><table align=right><tr><td width=500><a href=copenhagen/christiania_entrance1.jpg target=_blank><img alt="Entrance to Christiania in Copenhagen" title="Entrance to Christiania in Copenhagen" src=copenhagen/christiania_entrance1.jpg width=500></a><br />&nbsp;&nbsp;<em>Gateway to Christiania, a world of its own.  Once inside no photographs are &nbsp;&nbsp;allowed to be taken.  <a href=copenhagen/christiania_entrance2.jpg target=_blank>Go here for another photo.</a></em></td></tr></table>

<FONT face=tahoma color=black size=3>Copenhagen is not a touristy city in the sense that Paris or Rome is but it has a touch of the fairyland setting which must have inspired Hans Christian Andersen.  There is not the stress that one finds in bustling cities but rather an easy-going spirit that is called zen.  The cars go by at a leisurely pace and rarely do you hear drivers honking at the mass of cyclists as everyone seems to respect law and order.  There is a great deal of tolerance and respect for one's neighbour.


<br />The Christiania settlement is just one example.  Imagine an entirely enclosed area where "anything goes" yes, a colony in which almost everything is tolerated.  Call it a hippies' colony if you like, though the term "alternative community" sounds better.  In fact if you want to see something in Copenhagen that you don't find anywhere else in this earth then Christiania is the place to go.
<br />If you are afraid to venture into such a place alone (there's no need to, actually, in the daytime because you will often find groups of schoolchildren there), you can join the Christiania visit which starts most days at 15h00.  Notices everywhere remind you that you are not allowed to take photos and everywhere you go you will find graffiti or what some call alternative art while soft drugs such as hashish are openly displayed for sale at between 50 and 120 Kroner a gram.  There is a limit to this though, as hard drugs are not tolerated.
<br />Christiania first came into existence in 1971 when hippies, vagabonds and the homeless took over an abandoned military base and began squatting on it, gradually turning this uncharted territory into a self-sufficient colony over the years.  Today Christiania has become one of the top tourist attractions in Copenhagen with many souvenir stalls selling items made by the Christianites themselves and an immense open-air cafeteria at Cafe Bar Nemoland with some 50 picnic-style tables spread out for a snack or drink during the visit.

