<FONT face=tahoma color=black size=3><em>The Little Mermaid</em> by Edvard Eriksen (photo on left) has been sitting demurely and wistfully alone on a rock in Langelinie since 1913 (except for 6 months in 2010 when it was sent to China for the Shanghai Expo).  As it is the symbol of Denmark, most tourists do not leave the city without visiting it.  But not too many people know about the new mermaid on the other side of the road where the canal is.  Called the "genetically modified Little Mermaid" (photo on right), it was also made from bronze in 2006 by the renowned <a href=http://www.bjoernnoergaard.dk/en/home target=_blank>Danish sculptor Bjorn Norgaard</a>.  In the square next to it are other open-air sculptural works by the same artist carrying the theme "Genetically Modified Paradise".   Bus No. 26 goes there.</td></tr></table>

