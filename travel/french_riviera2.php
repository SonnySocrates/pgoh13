<h1><span style="background-color:midnightblue;">&nbsp;&nbsp;Visiting Nice (French Riviera)&nbsp;&nbsp;</span></h1>

<table width="980"><tr><td>

<p><table align=right width=500><tr><td valign=top><img src=nice_esplanade500.jpg width=500></td></tr><tr><td><font size=2><b>A panoramic view of the Promenade des Anglais in Nice.  It is 8 kms long!</b></font></td></tr></table><FONT face=tahoma color=black size=3>For those coming from outside France the Nice-C&ocirc;te d'Azur International Airport is the starting point to all cities in the French Riviera (since neither Cannes nor Monaco has one).  Its <em>Promenade des Anglais</em> is where everyone goes for a leisurely stroll both in the daytime as well as in the evening.  It is also along this esplanade that the famous Hotel Negresco is situated.  Not far from here is a small train that takes tourists for a ride round the old town and goes right up to the <em>Colline du Chateau</em> or Castle Hill from where you can have a breathtaking view of the old town, the beach and the hills.  But don't try to look for a castle there - there is none despite its name!  <br />Another tourist attraction in Nice is its famous flower market (<em>Le march&eacute; aux Fleurs</em>) at the Cours Saleya not too far away from the starting point of the little train but has more to offer than just flowers as there is also a fruits market nearby.  As with most markets in France it is closed on Mondays.
<br />A dip in the pollution-free sea here is a must for swimmers though you will have to put up with quite big pebbles all the way to the sea.  The <em>Parc Phoenix</em> is also worth a visit.  Its well-heated greenhouse contains luxurious plants as well as animals of a distant land that can be admired at close range.
<br />And let's not forget that Nice is also famous for its Carnival.  If you are interested go to its <a href=https://www.nicecarnaval.com/en/news/carnival-in-the-city/ target=_blank>official site here</a> for more details.

