If you intend to pass a whole day at the beach it might be worth paying 6 euros for a lounger.  You will be able to make use of it from 9h00 to 20h00.  (I had to pay the same amount though I used it for just an hour as I needed a safe place to leave my belongings while I swim in the sea.)
<br>If you find that the Barceloneta beach is too crammed you can take a short train ride either to Costa Brava to the north or to Costa Dorada to the south.  Such a train will stop at so many beaches along the way that you will not know where to descend.
<br>My own favorite beach to the north (along the Costa Brava) is called Lloret de Mar <a href=costabrava_map.php target=_blank>(click to see map)</a>.  It is only an hour's coach ride.  
You can go in the morning and return in the evening though chances are you'll like it so much that you'll want to stay overnight.  But be warned that the hotels there are quite expensive and the cheaper ones are all taken up, seeing that bookings are done months ahead.
<br />But if the sea and sun do not tempt you then take a funicular ride up to the Tibidabo Mountain.  However once you reach the top you won't find much wandering ground as the hill has been turned into a huge amusement park and you're more likely to run into groups of laughing schoolchildren.  But if you have children with you this is the place to take them to.
