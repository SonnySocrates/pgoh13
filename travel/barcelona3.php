<h2>The Barceloneta Beach</h2>
<p><FONT face=tahoma color=black size=3>Not only in summer but whenever the sun comes out, thousands of people head for Barceloneta (the subway station carries the same name) where the city has its kilometer-long beach of soft fine sand and clean sea water (unless you are unlucky enough to go swimming after a heavy rainfall when the detritus are washed to the shore).
<br>  There are lifeguards every few meters and full amenities are provided such as running water for a quick shower, free toilet facilities right at the beach itself and a wooden walkway from the beach to the esplanade to allow your feet to be sand-free (there are low water taps specially designed to help you get rid of the sand on your feet).

