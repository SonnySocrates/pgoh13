<h1><span style="background-color:midnightblue;">&nbsp;&nbsp;Visiting Monaco in the French Riviera&nbsp;&nbsp;</span></h1>

<p><table align=right width=500><tr><td valign=top><img src=monaco_main500.jpg width=500></td></tr><tr><td><font size=2><b>An overview of the city of Monaco.</b></font></td></tr></table><FONT face=tahoma color=black size=3>Monaco, or to give its full name, the Principality of Monaco, is so small (hardly two square kms) that its capital is also called Monaco (Monte Carlo is not the capital but just its most popular district and where the famous Casino is located).  In fact it is the second smallest country in the world (the smallest being Vatican City).
<br />If you have only an afternoon in Monaco head directly to its famous "Rock" (Le Rocher).
 Time your visit to witness the impressive changing of the guards ceremony in front of the Palace at 12 noon daily.  It is also at le Rock that you will find the Prince's Palace as well as the Cathedral (where Prince Rainier and Princess Grace were buried) and the Oceanographic Museum which houses an astounding collection of aquaria on marine life, especially deep-sea predators.  Just across the road from the museum is a small train waiting to take you for a 30-minute guided tour through the principality.
<br />Another attraction in Monaco is the Exotic Garden (<em>Jardin Exotique</em>).  Here you will find an immense collection of exotic plants that blend harmoniously with the landscape.  Be prepared to do a lot of walking though (mostly downwards though eventually you'll have to climb all the way to the top again!)
<br />If you are brave enough to go some 60m below you'll be rewarded with a cave that keeps intact the stalagmites and stalactites that have formed over the centuries.

