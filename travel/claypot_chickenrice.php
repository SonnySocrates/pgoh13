<center><h1 style="background-color:midnightblue;color:yellow;font-family:arial;font-size:30px;display:inline-block;padding:5px 10px;text-align:center;">Claypot chicken rice in rice cooker</h1></center>

<FONT face=tahoma color=black size=3>One great Chinese dish is the claypot chicken rice.  As I don't have a gas cooker I am not able to use the claypot so I had to make do with a rice cooker.  The pictures below show some of the steps involved in preparing this dish.  It's good for six servings (or for four great eaters).   Since much of Chinese cooking is mere guesswork based on experience (you only have to observe the great chefs in Chinese restaurants at work) I have not given the measures that I used here.   What you can do is to taste it from time to time to see if you need to add more soy sauce, etc.

<br>Note: <em>This page is written as a reminder to myself the next time I cook the dish.  It is a very simple version of the dish for the simple person that I am, so use it at your own risk!</em>
<br><br>
<table width=800><tr>
<td width=400 valign=top><img src=claypot_rice1.jpg><br>What was used: 12 pieces of chicken, 3 Chinese sausages,  garlic, ginger, cornstarch, Chinese rice wine, sesame oil, oyster sauce, soy sauce and one Maggi chicken stock cube.  It will certainly be better if you can add some Chinese mushrooms and vegetable to it.</td>
<td width=400 valign=top><img src=claypot_rice2.jpg><br>The first thing to do is to marinate the chicken by coating it with Chinese rice wine, sesame oil, oyster sauce, soy sauce and cornstarch (mix these in a bowl first).  You can then leave it aside for 30 minutes while you continue with the rest of the preparation.</td>
</tr><tr>
<td width=400 valign=top><br><img src=claypot_rice3.jpg><br>The ginger and garlic are finely minced (I must admit mine are a bit coarse) while the sausages are sliced diagonally.  If you can't stand ginger put just half of what you see here!</td>
<td width=400 valign=top><br><img src=claypot_rice4.jpg><br>In a big pan or wok fry the garlic and ginger in some oil then put in the marinated chicken (6 pieces at a time) and fry till they turn brown.  The sliced sausages are then added and mixed with the chicken for 2 minutes.</td>
</tr><tr>
<td width=400 valign=top><br><img src=claypot_rice5.jpg><br>To give the rice all the flavour, it is very important that you pour the 5 cups of uncooked rice straight into the pan and stir it with the chicken pieces and sausages so it will absorb all the flavour.  Leave the heat on at high.  Add more soy sauce and sesame oil and keep on stirring till the white rice lose their whiteness.</td>
<td width=400 valign=top><br><img src=claypot_rice6.jpg><br>My rice cooker can take up to 10 cups of rice.  However with the presence of chicken the maximum I can put here is 5 cups of rice plus 6 cups of water (more or less depending on whether you want your rice soft or firm).  But put the water into the rice cooker (together with the melted cube of chicken stock) before you put the contents of the pan in.  Give it a stir after 15 minutes and add a dash of sesame oil and soy sauce if it's not salty enough.</td>
</tr><tr>
<td width=400 valign=top><br><img src=claypot_rice7.jpg><br>The finished dish - not quite as yummy as it would have been in a claypot perhaps but close enough to claypot chicken rice.  You cannot complain really since you are making do with limited means.  Oh, I forgot to sprinkle the chopped scallions in the plate for the picture!  And perhaps more of the dark soy sauce to give it the dark color.</td>
