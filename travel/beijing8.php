<h3 style="font-family:arial;font-size:36px;background-color:midnightblue;color:yellow;display:inline-block;padding:0 15px">Carrefour hypermarket</h3>

<br />
<FONT face=tahoma color=black size=3>One of the biggest shopping/food malls near Beijing is the Carrefour hypermarket at Longde Times Square, No. 186, Litang Road in the Changping District, about 20kms to the north of Beijing.  It can be reached by Line 5 of the Beijing subway as it is near the Tiantongyuan neighbourhood.  On its uppermost floor is a food court where a large variety of Chinese dishes can be found.  Be prepared to sit with the local populace at long tables which are quite spartan.  Nearby is a McDonalds and a KFC as well.  The Tiantongyuan neighbourhood is reported to have over 400,000 residents.</font></font></font>
