<p><FONT face=tahoma color=black size=3>With its colourful array of costumes, prancing dragons and lions and deafening firecrackers from shop awnings or roadside poles this is an event that no camera and video enthusiast should miss.
<br />So watch out for this great event that the Chinese business community in Paris organise to celebrate the Chinese New Year around January or February each year.
<br />When the festival is around the corner check out <a href="http://www.chinoisdefrance.com/nouvel-an-chinois.htm" target="_blank">this site</a> for the actual dates and times of the various processions.
<br />The biggest of them is held in the Chinatown area in the 13th district, the assembly point being usually near the Olympiades (just after Tang Fr&egrave;res).  Anyway you don't have to worry about not finding this street procession, as any point along any of the roads that it passes is just as good a place to watch.<br />If you are coming by the underground just get off at either the Place Italie, Porte de Choisy or Porte d'Ivry stations at about 13h30 and follow the crowd.  You should go even earlier if you want to ensure a good spot for your shots.
<br />This 52-second video clip of the dragon dance, taken with about one of the cheapest digital cameras on the market on 25th February 2007, will give you a glimpse of the procession.  - PJG

