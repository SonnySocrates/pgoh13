<h1 style="margin-top:20px;">Bruges, Venice of the North</h1>
<br>

<div class="clear"></div>

<article class="extendfull">
<section><a alt="Bruges canal"  title="Bruges canal" href=belgium/bruges_canal11200.jpg><img src=belgium/bruges_canal1.jpg /></a>
<br>As you walk round the city your eyes are often treated to astounding views of its canals making Bruges a postcard city in the true sense of the word.
</section>
<section><a alt="Bruges canal"  title="Bruges canal" href=belgium/bruges_canal21200.jpg><img src=belgium/bruges_canal2.jpg /></a>
<br>It will not do justice to show just one picture of the canals so here are a few of them.  A boat ride that takes you in and out of the numerous canals costs 8 euros.
</section>
<section><a alt="Bruges canal"  title="Bruges canal" href=belgium/bruges_canal31200.jpg><img src=belgium/bruges_canal3.jpg /></a>
<br>Another eye-refreshing view of the canals of the "Venice of the North".  Truly nature at its best.  Click on each photo to see a bigger version.
</section>
<section><a alt="Bruges canal"  title="Bruges canal" href=belgium/bruges_canal41200.jpg><img src=belgium/bruges_canal4.jpg /></a>
<br>The canal waters go right up to the doorstep of some of the houses standing on the canals itself.  The canal cruise lasts 30-45 minutes.
</section>


<section><a alt="Belfort, a 12th-century belfry in Markt, Bruges"  title="Belfort, a 12th-century belfry in Markt, Bruges" href=belgium/bruges_MarktBelfry1200.jpg><img src=belgium/bruges_MarktBelfry.jpg /></a>
<br>The main attraction in Markt square, the very heart of Bruges, is the towering Belfort, a 12th-century belfry.
</section>
<section><a alt="Statue of Jan Breydel and Pieter de Coninck in Markt, Bruges"  title="Statue of Jan Breydel and Pieter de Coninck in Markt, Bruges" href=belgium/bruges_MarktCenter1200.jpg><img src=belgium/bruges_MarktCenter.jpg /></a>
<br>Right in the centre is the statue of Jan Breydel and Pieter de Coninck, two important figures in the history of Flanders.
</section>
<section><a alt="Markt square in Bruges"  title="Markt square in Bruges" href=belgium/bruges_cafes1200.jpg><img src=belgium/bruges_cafes.jpg /></a>
<br>At the other end of the Markt (opposite the belfry) are tourist-oriented caf&eacute;s and restaurants.  There is a Quick here for budget travellers.
</section>
<section><a alt="Historium museum in Markt, Bruges"  title="Historium museum in Markt, Bruges" href=belgium/bruges_historium1200.jpg><img src=belgium/bruges_historium.jpg /></a>
<br>Another part of the Markt square - the <a href="https://www.historium.be/en" target=_blank>Historium museum</a> with the Tourism Office next to it.  You can get a free map of Bruges here.
</section>

<section><a alt="Wednesday market at the Markt in Bruges"  title="Wednesday market at the Markt in Bruges" href=belgium/bruges_Marktmarket1200.jpg><img src=belgium/bruges_Marktmarket.jpg /></a>
<br>There is a very animated market at the Markt every Wednesday morning.  The Wollestraat from here has souvenir and fancy shops on both sides of it and leads to a bridge across the Dijver canal, from where you can get a cruise boat. 
</section>
<section><a alt="Bruges City Hall at Burg square"  title="Bruges City Hall at Burg square" href=belgium/bruges_BurgCityHall1200.jpg><img src=belgium/bruges_BurgCityHall.jpg /></a>
<br>Just next to the Markt square (take the street called Breidelstraat) is another "must-see" square, the Burg, where the City Hall (<em>Stadhuis</em>) is located.  The top floor is open to the public.
</section>
<section><a alt="Basilica of the Holy Blood at Burg square, Bruges"  title="Basilica of the Holy Blood at Burg square, Bruges" href=belgium/bruges_BurgChurch900.jpg><img src=belgium/bruges_BurgChurch.jpg /></a>
<br>Another building of interest at the Burg square is the Basilica of the Holy Blood.  There is a lift to the top.
</section>
<section><a alt="Inside the Basilica of the Holy Blood at Burg square, Bruges"  title="Inside the Basilica of the Holy Blood at Burg square, Bruges" href=belgium/bruges_BurginsideChurch1200.jpg><img src=belgium/bruges_BurginsideChurch.jpg /></a>
<br>Inside the Basilica of the Holy Blood.
</section>

<section><a alt="Simon Stevinplein square in Bruges"  title="Simon Stevinplein square in Bruges" href=belgium/bruges_SimonStevinplein1200.jpg><img src=belgium/bruges_SimonStevinplein.jpg /></a>
<br>The quaint square at Simon Stevinplein, halfway between the Sint-Janhospitaal and the Markt.  Take Mariastraat from here to go to the Sint-Janhospitaal, one of the tourist attractions in Bruges.
</section>
<section><a alt="Onze-Lieve-Vrouwekerk Museum in Bruges"  title="Onze-Lieve-Vrouwekerk Museum in Bruges" href=belgium/bruges_OnzeLieveMuseum1200.jpg><img src=belgium/bruges_OnzeLieveMuseum.jpg /></a>
<br>Formerly a Notre Dame church, this building, with a monastery on either side of it, has become the Onze-Lieve-Vrouwekerk Museum.  Among its collection is Michelangelo's world famous "Madonna and Child" marble sculpture.
</section>
<section><a alt="Sint-Janshospitaal and Memling Museum in Bruges"  title="Sint-Janshospitaal and Memling Museum in Bruges" href=belgium/bruges_SintJanshospitaal1200.jpg><img src=belgium/bruges_SintJanshospitaal.jpg /></a>
<br>Just opposite the Onze-Lieve-Vrouwekerk Museum is the Sint-Janshospitaal, a hospital and pharmacy that was founded in the second half of the twelfth century.  The Memling Museum is in its grounds.
</section>
<section><a alt="Pharmacy at Sint-Janshospitaal in Bruges"  title="Pharmacy at Sint-Janshospitaal in Bruges" href=belgium/bruges_pharmacy1200.jpg><img src=belgium/bruges_pharmacy.jpg /></a>
<br>The pharmacy at the hospital, which was still open until 1971,  has been preserved intact for the benefit of visitors.    <a href="https://www.museabrugge.be/en/visit/locations/sint-janshospitaal" target=_blank><em>More here</a> on the Sint-Janshospitaal/Memling Museum</em>.
</section>

<br>

<section><a alt="Musea Brugge ticket to Bruges museums"  title="Musea Brugge ticket to Bruges museums" href=belgium/bruges_museabrugge1200.jpg><img alt="Musea Brugge ticket to Bruges museums"  title="Musea Brugge ticket to Bruges museums"  src=belgium/bruges_museabrugge.jpg /></a>
<br>Don't be confused by the museum tickets.  All museum tickets have the same name "Musea Brugge" in big letters at the top and the particular museum's name is in tiny letters in the middle.
</section>
<section><a alt="Beguinale House in Bruges"  title="Beguinale House in Bruges" href=belgium/bruges_entrancebeguinalehouse1200.jpg><img style="width:100%;" src="belgium/bruges_entrancebeguinalehouse.jpg" /></a>
<br>The Beguinale House is one of the historical attractions that Bruges has to offer.  In the Middle Ages the departure of their husbands for the Crusades resulted in many women living in community.  Upon the death of their husbands they devoted their lives to praying and to performing acts of charity.  The place has been turned into a convent.
</section>
<section><a alt="Caleche ride in Bruges"  title="Caleche ride in Bruges" href=belgium/bruges_caleche1200.jpg><img src=belgium/bruges_caleche.jpg /></a>
<br>You might want to take a 30-minute ride round the city in a caleche.  It's quite costly though, at 50 euros.  But if the bill is split among five persons it could become affordable.  The driver told me that his horse is already 12 years old and can live to 30 years.
</section>
<section><a alt="Windmill in Bruges"  title="Windmill in Bruges" href=belgium/bruges_windmill1200.jpg><img src=belgium/bruges_windmill.jpg /></a>
<br>You can still find a couple of windmills just outside the city centre.
</section>

<section><a alt="Groeninge Museum in Bruges"  title="Groeninge Museum in Bruges" href=belgium/bruges_groeningemuseum1200.jpg><img src=belgium/bruges_groeningemuseum.jpg /></a>
<br>The main entrance to the Groeninge Museum.  You can combine your visit to the museum with a canal cruise as there is a landing platform (No. 3) just on the opposite side of the museum entrance near the Dijver canal.
</section>
<section><a alt="Jan van Eyck's painting at the Groeninge Museum in Bruges"  title="Jan van Eyck's painting at the Groeninge Museum in Bruges" href=belgium/bruges_vaneyck1200.jpg><img src=belgium/bruges_vaneyck.jpg /></a>
<br>One of Jan van Eyck's best-known paintings at the Groeninge Museum.   The museum is internationally known for its collection of works by the Flemish Primitives, foremost among them being <a href="https://en.wikipedia.org/wiki/Hans_Memling" target=_blank>Hans Memling</a>, a German painter who was made a citizen of Bruges.
</section>
<section><a alt="Vismarkt (fish market) in Bruges"  title="Vismarkt (fish market) in Bruges" href=belgium/bruges_fishmarket1200.jpg><img src=belgium/bruges_fishmarket.jpg /></a>
<br>Fresh prawns at the Vismarkt (fish market) not far from the quay at Rozenhoedkaai.  The market is closed on Sundays and Mondays.
</section>
<section><img alt="De Halve Maan brewery in Bruges"  title="De Halve Maan brewery in Bruges" style="width:100%;" src="belgium/bruges_brewery.jpg" />
<br>At the end of your visit to Bruges you might want to visit the De Halve Maan brewery at Walplein 26 (enter by Walstraat) and try its famous Straffe-Hendrik (at 4.25 euros a glass) or Brugse Zot beer in the open-air terrace.  You can even join a conducted tour of its brewery.  <a href="https://www.halvemaan.be/en/visit/brewery" target=_blank><em>More here.</em></a>
</section>
