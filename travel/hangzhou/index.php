<p><table width=300 align=right><tr><td valign=top><a href=hangzhou_lake4.jpg target=_blank><img src=hangzhou_lake4.jpg width=300 height=225></a></td></tr></table>If you should be in Shanghai and miss out Hangzhou you might regret it for the rest of your life.  After all it is only one hour and 15 minutes by the bullet train and there are very regular and frequent trips.  To spend the whole day contemplating and walking round the Westlake (literal translation of its Chinese name Xihu) alone is enough to make the trip worthwhile but besides the lake, it also has a vibrant night market at Hefang Jie (Hefang Street) nearby which will take anyone's breath away.  Two important points to note though.  The train to Hangzhou does not depart from the main railway station of Shanghai but from its South Station.  Likewise this train does not arrive at the principal train station in Hangzhou but in its East Station.  So don't get excited if you see its final destination printed as East Hangzhou station on your train ticket.
<br />

The photos of its world-renowned lake here will speak for themselves so will the photos of its very animated night market at Hefang Road with makeshift stalls and permanent shops selling mainly souvenirs and ornamental objects.
<table width=750><tr><td valign=top><img src=hefangjie.jpg width=375 height=281></td><td valign=top><img src=food_alley.jpg width=375 height=281></td></tr></table>

In fact you can spend a whole night here just looking for that special silk dress.  Or ornamental fans that Hangzhou is also famous for.  Or have your name engraved on stone in Chinese characters.  And what about bringing a pair of chopsticks as a souvenir?  No, not the ordinary pair of chopsticks that you are given in Chinatown restaurants but beautifully and artistically-made ones.  You will have no difficulty finding such a pair as there are at least three shops that sell nothing but chopsticks!  But if you are buying from the stalls don't be shy to haggle over the price because bargaining is the name of the game here (unless it's in the shops).
<br />But Hefang Street is also the place for tasting all kinds of exotic food and snacks.  This particular alley only appears at the far end of the market. There you will be confronted with stalls selling all kinds of local foodstuffs.  Don't expect any table service here though.  After ordering and paying for your food you will have to bring it yourself to any table along the alley and eat side by side with the local people.

<br />But if roadside stalls are not your cup of tea and you want the sleek type of proper restaurants you will not be disappointed neither.  For there is one whole street here devoted to gourmets and restaurants.  It is the Gaoyinjie (Gaoyin Street) but whatever the name you will not miss the brightly illuminated neon lights that lit up the whole road.  In fact if you should walk past one end of the local foods alley you will meet this "gourmets' street" headon.
<br /><table width=300 align=right><tr><td valign=top><a href=taichichuan4.jpg target=_blank><img src=taichichuan4.jpg width=300 height=225></a></td></tr></table>If you should stay overnight at Hangzhou make sure you get out of bed early to see how religiously the Chinese (especially the elderly) take to their tai chi chuan exercises.  I talked to one souvenir shopkeeper at the lake who was still dressed in his tai chi chuan outfit when opening his stall.  He said he would perform one hour of tai chi chuan exercises every morning before opening up for business.  That puts him in great form for the rest of the day.  In fact, as early as 06h00 groups of tai chi chuan enthusiasts already start gathering in different parts of the lake.  Women seem to outnumber men and besides the exercise, a number of groups even have choreography and music to match.
<br />A legendary restaurant that is expensive by local standards but within the pocket of most tourists is the Lou Wai Lou Restaurant which is located in Solitary Hill.  Just ask around and you can be sure someone will put you in the right direction.  It's so wellknown for its Hangzhou dishes that you should plan to have your lunch here on your excursion to the lake.  One of its speciality dishes, and which happens to be among the cheapest on its menu, is braised pork.  If you have never eaten pork skin before you will never believe that it could be that succulent.  But don't ask for the piece you see on the menu or you will never be able to finish it (besides it's something one shouldn't eat too much of).  Just ask for a small piece for one person.
<br /><table width=300 align=left><tr><td valign=top><img src=making_greentea.jpg width=300 height=225></td></tr></table>Hangzhou is also famous for its Dragon Well Tea, more popularly referred to as green tea.  The photo here shows the tea-seller preparing the tea.  He keeps on stirring tea-leaves on the pan for an hour or so in order to toast it.  The pan is really hot and it is unbelievable how his bare hands could withstand the heat (question of getting used to it, he says).  So if you want tea straight from the manufacturer this is where you can have it (it cost me just 20 RMB for a small tin). 
<br />As you spend your day walking round Westlake you are likely to come across the famous Lingyin Temple, which, believe it or not, was first built in 326 AD though it has been rebuilt 16 times since.  Continuing your walk you will be fascinated to come across such names as the Three Pools Mirroring the Moon or the Calm Lake Reflecting the Autumn Moon or the Breeze-Swaying Lotus the Winding Corridor.

