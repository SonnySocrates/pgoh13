<h1>Introducing an exquisite Chinese dish</h1>

<p>This dish - yet another version of the famous <i>tau yu bak</i> (braised pork in soy sauce) - is simply delicious and I bet anyone, even if he is not Chinese, can cook it successfully provided he is able to obtain the necessary ingredients.  Oh, by the way, this dish is not often found in Chinese restaurants so if you are hung on this dish you have to cook it for yourself!<br />  
I know how frightening many recipe books can be, with a list of more than a dozen ingredients needed to make just a single dish - enough to scare the daylights out of you.
<br />Being such a person myself (I mean, someone who is easily put off by complicating recipes) I will include here only what is absolutely necessary so that even a novice will be able to give it a try.
<br />
However, although actual preparation time is at a minimum, it needs an hour to simmer, so prepare it well ahead of mealtime.  Cooked this way, the fatty part of the pork (including the normally thick skin) is really heavenly.  As a matter of fact there is not too much of the original fat left as much of it has been "drained" away.
</p>

<h2>Recipe for pork belly in Chinese cooking wine</h2>
<p>
