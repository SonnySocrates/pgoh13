<u>Ingredients (for 4 persons):</u>
<UL>
<LI><i>4 thick slices of pork belly (about 600gm)
<LI>a sheaf (6 to 8) of spring onion leaves
<LI>a big piece of ginger
<LI>Chinese cooking wine
<LI>loose brown sugar (else use white sugar) 
<LI>soy sauce 
<LI>(optional) You can add hard-boiled eggs, tofu cut into small pieces, potato cubes and carrot to make it a unique and filling dish that can be eaten with just white rice for lunch or dinner.
<LI>(optional) boil bok choy, a popular Chinese vegie and add to the sauce
</i></UL>

<u>Preparation:</u>
<br />

1. Cut the 4 slices of pork belly into 3 cm pieces (some might prefer slightly bigger pieces).<br />
2. Put water in a pot to boil. When the water starts boiling put in the small pieces of pork belly.<br />
3. Leave them there in boiling water for at least 5 minutes, then throw away the scum and water.<br />
4. Meanwhile cut the spring onion leaves into 4-5 cm lengths and the ginger in big, flat slices. The ginger in big slices is for those who find ginger too strong to eat so they can easily be removed. I personally like to eat ginger so I mince them up tiny so they become part of the sauce.<br />
5. Put in a bowl 6 tablespoonfuls of soy sauce (you can use light or dark soy sauce, or a combination of both, according to your preference) and about the same amount of Chinese cooking wine.<br />
6. Now pour the above mixture on the boiled pork pieces to marinate.<br>
7. <img class="right" src=creuset.jpg>Heat enough oil in a wok or a "creuset" (the oil should just cover the surface).  A "creuset" is really ideal for stewing.  When the oil becomes hot, throw in the flat ginger slices and 3/4 of the chives/spring onion (leave the rest for the end) and stir  for 1 or 2 minutes.   Then add 5 or 6 tablespoonfuls of loose brown sugar (if not use white sugar) and stir again.  The beautiful part of this dish is that it is very flexible which means that even if you put in too much or too little of soy sauce, cooking wine, sugar, ginger or spring onion it will still be good so don't be afraid to experiment.<br />
8. Add in immediately the pieces of pork belly (with all the sauce in the bowl) and fry in strong heat till it changes colour.<br /> 
9. Cover the wok completely, reduce the heat and let it simmer for one hour (and not a minute less, if you want the pork to be really succulent).  And that's it, your work is done and you have one hour to do something else (though from time to time you should open the lid to give it a stir).<br />
10. Just before serving throw in the rest of the spring onion.  Serve hot with rice.
<br /><br /><u>Note:</u> How much or how little of each ingredient to use is really a matter of taste and comes with practice and through trial and error.
<br />When you buy a bottle of Chinese cooking wine (i.e. if you happen to live in one of those big cities with a Chinatown) look at the price.  It should cost around two dollars.  If it costs four or five times more it is probably Chinese wine for drinking (though it can be used for cooking as well - but why pay through your nose for it). 
<br />Hard-boiled eggs are often added to the saucepan, turning it into a dark brown colour.  Besides the eggs, I sometimes add small pieces of tofu and potato cubes to it, making the dish more complete, especially if you can't eat too much of the pork belly.  The good thing about this dish is that it can easily be kept over for the next day or two.
<br><u>Postscript:</u>  A "bigger" version of this is called "dong1 po1 rou4", which comes from Hangzhou, about 160km from Shanghai <img src=dong1po1rou4.jpg>.  Here the pork belly is cut into "chunks" of square pieces of 6-7cm.  In such a case the meat has to be left simmering for even longer.

<p>
<em>(30 July 2016)</em>  Today I tried the "dong1 po1 rou4".  I bought 600gm of pork belly then put the whole thing into a pot of boiling water.  In the meantime I put 6 tablespoonfuls of soy sauce and the same amount of Chinese cooking wine in a big bowl.  After 5 minutes I removed the pot and leave it to rest.  In the meantime I minced the ginger and cut a bunch of six spring onions into 4cm pieces.  When the pork has cooled I cut it into 5 big chunks which I then put into the big bowl to marinate.  I then heated up some oil in the French creuset and put in the ginger and three-quarters of the spring onion and stirred for a minute or two then added 6 tablespoonfuls of brown sugar and continued stirring.  Then I added in the marinated pork together with the sauce and continued frying at great heat, stirring all the time.  After that I put on the lid and left it to stew for an hour.  Added the rest of the spring onion after some time.  Delicious!
</p>

