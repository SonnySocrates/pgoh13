<FONT face=tahoma color=black size=3>Described as <em>The Eternal City</em> and glamorized in the film <em>Three Coins in the Fountain</em>, Rome is without doubt at the top of anyone's list of places to visit.
<br />But if you have an extra few days to spare in Rome then you should head for Naples <a href=naples.php target=_blank> (more here)</a>, which is less than one and a half hours away by train and is the starting point for trips to see the ruins of Pompeii <a href=pompeii.php>(more here)</a> and to the captivating island of Capri <a href=capri.php>(more here)</a>.
<br />Rome did not disappoint me but left me perplexed sometimes.  For instance in order to consult your emails at a cybercafe you will have to hand over your identity card or passport for recording and one cybercafe even made a photocopy of my document! <br />Then when I bought my railway ticket from a machine with my Visa card I didn't have to type my secret code number nor did I have to put my signature anywhere.  When I pushed my card in, a train ticket just popped out but there was no receipt for my record.  I was told that is how it is.  Strange.
<br />Upon arrival in Rome the first thing you might want to do is to buy the Roma Pass. At 20 euros it is a good bargain as it gives you unlimited public transport for 3 days plus free access to the Coliseum and a second museum of your choosing.
<br />Unlike many other cities you cannot board a bus in Rome unless you already have a ticket (which you can buy at the tobacconist's).  Boarding a bus without validating your ticket could result in your having to pay a 50 euros fine.  So make a stop at the tobacconist's first if you intend to take the bus.
<br />There is a little-known beach in Santa Marinella that is extremely popular with the local population.  It is just an hour's train ride from Rome.  The water is clean and clear but the downside is that there is little sand for walking since the whole beach is covered with parasols and mattresses (see picture below).</font></font></font>

