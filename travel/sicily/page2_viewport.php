<br>
As in Catania, the budget traveller can have difficulty in finding cheap accommodation in Siracusa (no problem for the big spenders, of course!).  However if you are looking for a hostel-type of hotel with bunk beds, then the LOL Hostel at 94, Via Francesco Crispi could be just what you are looking for.  The hostel is really well-situated, being next door to the train station (with trains going to Catania and Taormina) as well as the long-distance bus terminals and also within walking distance of both Ortygia and the Neapolis archaelogical park.  The cost of 20 euros for a night's stay includes a solid breakfast as well as bedsheets and the use of a common kitchen. </font></font>

