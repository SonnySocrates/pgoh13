<br>
<font face=tahoma size=3>Once across the bridge you will see the remains of what was formerly the Apollo Temple.  Continue walking forward until you reach the Piazza Archimede (see photo).  From here a short walk will take you to Syracuse's Duomo in the Piazza del Duomo.
<br>When in Siracusa you must also visit the Neapolis archaelogical park to see the Greek theatre and the Roman amphitheatre as well as what is known as <em>L'Orecchio di Dionisio</em> or Dionysius's ear. Make sure you bring a torchlight along if you hope to explore the cave as it is completely dark inside.  Shout out loud and hear the echo of your voice!
<br>Also do not rush to the entrance of the Neapolis archaelogical park if you have not bought your tickets yet.  The tickets are sold not at the main entrance but from the ticketing office some 100m away on the opposite side of the road (at the end of a long row of souvenir shops).

