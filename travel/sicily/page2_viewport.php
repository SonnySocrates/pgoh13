<font face=tahoma size=3>There is a direct bus from Catania to Syracuse, or to use its local name, Siracusa.  You can check the times from <a href="http://www.interbus.it/" target=_blank>here.</a>  The trip takes 1 1/2 hours and costs 6.20 euros for a single trip and nearly 10 euros for a return ticket (which has to be on the same day).  Once you arrive at the coach terminus turn backwards and continue walking straight on till you arrive at the crossroads.  There, follow the directions pointing to Ortygia (also spelt Ortigia) which is the central point of interest in Syracuse (apart from the Neapolis archaeological park). Keep walking straight on for about a kilometer till you come to a bridge.  Cross the bridge and you will be in the island of Ortygia.</font></font>

