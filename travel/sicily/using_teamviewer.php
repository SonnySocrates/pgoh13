<center><h1 style="background-color:midnightblue;color:yellow;font-family:arial;font-size:30px;display:inline-block;padding:5px 10px;text-align:center;">How to use TeamViewer to transfer files</h1></center>

<FONT face=tahoma color=black size=3>At some point in our cyber life we would have to buy a new computer.  What then?  Are we going to forgo all those nice folders containing precious files (photos, music, video clips, etc.) that have been built up lovingly over the years and which are in the old PC?  Certainly not , if only we know how to transfer all that stuff to our new PC.  This is where TeamViewer comes in.  In fact I have just used TeamViewer to transfer all my folders from my old desktop computer using Windows 7 to my new PC using Windows 8.  It is so unbelievably easy even a neophyte could do it.
<br><table style="float:right;padding-left:10px;width:300px;"><tr><td><img src=tv_screenshot.jpg width=300></td></tr></table>This is how you go about it.  First you have to download the .exe file from <a href="http://www.teamviewer.com/en/download/windows.aspx" target=_blank>here.</a>  Once you have done that click on the downloaded file to install it (do the same separately on each PC).  Of course both PCs must also be connected to internet otherwise they will not be able to "communicate" with each other.  These are the only conditions.  You don't need a cable to connect the two PCs at all.  Once installed, a page will open showing you the ID number (comprising 9 digits) as well as the password to use.  The screenshot on the right shows the ID number and password that were attributed to the computer.  <em>(The ID number on the right hand side was the number that I copied from my old PC.  It was originally blank.)</em>
<br>To make it clearer let's consider your new computer as the "local" computer and your old computer as the "remote" computer.  
What you now do is to type the ID number of the "remote" (old) computer into your new computer and check "File transfer" instead of the default "remote control".  Once you click on "Connect to partner" you will be asked to type in the password of the other computer.  Having done this click "Connection" and you will see a new page with the directory of all the folders on your new computer on the left side and the hierarchy of your old computer's folders on the right.  This is where the real business (I mean fun!) begins.  
<br><br>
<center><table style="border:1px solid black;width:718px;"><tr><td><img src=tv2.jpg></td></tr></table></center>
<br>Just choose the folder on the right side that you want to copy into your new computer and "drag" it to an empty space on the left pane.  Once you release the mouse you will see that the transfer starts immediately.  But if it is just a particular Word document (or photo or video) just click on the folder where it is and on the other side open the folder where you want to put it in then drag it there.

<br>But that's not all.  With Teamviewer it is also possible for you to control a computer anywhere as if you are physically in front of it!  In fact "remote control" is the default setting that we did not select earlier as we were more concerned then with transferring files.  So each time you run TeamViewer you'll be given a unique session password which you can use to enter another person's PC or to enable another person to enter yours in case you need remote assistance.
<br>Note: <em>If you should see any TeamViewer notices opening up in tiny windows please ignore them and most important of all, don't close them as doing so will cause your transfer to be interrupted and you will have to start all over again!





