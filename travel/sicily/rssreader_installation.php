<p><FONT face=tahoma color=black size=3>You must have seen this orange icon <img src=rss_combined.gif> in many websites (including this one).  While it is possible to read the page by just clicking on it this is not the whole idea behind it.  It is meant to be part of your collection of similar feeds from many other different websites that are of interest to you to be put together in a news reader or feed aggregator to save you the trouble of having to visit each site in turn.
<br />But first what is a news reader or feed aggregator?  Yes, as you can see from the name, originally they are meant for easy reading of up-to-the-minute news items from different news websites but since then many websites (not essentially newspapers, as this one) have used it to inform their visitors each time some page is updated.
<br />There are quite a number of such news aggregators for you to choose from but I have chosen the RssReader as an illustration here.  It is not at all difficult and it's possible to have one up and running in no time at all.
<br />I will explain the step-by-step procedures here for installing and making use of the RssReader:<br>
<br />1. Download the <b>rssreader.exe</b> file from the <a href=http://www.rssreader.com/download.htm target=_blank>rssreader.com website here.</a>
<br />2. Once downloaded double-click on the file to install.  Installation is fast and easy.
You will be asked if you want to install the RssReader for "yourself" or "anyone using this computer" (the default setting is "Just me").  &nbsp;If that's what you want just click on "Next" and that's all there is to it.  The installation takes no time at all.
<br />3. Now look for this XML or RSS icon <img src=rss_combined.gif> in any website that you are interested in, right-click the mouse button, then choose "Copy Link Location" (or "Copy Shortcut").
<br />4. Next look for this RssReader icon <img src=RssReader_icon.gif width=30> on your Desktop (it should be there if you have completed the above installation) and click on it to open.  Once it opens (it might take some time), click on "Add" in the top menu bar and paste the URL that you have just copied in the box.  Click "Next", "Next" again and "OK" and that's it.  From now on you can just keep on adding any new rss feeds that you come across in internet the same way.  This is the box that invites you to paste the URL.  By doing so you are "subscribing" to the website's rss feeds.
<br /><center><img src=add_newfeed.gif width=553></center>
<p>5.  To give you an example here are some URLs of feeds that I find interesting that you can experiment with by copying and pasting to understand how to use your RssReader (you can always uninstall and re-install it later to make a fresh start or simply highlighting one of the feeds and clicking on the "Delete" button):

<br />http://www.fark.com/fark.rss  (for news with a difference)
<br />http://rss.cnn.com/rss/edition_world.rss (for CNN world news)
<br />http://www.rottentomatoes.com/syndication/rss/complete_movies.xml (if you are a movie-goer)
<br />http://newsrss.bbc.co.uk/rss/sportonline_world_edition/front_page/rss.xml  (for BBC sport feeds)
<br />http://feeds.people.com/people/headlines  (rss feeds about celebrities)
<br />http://feeds.huffingtonpost.com/HP/MostPopular
<br />By the way the rss feed of this website has the following URL:
<br />http://feeds.feedburner.com/RssFeedsFromPgoh13com
<br />6.  From now on each morning you only need to click on the RssReader icon, then right-click on "Open RssReader" in its icon in the bottom bar to get what you see in the screenshot below and be able to read up-to-the-date news from a number of websites without much sweat.  In the example below there are 7 websites (if you have put in all the 7 URLs given above) that you can read from this one single place.  But of course you can add many more.  So as you can see, having the RssReader or any other news aggregator is simple, practical and a great time-saver.
<br />If you are not too happy with the RssReader layout you can try another popular news aggregator such as <a href=http://www.awasu.com/ target=_blank>Awasu</a>.  In fact I started out with Awasu but I found RssReader more direct and without any frills.
