<font face=tahoma size=3>Palermo is actually Sicily's capital city. If you come by coach to Palermo from Catania, the bus will stop next to the Central Railway Station of Palermo.  The coach from Catania to Palermo is run by the SAIS company.  Make sure you don't miss the coach as it is some 100 metres away from the booking office on the opposing corner of the road. The trip takes 2 hours and 40 minutes and a ticket costs 15.20 euros.  Although the return ticket costs only 24.20 euros they will refuse to sell one if the return trip is not on the same day. There is a bus every hour or every 1 1/2 hours, depending on the time of the day.</font></font>
