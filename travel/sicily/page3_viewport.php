<font face=tahoma size=3>It's easy to make a quick tour of Palermo if you have only a day.  From the Central Station cross the big square and walk down Via Roma for less than a kilometre until its intersection with Via Vittorio Emanuele.  Near here is the 700-year-old Vucciria market, already a hive of activity when dawn breaks.  So turn right if you want to see the Vucciria market (closed on Sundays) and the adjoining Piazza San Domenica else turn left for the Pretoria Fountain with its unique nude statues.  If you continue walking straight ahead after the Pretoria Fountain you will notice the spectacular Quattro Canti (meaning four corners) at its intersection with Via Maqueda in the Piazza Vigliena.  You might think you are in a museum but actually you are in the middle of a busy intersection, such is the magnificence of the four facades on four different and opposing buildings.  Continue walking and you will notice Palermo's famous Cathedral to your right.  Leaving the Cathedral to continue walking along the same road you will not fail to notice the Porta Nuova at the very end of the road and to its left the Palazzo Reale.</font></font>

