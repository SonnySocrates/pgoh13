<font face=tahoma size=3>A single bus ticket costing 1.40 euros will allow you to take both buses (in fact the ticket is valid for 90 minutes). The promenade along the Mondello beach is a popular spot for joggers.  After filling your lungs with sea air you might want to stop for a snack at Scimone (near the Alle Terraze restaurant), and try out typical Sicilian pastries such as the <em>arancini</em> (fried rice balls) or the <em>cannoli</em>, a tube-shaped dessert.
