<font face=tahoma size=3>While in Palermo don't miss the nearly 1km-long Mondello beach about 10kms away. It is really second to none.  To go there take Bus 101 from near the Central Railway Station (Stazione Centrale) or at one of its stops along the Via Roma.  Get down at Politeama and wait at the same bus stop for Bus No. 806 which will take you all the way to Mondello beach (the bus actually ends there).</font></font>
