<p><table align=right width=500><tr><td valign=top><a href=msiapix_new/Kuan_Yin_statue_Pagoda.jpg><img style="border:1px solid black;" alt="The Kuan Yin statue and the Kek Lok Si pagoda in Penang" title="The Kuan Yin statue and the Kek Lok Si pagoda in Penang" src="msiapix_new/Kuan_Yin_statue_Pagoda500.jpg" width=500></a></td></tr><tr><td><font size=2><em>The Kuan Yin statue and the Kek Lok Si pagoda dominate the skyline as you approach the Air Hitam market in Penang.  (Picture taken on a cloudy morning.)</em></font></td></tr></table><FONT face=tahoma color=black size=3> PENANG is undoubtedly one of the highlights of your visit to Malaysia.  Known all over Malaysia and Singapore for the high quality and variety of its food, it has also many places of interest to offer.
<br /> Added to this the long stretches of fine, sandy beach along Batu Ferringhi and the cool, invigorating air up Penang Hill plus the low cost of living and you have everything that a traveller can look for in a holiday destination.
<br />As if all this is not enough it has, since 7 July 2008, been listed as a UNESCO World Heritage site (together with Melaka).
<br />There are two ways you can enter Penang island if you should come by bus from the mainland - you either get off at Butterworth and walk some distance to take the ferry to cross over to George Town or the bus will take you across the Penang Bridge.  In the latter case some buses will just end up at Sungai Nibong which is the terminal for express buses and quite some distance from George Town.  Make sure you take the ones that continue to Komtar in George Town or you will have to take a taxi to continue the journey!  By the way Komtar is also where you go to for buses that go to Batu Ferringhi and Air Hitam as well.
<br />You can spend one whole day at Air Hitam.  Start with an early morning breakfast at one of the shaggy-looking foodstalls at the Air Hitam market which nevertheless offer yummy hawker dishes such as the <em>koay teow soup</em> or the <em>curry mee</em> that Penangites adore.  The fact that you are seated at one table does not mean that you cannot order food from the other stalls.  But be prepared to pay each time the food (or drink) is brought to you and not at the end of your meal.

