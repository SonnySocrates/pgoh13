<FONT face=tahoma color=black size=3>After breakfast take a walk up the market which is just along the main road.  At least you will never get lost here!  After this interesting market visit you can head for the Kek Lok Si pagoda which is a short walk away but will need some climbing to go to the top.  The Kuan Yin statue (Kuan Yin being the name of the Goddess of Mercy) is situated next to the pagoda.   Completed in 2002, it stands prominently at 30.4m.  You can either go there by car or take the "inclined lift" (a sort of cable car) as the way up is quite stiff.
<br />When you have spent the whole morning visiting the pagoda, you might want to have your lunch in one of the restaurants nearby before proceeding to the Penang Hill Railway to take the funicular train up the hill.  You will be surprised at the great speed in which the Swiss-made train is capable of going on the way up.  In fact ever since its inception in 1923, the train to Penang Hill has been changed only twice - in 1977 and in 2011.</font></font></font>

