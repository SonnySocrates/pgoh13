<h1><span style="background-color:midnightblue;">&nbsp;&nbsp;Visiting Madrid City (Part 1)&nbsp;&nbsp;</span></h1>


<FONT face=tahoma color=black size=3>Madrid and Barcelona have always been bitter rivals as far as tourism is concerned but thanks to the high-speed AVE train which has cut down travel time between Barcelona and Madrid to less than 3 hours, it is now possible for tourists with hardly a week in Spain to take in BOTH cities.</font></font></font>
<table width=750 border=1><tr><td><img title="Puerta del Sol in Madrid" alt="Puerta del Sol in Madrid" src="puertadelsol.jpg" width=750>
<br />Madrid's <em>Puerta del Sol</em> is a constant hive of activity both day and night.</td></tr></table>
<br /><FONT face=tahoma color=black size=3>Besides, whether you are coming to Spain from Paris or going to Paris for your next stop, you can easily take the Talgo night trains (the one running between Madrid and Paris is called "Elipsos Francisco de Goya" and the one between Barcelona and Paris "Elipsos Joan Miro") thus saving valuable time by sleeping on the train.  To give an example, the Talgo from Paris leaves the Austerlitz station daily at 19h43 to arrive at the Madrid Chamartin station at 09h13 next morning giving you the whole day to explore the city.  Be warned though that these Spanish trains have separate compartments for male and female passengers so if you are travelling as a couple you will have to be separated.  Besides this inconvenience the tickets are also quite expensive.  But seeing that you save a night's hotel as well as gaining a full day in Madrid it might be worth paying the extra.  Travellers on a shoestring might be interested to know that it might be cheaper to travel on a low-cost flight (such as <a href=http://www.easyjet.com/asp/en/book/index.asp?lang=en target=_blank>EasyJet</a>) than to take the train though the cheap fares normally will have to be booked months ahead.</font></font></font>



