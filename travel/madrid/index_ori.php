<br />
<br /><br /><FONT face=tahoma color=black size=3>Upon arrival in Madrid you cannot help having a funny feeling for Madrid is 600m in altitude. If you arrive in the morning settle down for "chocolate con churros".  After that you might want to make the tourism office your first stop.  You might as well go to the main one at 27, Plaza Mayor as it is located near the Sol underground station which is really the heart of the city.  Here you can even enrol for a guided tour of the city on bicycle.  It's open 7/7 from 9h30 to 20h30.


<br />If Paris is famous for its Louvre Museum, Madrid is no less famous for its Prado Museum.</font></font></font>

