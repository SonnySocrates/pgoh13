<br /><FONT face=tahoma color=black size=3>Not far from the Prado Museum and in front of the Botanical Gardens is the Caixa Forum centre (at 36 Paseo del Prado M° Atocha).  This avant-garde art gallery is worth a visit especially for its futuristic architectural design, both outside and inside. It is open 7/7 from 10h00 to 20h00.  It also houses a restaurant for visitors.  More details here (in Spanish):
http://obrasocial.lacaixa.es/nuestroscentros/caixaforummadrid/infoservicios_es.html
<br />Palacio Real (the Royal Palace) where the King Juan Carlos lives
<br />Not far from here is the Campo del Moro, the park of the Royal Palace (M° Principe Pio)
<br />While in Madrid it would be a pity not to make a side trip to Toledo, as it is only half an hour away with the hourly AVANT train from the Atocha railway station and costs only 8.30 per trip.
<br />Others though, might prefer to go to Segovia, famous not only for its monuments but also for its gastronomy - roasted cordero lechal (lamb) and cochinillo (pork) being the most popular traditional dishes.  It is just an hour away from Madrid by car though the trains take up to two hours.  It would be faster to go to Segovia with the long-distance buses from the Méndez Álvaro Station (metro Méndez Álvaro) as they take only 75 minutes.  
<br />Part of the fun in travelling is to taste foreign food and in this aspect Madrid, like other cities in Spain, has its fair share of paellas and tapas.  And what about churros and the horchata drink.  Don't leave Spain without trying these!  The best place to go for tapas is in a tasca, the Spanish equivalent of a pub, for it is here that you can rub shoulders with the Madrile&#241;os, many of whom make it a stop after work to chill out.
<br />Your shopping experience can start by getting off at the Sol metro station.  Head for Calle de Fuencarral and continue your shopping splurge around the Chuecas area!
