<FONT face=tahoma color=black size=3>Much climbing is needed before you find yourself back in the Plaza Zocodover.  It's now time to go down one of the narrower streets from Plaza Zocodover and see the town centre with shops lining both sides of the descending streets.  Many of the shops have a rich array of exquisite swords of another age,  for Toledo is famed for its sword-making skills.  Apart from swords Toledo is also famous for its pastry made from almond called <em>mazapan</em> (marzipan) that the locals are proud of.
<br />When you have walked down some distance you will probably find yourself in front of Toleda's cathedral, which the locals call Catedral de Santa Maria, though the Tourism Office here would rather call it Catedral Primada or "first cathedral" (I was told that it was so-named because it is the cathedral that "controls" all the other cathedrals in Spain).  Undoubtedly Toledo is still the religious centre of Spain today.  But other people refer to it as la Catedral de Toledo.  Facing it is the Ayuntamiento or town hall of Toledo.  So whichever route you take from Plaza Zocodover there will always be something interesting to see.</font></font></font>
