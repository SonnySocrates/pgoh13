<h1><span style="background-color:midnightblue;">&nbsp;&nbsp;Visiting Toledo in Spain&nbsp;&nbsp;</span></h1>

<center><? include "banner_ad.inc"; ?></center>

<table width=750><tr><td>

<p><table width=375 align=left><tr><td valign=top><a href=toledo_zocodover.jpg  target=_blank><img src=toledo_zocodover375.jpg width=375></a><br /><em>Plaza Zocodover in Toledo is the central point from where all visits start.</em></td></tr></table>
<FONT face=tahoma color=black size=3>If you have time left in Madrid it would be a pity not to visit neighbouring Toledo, known as the city of three cultures or the city of tolerance.  Way back in the 13th century it was inhabited by Christians, Muslims and Jews who built churches, mosques and synagogues for their worship.  Today vestiges of these still remain and have earned Toledo a Unesco Heritage Site status.  It had also been an imperial city and capital of the Spanish Empire but suffered a setback in 1561 when Philip II moved the royal court from here to Madrid.
<br />There are both half-day or full-day excursion trips to Toledo from Madrid costing about 40 or 70 euros.  But it is no big deal to go on your own as it takes only 30 minutes by train from Madrid's Atocha railway station (if you take the metro make sure you get down at the "Atocha-RENFE" station and not the Atocha station) and costs 18 euros for a return ticket (ask for <em>ida y vuelta</em>).  Upon arrival in Toledo go to the bus-stop just outside the railway station and hop onto one of the buses that has Plaza Zocodover written on it.  It will take you along the steep and winding road right up to Plaza Zocodover (it's also the terminus so you won't get lost).  This will be the focus point for your day's visits, almost all of which can be done on foot.
<br />Once you get off the bus at Plaza Zocodover climb up the main road a bit and you will have two choices.  Either you turn left and continue walking for about 100 metres to arrive at the famous Alcazar Museum or if would like to have a panoramic view of the city walk straight ahead and enter the public library building (<em>Biblioteca de Castilla-La Mancha</em>) and go up to the 9th floor.  This is the cafeteria but from here you can have breath-taking viewpoints of the city from three different angles.
<br />Once outside, walk down the main road from Plaza Zocodover for about 500 metres and you will arrive at la Puerta de Bisagra, which is of Muslim origin and a sort of doorway to the old city.
<br />If you should enter la Puerta de Bisagra and keep on climbing you will arrive at the imposing Puerta del Sol.</font></font></font>  
