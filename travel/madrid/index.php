<FONT face=tahoma color=black size=3><br />Part of the fun in travelling is to taste foreign food and in this respect Madrid, like other cities in Spain, has its fair share of paellas and tapas.  And what about churros and the horchata drink.  Don't leave Spain without trying these!  The best place to go for tapas is in a tasca, the Spanish equivalent of a pub, and it is here that you can rub shoulders with the Madrile&#241;os, many of whom stop here after work to chill out before returning to their homes and families.
<br>Want to blend yourself with the local crowd on a Sunday afternoon and eat at the popular tapas restaurants?  Then get off at La Latina metro station (Line 5, the green line on the metro map) which is near the Sol and Opera stations. Walk along Calle Cava Baja, Calle Cava Alta and its surrounding streets to decide on where you want to eat.  </font></font></font>
