<h1><span style="background-color:midnightblue;">&nbsp;&nbsp;Visiting Madrid City (Part 1)&nbsp;&nbsp;</span></h1>





<FONT face=tahoma color=black size=3>Madrid and Barcelona have always been bitter rivals as far as tourism is concerned but thanks to the high-speed AVE train which has cut down travel time between Barcelona and Madrid to less than 3 hours, it is now possible for tourists with hardly a week in Spain to take in BOTH cities.

Incidentally the same AVE train (Spanish bullet train) now takes only 1 1/2 hours from Madrid to Valencia (it used to take 4 hours) running at a speed of 438kms, making it Europe's fastest train.  </font></font></font>
<table width=750><tr><td><img title="Puerta del Sol in Madrid" alt="Puerta del Sol in Madrid" src="puertadelsol.jpg" width=750>
<br />Madrid's <em>Puerta del Sol</em> is a constant hive of activity both day and night.</td></tr></table>


<br />

<FONT face=tahoma color=black size=3>Seeing that Barcelona is imposing its Catalan character more and more with each passing day more tourists who want to see the real Spain are coming to Madrid.  At least everyone here speaks Spanish or "castellano", which is not the case in Barcelona where some Catalans do not, or refuse, to speak in Spanish, making it frustrating for the tourist who had studied Spanish in school and who wants to practise it with the local population.</font></font></font>


