<FONT face=tahoma color=black size=3><br />Though Madrid is basically flat yet it stands over 600m above sea level.  Its main tourism office is well-situated at 27, Plaza Mayor as it is not far from the Puerta del Sol (Sol metro station) which is really the heart of the city.  Here you can even enrol for a guided tour of the city on bicycle.  It's open 7/7 from 9h30 to 20h30.
