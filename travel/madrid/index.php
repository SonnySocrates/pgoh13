<br /><FONT face=tahoma color=black size=3>Anyway, whether you are coming to Spain from Paris or going to Paris for your next stop, you can easily take the Talgo night train (Madrid/Paris or Barcelona/Paris) thus saving valuable time by sleeping on the train and having the whole day to explore the city.  Be warned though that these Spanish trains have separate compartments for male and female passengers so if you are travelling as a couple you will have to be separated.  Another possibility is to travel on a low-cost flight (such as <a href=http://www.easyjet.com/asp/en/book/index.asp?lang=en target=_blank>EasyJet</a>) whose promotional fares can sometimes be lower than those of a train.</font></font></font>


