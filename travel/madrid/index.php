<tr><td colspan=3><FONT face=tahoma color=black size=3>A typical Spanish breakfast consists of <em> chocolate con churros</em> (picture on left) for 2.50 euros.  It is not just the usual type of chocolate drink but very, very thick indeed.  Others will opt for <em>cafe con leche</em> with <em>magdalena</em>, <em>bollos</em> (sweet rolls) or <em>torrijas</em> which is a soft version of the French toast.  Or if you are very hungry early in the morning you might ask for <em>patatas bravas</em> (fried potatoes) or <em>tortilla</em> (Spanish omelette), which can be 4 cms thick!  In short, breakfast is never a problem in Spain.  The middle picture shows roast chicken accompanied by cider and to order it you say <em>pollo asado con sidra</em> (you really have to click on the picture then F11 to see how tempting it is).  Give yourself a treat by having this at Casa Mingo at 34, Paseo de la Florida (Principe Pio metro station).   With its succulent chicken and its top-quality cider manufactured at the restaurant itself it is no wonder that the restaurant has been flourishing for the last 150 years.  For 15 euros you have a whole chicken plus a big bottle of cider - a great bargain indeed.  Yet the restaurant remains simple to this day - its tables cannot be any plainer.  But then you're here for good food, not for the table decorations.  Another typical Madrilenian dish is called <em>cochinillo asado</em> (roasted suckling pig) and yet another is <em>orejas de cerdo</em> (pig's ears!).  Then there is of course the paella.  You know how rich the paella can be - with huge gambas, mussels, squids and crayfish on top of the rice.  Yet it is also a dish for simple folks.  This plate of paella (picture on right), for example, with the basic ingredients of yellow rice and meat, costs only 5 euros, with bread thrown in for good measure.  Or perhaps you'd prefer to spend 40-50 euros on a <em>tapas</em> spree!  Whatever you do, make the most of your Spanish holiday!
