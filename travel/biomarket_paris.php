<h1>Cent Quatre (104)</h1>
<br><br>
<FONT face=tahoma color=black size=3>A good place you can go to on a rainy day on a Sunday (access to it is free) is what the locals call Cent Quatre (the French word for 104) as it is located at 104, rue d'Aubervilliers in the 19th district of Paris.  Its official name though is CENTQUATRE-PARIS.
<br>Actually it has another entrance at 5, rue Curial.  The closest subway stations are the Riquet and Marx Dormoy stations.  Built on the grounds of what was formerly a municipal undertakers building, Cent Quatre has been in existence since 2008 when it was first open to the public.  It is a place dedicated to multi-cultural and artistic activities.  There is also a "House for Children" where parents can accompany their toddlers and children below 5 years in activities that stimulate their development.  You can find full information (opening hours, etc.) from its website <a href="http://www.104.fr/english/" target=_blank>here.</a>
