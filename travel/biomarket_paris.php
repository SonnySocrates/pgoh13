</div><FONT face=tahoma color=black size=3>It is really difficult to find the equivalent of a food court in Paris and this is one of them.  The foodstalls here serve exotic dishes such as Moroccan couscous and tagine as well as Japanese, Italian, Lebanese and Caribbean (West Indian) food from the Antilles islands.  There is even a stall there selling dishes made from organic products.
<br />You might be able to spring a surprise on your Parisian friends by bringing them here as not many people staying in Paris are even aware of its existence.  It is located in the quiet Marais district and is inside the oldest covered market of Paris.
<br />Called "Market of the Children in Red" (<em>le March&eacute; des Enfants Rouges</em>), its two entrances are quite inconspicuous and you might pass by without hardly noticing them, which explains why not many Parisians know about it, though it is well-known to all those staying in the third district of Paris.
<br />Yet it is quite unlike any other place to have lunch in Paris and is the closest one can get to the alfresco food courts that you find in Asian countries, though on a much smaller scale.  Tucked away at <em>39 rue de Bretagne</em> among numerous restaurants and caf&eacute;s in the area, it is within 5-10 minutes walk from either the Arts et Metiers, Temple or the St-Sebastien Froissart underground stations.
<br />According to the town hall, in 1534 an orphanage was built at the request of Madeleine de Navarre.  This was situated in a street near the market and the orphans were all dressed in red, the colour of charity.
<br />Hence the origin of its unusual name.  The market itself is small and expensive as the stalls sell only bio (organic) vegetables, but I think its conviviality is more in the alfresco aspect of its six or seven eating stalls that are part of the market.
<br />Unlike the food courts in Asia though, where you can sit at any table you like and order from the stalls near and far, here you have to sit at the tables next to the food stall and move on to another table if you wish to order food from some other stall.  Oh, by the way, the best time to go is just after 12 noon.  Don't bother to come after 13h00 unless you are prepared to wait in the queue for a seat as all the tables would have been taken up by then.  One more thing...as with all markets in Paris, it's closed on Mondays!

