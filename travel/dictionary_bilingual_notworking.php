<b>The hyphen in Malay words:</b>
<br />Please note that hyphenated words in Malay must always be typed with the hyphen if you expect to find them here (eg. undang-undang, NOT undangundang, mengapi-apikan, NOT mengapiapikan).  This is because the hyphen is an integral part of a Malay word.  I know it's just the opposite in my English-Malay section but then the hyphen in English words often disappears over time, which is not the case with Malay hyphenated words!
<br /><br />

<b>Type Malay word with its prefix and suffix:</b>
<br />
One innovative feature of this dictionary is that you can type a word with its prefix and suffix (eg. <em>mengenakan</em> or <em>menyertai</em>)  to get its meaning and also be told from what word it comes from.  In a typical Malay dictionary, such words would only appear under their root words (in this case <em>kena</em> or <em>serta</em>) and if you don't know what their root words are, you will never be able to look them up in a Malay dictionary.  While this might be obvious to many Malaysians, it is not the case for students of Bahasa Malaysia.  Take the very common word <em>sedikit</em> (=a little), for example.  You will not find it in most dictionaries (not even in Dewan Bahasa dan Pustaka's "Kamus Dewan" Edisi Keempat).  Why?  Simply because you have to look it up under its root word, which is <em>dikit</em>.  Same for <em>pejabat</em> (=office).  You have to look for it under <em>jabat</em>.  How many students would be able to do this, I wonder.
<br />As a result of this arrangement, you will not find any of the derived words (with prefixes or suffixes) grouped together under the root word here as is done in most Malay dictionaries.  Each word that has a prefix or suffix will be treated here as an independent entry.  Thus if you happen to come across the word <em>perkelahian</em>, for example, in your reading, just type the whole word as you see it (with its prefix and suffix).  Don't try to look it up under its root word (which is <em>kelahi</em>).   And what about verbs like <em>mengawal</em>?  How would you know that the root word is <em>kawal</em> and not <em>awal</em>?  This is one aspect of Malay that makes it more difficult than other languages.
<br />As almost every verb in Malay can be made passive by just putting the prefix di before it, I am afraid I have to leave all these verbs out.  Just be aware that if the verb "to beat" is pukul, then dipukul would simply mean "is beaten", "are beaten", "was beaten" etc.
