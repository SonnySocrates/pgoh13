<br />You just walk from one group to another as your fancy takes you, lingering here for a few minutes, there for half an hour or more.  The pictures here unfortunately can only bring out what you see, not what you hear or even the amicable and jovial ambience that fills the air and permeates through each and every visitor.  You will return home with the answer why the Chinese have so little and yet are so happy in life.  And it has nothing to do with their religion.
