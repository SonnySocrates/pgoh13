<FONT face=tahoma color=black size=3>Enough has been said about the Great Wall of China, Tian An Men and its Forbidden City and the Summer Palace.  If you should ask me what is the attraction not to be missed in Beijing I would say without any hesitation that it is the Temple of Heaven (Tiantan Gongyuan).   In fact I would describe it as a spontaneous playground for Chinese adults young and old (some foreign tourists cannot resist but join in) and where the joy of living is everywhere.
<br />Even if you don't like temples this is one temple you simply have to visit when you are in Beijing.  Why?  Because when you come here you are not just visiting the temple itself.  You are also taken to the very heart of Chinese leisure life.  In the park that is part of the temple grounds and for which you pay a reduced admission fee (15 yuan instead of 35 yuan for a through ticket) you have the privilege of observing at first-hand a microcosm of today's Chinese leisure life as well as the Chinese outlook on life.  Whether you are with them on the temple grounds or along the Long Corridor that leads to the Hall of Prayer, you will forget all the worries that you have and become as zen as the local populace.  Go on a Saturday or Sunday morning (I was there on a Saturday morning) when many of the "performers" would be free from work and you will see what I mean.  I have never come across a park that is so full of life as the amateur performers are all there just to amuse themselves.  That they entertain the onlookers at the same time is just a coincidence as they perform without any monetary expectations.
<br />One of the most popular activities among the locals is community singing.  By buying a photocopied collection of traditional Chinese songs costing 15 yuan anyone can join in the singing (ok, ok, I don't expect you to do so!)
