<h1>Visiting Singapore</h1>

<br />
<b>This article and photos have been contributed by Heidi Bue of Germany*</b>
<p><img src=floating_stadium.jpg width=400 align=right style="padding-left: 15px;" />
<FONT face=tahoma color=black size=3>Open in 2010, the Marina Bay Sands continues to wow visitors to Singapore today.  It is a world by itself and the huge complex includes a Las Vegas-style casino as well as a luxury hotel with over 2,500 rooms.  We have heard of floating markets and floating mosques.  Now Singapore comes up with its own floating stadium on the waters of the Marina Reservoir nearby (see photo on right).  The multi-coloured patches in the photo comprise the grandstand which can seat up to 30,000 spectators.  Among the events held here were the National Day Parade, New Year's Eve countdown as well as the inaugural Formula 1 Singapore Grand Prix.
<br />The photo below shows its SkyPark featuring a 150 metres long infinity-edge pool (infinity pools produce a visual effect of water extending to the horizon) with the three 55-storey towers of the Marina Bay Sands Hotel "supporting" it.  Mind-boggling indeed!
<br />Be warned though that the pool is for hotel guests only so unless you are willing to fork out the S$450 per night stay  (and this is about the cheapest room you can find there!) you might as well forget about that vertiginous swimming experience!  But <a href=http://www.dailymail.co.uk/news/article-1289194/Marina-Bay-Sands-resort-opens-Singapore.html target=_blank>go here</a> to have an idea of what it is like to swim there.</p>
