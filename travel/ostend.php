<h1>Visiting Ostend in Belgium</h1>

<p>
<img src=belgium/ostend_floralclock1000.jpg />
Ostend, the favourite holiday spot for the Belgian kings Leopold I and Leopold II, is one of those cities that make travelling a pleasure as everything is within walking distance.  It is possible to make a day trip to Ostend from Brussels as the train ride takes only 1 hour 15 minutes.  And don't miss it if you are in Bruges as it is only 15 minutes away by train.  Be warned though that this Flemish city is not too tourist-friendly.  Everything is only in Flemish so you are likely to have a hard time finding your way around and a city map from the Tourism Office should be acquired before you start your wanderings.  You will find the Tourism Office next to the Casino-Kursaal (picture below) which serves as a good point of reference as it is located midway along the esplanade.  Budget travellers will be glad to learn that since February 2014 a youth hostel belonging to HiHostels has opened in Ostend.  You can find more information <a href="https://www.hihostels.com/hostels/oostende-de-ploate" target=_blank>here.</a>  (Picture above shows the unique floral clock at Leopold Park.)
</p>
