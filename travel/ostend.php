<article class="extendfull">
<section style="background-color:#cccccc;">CASINO-KURSAAL
<br><a alt="The Casino-Kursaal at Ostend, Belgium"  title="The Casino-Kursaal at Ostend, Belgium" href=belgium/ostend_casino1200.jpg><img src=belgium/ostend_casino.jpg></a>Hailed as a seaside resort in Belgium, Ostend has two great names linked to it.  One is the painter James Ensor, who is associated with the expressionism and symbolism movements and the other is the musician Marvin Gaye, nicknamed "The Prince of the Soul".  <em>More below*</em>
</section>
<section style="background-color:#cccccc;">THERMAE PALACE
<br><a alt="Thermae Palace at the Albert I-Promenade in Ostend, Belgium"  title="Thermae Palace at the Albert I-Promenade in Ostend, Belgium" href=belgium/ostend_leopoldII_statue1200.jpg><img src=belgium/ostend_leopoldII_statue.jpg /></a>
A good starting point for your leisurely evening walk along the Albert I-Promenade is the Thermae Palace Hotel.  Head for the arcade where you will see the looming equestrian statue of Leopold II.  It's quite awe-inspiring.
</section>
<section style="background-color:#cccccc;">DANCING WAVES
<br><a alt="Dancing Waves (Dansende Golven) at the Albert I-Promenade in Ostend, Belgium"  title="Dancing Waves (Dansende Golven) at the Albert I-Promenade in Ostend, Belgium" href=belgium/ostend_dancingwaves1200.jpg><img src=belgium/ostend_dancingwaves.jpg /></a>
Another sight that will catch your eye as you stroll along the esplanade fronting the sea is a sculpture by Patrick Steenon called Dancing Waves (Dansende Golven).
</section>
<section style="background-color:#cccccc;">ROCK STRANGERS
<br><a alt="Rock Strangers at the Albert I-Promenade in Ostend, Belgium"  title="Rock Strangers at the Albert I-Promenade in Ostend, Belgium" href=belgium/ostend_rockstrangers1200.jpg><img src=belgium/ostend_rockstrangers.jpg /></a>
As you walk further down you will come across what is known as "Rock Strangers", a number of huge, beaten-out-of-shape structures in red.  They are the works of Arne Quinze, a Belgian conceptual artist who started out as a graffiti artist in Brussels.
</section>

<section style="background-color:#cccccc;">FISH MARKET
<br><a alt="Fish auction market in Ostend, Belgium"  title="Fish auction market in Ostend, Belgium" href=belgium/ostend_fishmarket1200.jpg><img src=belgium/ostend_fishmarket.jpg /></a>
Next you will pass by the daily fish auction market where the day's catch is brought in every morning 7 days a week (it's only closed on Christmas and New Year's Day) between 6 and 8 in the morning.  No wonder you should only be eating fish when in Ostend!
</section>
<section style="background-color:#cccccc;">AQUARIUM
<br><a alt="Ostend's North Sea Aquarium along Visserskaai"  title="Ostend's North Sea Aquarium along Visserskaai" href=belgium/ostend_aquarium1200.jpg><img src=belgium/ostend_aquarium.jpg /></a>
Just next to the fish market is Ostend's North Sea Aquarium along Visserskaai, which is actually an extension of the Albert I-Promenade.  This is quite a small aquarium but has a good collection of sea-shells.  As you leave the aquarium look towards the port and if you are lucky you might see the ferry boat coming in or maybe it's already docked and waiting for the last passengers.
</section>
<section style="background-color:#cccccc;">
FREE SHUTTLE BOAT
<br><a alt="Free shuttle boat at Vissersplein in Bruges"  title="Free shuttle boat at Vissersplein in Bruges" href=belgium/ostend_shuttleboat1200.jpg><img src=belgium/ostend_shuttleboat.jpg /></a>
It is with this free shuttle boat at Vissersplein (in case you want to look it up on your map) that you will be able to visit Napoleon's Fortress.  The short 10-minute ride will take you to Maritiem Plein.  There is nothing much here except Napoleon's Fortress.  If you are not too keen to take the long walk to the fortress you can just enjoy the boat trip back to Vissersplein.
</section>
<section style="background-color:#cccccc;">
AMANDINE MUSEUM
<br><a alt="Amandine museum in Bruges"  title="Amandine museum in Bruges" href=belgium/ostend_amandine1200.jpg><img src=belgium/ostend_amandine.jpg /></a>
Next on view at Vissersplein is the Amandine fishing boat which has since become an interactive museum.  It was on 3rd April 1995 that it entered Oostende harbour for the last time after its fishing expedition on the waters of Iceland.  <a href="http://www.zeilschipmercator.be/amandine/en/" target=_blank>More here...</a>
</section>

<section style="background-color:#cccccc;">
RAILWAY STATION
<br><a alt="Ostend's railway station"  title="Ostend's railway station" href=belgium/ostend_railstation1200.jpg><img src=belgium/ostend_railstation.jpg /></a>
Ostend's railway station is just behind the Amandine museum so if you should arrive by train you would already be in the port and the centre of things. If you want to head for the promenade turn right as you leave the railway station.  Keep on walking and you will arrive at the beach.
</section>
<section style="background-color:#cccccc;">
BEACH
<br><a alt="Ostend beach"  title="Ostend beach" href=belgium/ostend_beach1200.jpg><img src=belgium/ostend_beach.jpg /></a>
The seafront is Ostend's principal attraction.  Note how fine and clean the sand is on the vast Ostend beach.  There is room for everybody here unlike some beaches where you have to inch your way through the sun-bathing crowd.
</section>
<section style="background-color:#cccccc;">
LEOPOLD PARK
<br><a alt="Leopold Park in Ostend"  title="Leopold Park in Ostend" href=belgium/ostend_park1200.jpg><img src=belgium/ostend_park.jpg /></a>
Leopold Park was built in 1860 during the reign of King Leopold II and was based on British parks with ponds, paths and flowerbeds.   The magnificent floral clock is at the entrance to the park, which is hardly a 5 minutes' walk from the Casino-Kursaal.
</section>
<section style="background-color:#cccccc;">
SHOPPING
<br><a alt="Kapellestraat, shopping street of Ostend"  title="Kapellestraat, shopping street of Ostend" href=belgium/ostend_kapellestraat1200.jpg><img src=belgium/ostend_kapellestraat.jpg /></a>
The principal shopping street in Ostend is the Kapellestraat, not far from the St. Peter and St. Paul Church.  If you continue straight on from here to Vlaanderenstr. you will find yourself at the Albert I-Promenade again.
</section>

<br>

<section style="background-color:#cccccc;">
ALBERT I-PROMENADE
<br><a alt="Albert I-Promenade in Ostend"  title="Albert I-Promenade in Ostend" href=belgium/ostend_promenade1200.jpg><img src=belgium/ostend_promenade.jpg /></a>
There are numerous souvenir shops, bars as well as restaurants along this long promenade along which you can enjoy the view of the seafront and fill your lungs with the invigorating air.
</section>
<section style="background-color:#cccccc;">
CHANGING BOOTH
<br><a alt="Changing booth at beach in Ostend"  title="Changing booth at beach in Ostend" href=belgium/ostend_changingbooth1200.jpg><img src=belgium/ostend_changingbooth.jpg /></a>
Unlike other beaches where you have to look for a secluded area in order to change, here you have changing booths for your use.  You will also find individual cabins further down the esplanade.
</section>
<section style="background-color:#cccccc;">
NEO-GOTHIC CHURCH
<br><a alt="St. Peter and St. Paul Church (Sint-Petrus-en-Pauluskerk) in Ostend"  title="St. Peter and St. Paul Church (Sint-Petrus-en-Pauluskerk) in Ostend" href=belgium/ostend_church1200.jpg><img src=belgium/ostend_church.jpg /></a>
The St. Peter and Paul Church (<em>Sint-Petrus-en-Pauluskerk</em>) is a neo-Gothic church built in 1907 with a memorial chapel dedicated to Louise-Marie, Belgium's first queen.  Just behind it is the <em>Peperbusse</em>, an old church tower.
</section>
