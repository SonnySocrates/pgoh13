<p style="font-size:20px;font-weight:500">If you haven't been to La Rochelle yet this is one place well worth a visit.  In fact it is not surprising that La Rochelle is the third most visited city in France (at the time of my visit), with more than three million visitors a year.</p>
This charming seaport has everything to satisfy the holidaymaker.  It combines history (one look at the Vieux Port at the waterfront with its turrets  and  mediaeval buildings will show you how steeped in history this city is) with modernity (you'll even be able to find shops selling state-of-the-art objects here).  A walk along <i>la rue du Palais</i> is a must as it combines the best of both worlds - the ancient and the modern.
<br>Yet the inhabitants still retain the fast-diminishing charm of the provincial people who still find time to talk to visitors and do not let the stress of modern living get the better of them.
<br>Besides it is just a bridge away from the Island of R&eacute;, the equally popular holiday destination for the French themselves.  These two places are well worth a visit at any time of the year but particularly so in July or August to enjoy swimming in the sea.
<br>The city is at its best during the yearly French songs festival over five nights, popularly known as <a href=https://www.francofolies.fr/ target=_blank>Francofolies</a> (photo above).   It normally coincides with the fireworks display along the whole of the esplanade at Avenue Michel Crepeau round midnight every July 14 to commemorate Bastille Day (French National Day).  So if you are at La Rochelle on that day you will be able to enjoy the concert as well as the gigantic fireworks display.
<br>The train journey from Paris by the TGV (France's bullet train) takes only three hours making a weekend visit feasible.  In fact if you are pressed for time you can spend one whole afternoon just visiting the Mus&eacute;e Maritime, the M&eacute;diath&egrave;que and the Aquarium (inaugurated in 2001) as they are all conveniently located next to each other and to the main railway station as well.  From here it's only a stone's throw to the scenic port, the old quarter filled with alfresco restaurants and the shopping area
<br>In fact, its strength as a tourist destination is that it allows for leisured strolls either along the old quarter or in its shopping area.  There is little trace of the hustle and bustle of city life and car honks from impatient motorists are quite rare indeed here.
Compared to the famous French Riviera on the south of France, the well-kept beach at Port des Minimes in La Rochelle is still relatively unspoilt by the big spenders.  To go to the Minimes beach you can either take the "sea bus" (bus de mer) from the <em>vieux port</em> (old port) near the Chain Tower end for 2.50 euros or a numberless bus simply called ILLICO from the bus terminus at Place de Verdun for 1.30 euros.  But watch out, this bus doesn't run on Sundays and public holidays in which case it is replaced by local bus number 43.</p>
