<br /><b>Public transport in Amsterdam:</b>
<br />For unlimited travel in trams, buses and the subway in Amsterdam you might want to buy a tourists' card upon your arrival.  It costs 7 euros for 24 hours, 11.50 euros for 48 hours and 15.50 euros for 72 hours (July 2011 prices).  If you are a museum buff you might want to pay more for the <em>Iamsterdam</em> City Card, which besides the unlimited public transport, also includes free museum admissions as well as a canal cruise.  Go to <a href=http://iamsterdamcard.com/ target=_blank>their website</a> for full details.  There are other options <a href="http://en.gvb.nl/reisinformatie/welke-kaart/Pages/Af-en-toe-reizen.aspx" target=_blank>here.</a>
