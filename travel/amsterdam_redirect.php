<FONT face=tahoma color=black size=3>Amsterdam, capital of the Netherlands, is a truly cosmopolitan country and the epitome of tolerance, whether it's drugs, prostitution or immigration.  
<br />It is not a "glamour" city in the same way that Paris, London or Barcelona is, yet the first-time visitor is bound to be awed in more ways than one - by its architecture, its leafy streets, its numerous canals and its healthy way of life with bicycles being truly the king of the road.

