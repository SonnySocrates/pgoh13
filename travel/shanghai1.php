<h3>Beijing-Shanghai High-Speed Railway</h3>

<br><span style="font-size:24px">After hosting the World Expo 2010, the city of Shanghai has taken the limelight out of Beijing.  But despite the great distance that separates it from Beijing one can easily board the night train  at Beijing late in the evening and wake up the next morning in Shanghai.  The Beijing-Shanghai High-Speed Railway, construction of which started in April 2008, is now running and takes less than 5 hours travel time between Beijing and Shanghai!  Full details of the Beijing-Shanghai train can be found <a href="https://www.klook.com/china-high-speed-rail/57-beijing/59-shanghai/" target="_blank"><font color=red>here.</font></span></a>
