<center><h1>&nbsp;&nbsp;Visiting Capri Island&nbsp;&nbsp;</h1></center>

<FONT face=tahoma color=black size=3>If you take a boat from Naples (the hydrofoil from <em>Molo Beverello</em> takes only 75 minutes) you will arrive at <i>Marina Grande</i>, the main port of the island of Capri.  From there you can either take the funicular train or a mini bus (orange in colour) to get to <i>La Piazzetta</i> (also known as <i>Piazza Umberto I</i>), which is the heart of Capri itself.  As the price is the same for both the train and bus you might want to take the bus up and come down by the funicular.  You will be surprised at the number of luxury shops and expensive restaurants you find along the narrow streets and alleyways of the village.  But there are no words to describe the amazing natural beauty that the island has to offer.  There is really something magical about it.  The panoramic views of the scenic seashores as well as the houses carved out of the rocky hillsides are enough to take one's breath away.  To go even higher up the island though you'll have to take the bus for Anacapri.  The <i>Grotta Azzurra</i> (blue grotto or cave) is another major tourist attraction in the romantic Isle of Capri.</font></font>

