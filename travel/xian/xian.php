<br />The following photos show the main places of interest to visitors to Xian.  The Terra Cotta Army Museum, which is where the terra cotta warriors (<em>Bingmayong</em>) are, is of course the highlight of a visit to Xian.  The warriors, although in existence since the days of Shi Huang Di, the first Emperor of China, were only found accidentally on March 29, 1974 by farmers drilling a well hole.  New excavations have again been re-started in June 2009 in the hopes of making more discoveries and unearthing more relics.  The photo to the right gives an overall view of Pit No. 1, the biggest and most important of the three pits at the museum.

