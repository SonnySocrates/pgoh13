<FONT face=tahoma size=3><em>(Added on October 2, 2015)</em>  If you are really down and out and find that even one euro for a cup of coffee is too much (but even McDonald's or Quick charges at least 1.30 euro for a cup of coffee), you will be glad to learn that there is a place in Paris where you can have a real cup of coffee for only 45 euro cents. Not only that, you can stay in this clean, bright and spacious café the whole day if you like to read a novel or write a book and nobody will bother you.  You might find people playing chess quietly at a corner and others reading the newspapers provided there.  And if you should come in the morning for breakfast (it opens at 9 a.m.), you can have a complete breakfast (orange juice, croissant and coffee) for only 1.60 euro.  The place is called Café des Petits Freres and is at 47 rue des Batignolles (at its corner with rue Bridaine).  It is in fact run by a charitable association called "Les Petits Frères des Pauvres" and the workers here are practically all volunteers who will most likely serve you with a smile (I understand that they have a rotating system so it is unlikely that you will be served by the same person if you should return the next day).  The nearest metro is Rome (take rue Boursault and  third street to the right) and it's open Monday-Saturday from 9h to 12h30 and again from 14h to 17h (certain days to 18h) but closed on Tuesday afternoons.  However during school holidays it is only open in the morning.

