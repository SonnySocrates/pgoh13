<br /><b>WHERE TO EAT CHEAPLY IN PARIS:</b>
<br />Apart from the very expensive French restaurants, the tourist on an average budget can eat typical French dishes at what is called a <em>brasserie.</em>  This should not be a problem as you can easily find them if you just keep your eyes open.  Many of them double up as a caf&eacute; with its sidewalk tables in summer.  However if you are after cheap food then you probably have to eat at one of the many Chinese and Indian restaurants that you find in every street.  I have come to know of a few that are really for those on a tight budget.  They too have a right to travel!
<br />One of the cheapest you can get is the one that sells Thai dishes in the 18th district of Paris.  It's called <A HREF="thai_resto.php" target="_blank">Maison Thai</A> (click on the name for a full description).  Here you can have a full meal (white rice with two Thai dishes) for only 4.50 euros.  It is quite a small place so don't come at 13h00 or you will have to join in a very long queue!
<BR>Another equally cheap restaurant where you can have a meal with a starter, main dish, cheese and dessert for only 5 euros is what is called a "social restaurant" and is really intended for the socially underprivileged who are able to have lunch and dinner here every day for free.  Read about it <A HREF="santeuil_resto.php" target="_blank">here.</a>
<br>While eating at good French restaurants or <em>brasseries</em> is a "must" for most tourists, those on a tight budget will be glad to learn that they can still eat well and cheaply in an expensive city like Paris by eating at some of the many Indian or Chinese/Vietnamese restaurants in Paris.  But how do you know which are the good ones?  I have handpicked a few for you <a href=chinese_resto.php#popular target=_blank>here.</a>
<br>But if it is cheap Indian food you are after, the best area to go to is La Chapelle (NOT the same as Porte de La Chapelle) in the 10th district of Paris.  Get down at the underground station of the same name and turn to your left as you exit the station.  Once you cross over to the other side of the road you are right in the heart of Paris's Little India.   You can be quite sure that any one of the numerous restaurants here will serve authentic (hey, by "authentic" I mean spicy!) Indian dishes and at quite reasonable prices too.  The ones at the famous Passage Brady are watered-down versions of Indian curry meant for European palates.  Their prices too are also targeted at tourists!
<br>As for Indonesian food, the D'jawa Restaurant at 148 rue Montmartre (Monday-Friday 12h-14h30 and 19h-22h) or at 54 rue de Miromesnil (Monday-Friday 12h-14h30 only) is a good bet for those who can't afford to eat at the few (and therefore expensive) Indonesian restaurants in Paris.  In fact it has its website <a href="http://www.djawa.fr/menu_box.html" target=_blank>here.</a>

