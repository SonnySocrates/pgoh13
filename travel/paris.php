<FONT face=tahoma size=3><b>GENERAL INFORMATION FOR TOURISTS:</b>

<br />1.  <FONT face=tahoma color=black size=3>After being closed for renovation works for about five years with its reopening dates announced, then postponed, on a number of occasions, the <strong>Mus&eacute;e Picasso Paris</strong> finally reopened on 25 October 2014.  Full details of its opening hours, etc. from its official website <a href=http://www.museepicassoparis.fr/en/ target=_blank>here.</a>  Please note that unlike other museums in Paris which are closed on Tuesdays, the <strong>Mus&eacute;e Picasso Paris</strong> is open on Tuesdays but closed on Mondays.
<br>2.  If you want to be in Paris during <strong>Paris Plage</strong>, a month-long yearly event along the banks of the Seine River as well as at Bassin de la Villette (near the Stalingrad underground station) you should try to plan your holiday to Paris to fall between mid-July and mid-August.   Its sandy "beaches", numerous activities such as open-air dancing, music and free deckchairs are meant to make stay-at-home Parisians feel that they're on holiday.  But there's no reason why you tourists cannot enjoy yourselves here too.  A great place to bring the kids!
<br />3.  To be sure that the <strong>Catacombs</strong> at Denfert Rochereau is open go  <a href=http://catacombes.paris.fr/en/homepage target=_blank>here</a> to check from their official website.
<br />4.  On the <strong>first Sunday of every month</strong> admission is free to the National Museum of Modern Art at the Pompidou Centre (Beaubourg), the Louvre Museum, Mus&eacute;e Picasso, Quai Branly Museum and almost all the other museums.
<br><strong style="color:red;font-weight:bold;">Warning: </strong> Certain museums, like the Louvre (but not the Pompidou Centre), are only free on the first Sunday of the month from October to March.  For the other six months of the year (April to September) it is not free on the first Sunday of the month.  Be warned.
<br />5.  All <strong>markets</strong> (and almost all swimming pools) are closed on Mondays while all museums (the Pompidou Centre is considered as a museum) are closed on Tuesdays.  (However the Mus&eacute;e d'Orsay, the Mus&eacute;e Picasso Paris and the Mus&eacute;e du quai Branly are open on Tuesdays and closed on Mondays.)

<br /><br />If you are looking for information on the interesting places to visit when you are in Paris, go to <a href=paris_page1.php>"What to do, Where to go in Paris"</a> as that page is intended for tourists visiting Paris.  This page deals with other aspects of Paris that even the locals might find useful.
