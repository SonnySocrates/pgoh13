<h2 span style="background-color:midnightblue;">How to pick out the pickpockets before they pick your pockets!</font></h2>

<table bgcolor="beige" class=padding style="border:1px solid black;"><tr>
<td width="700"><span style="font-family:arial;
	font-size: 12px;font-style:italic;text-align:left;"><b>Men</b>, beware of some young Romanian women who get very close to you in a crowded subway train.  A number of them have been picked up by French police for pickpocketing but many more are still operating.
<br /><b>Women and old people</b>, look around you before you use an isolated ATM machine on the roadside.  Groups of 3 or 4 adolescents have been known to hang around ATM machines unobtrusively, observing the people using them.  They will give the woman ample time to key in her PIN number and amount.  Then they will rush out to harass her, who more often than not, is so surprised that she would just withdraw the card and leave, not knowing that the money would be coming out after that.  In other cases (where you have not chosen the amount yet) they would simply touch a figure and validate it immediately and your card would come out in a twinkling, making you think that it is safe to remove it, since you have not yet typed in the figure.  To be on the safe side, always use the ATM machines inside banks rather than those on the road pavement.&nbsp;&nbsp;</span></td></tr></table>
<br />
<FONT face=tahoma color=black size=3>In certain lines (especially Line 2 in the stretch between "Place de Clichy" and "Barbes Rochechouart") when the metro (underground or subway) is normally packed with tourists, office-workers and housewives, the pickpockets are eternally present.  They like to be near the door so it's easy for them to make their getaway.  If you are picked as a victim you have already been observed even before you stepped into the tube.  When the "metro" arrives they will delay until the moment when you get in to go in just after you.  Sometimes they work in pairs with one of them on each side of you.  A favourite trick of theirs is to stop you from walking forward and distracting your attention by pretending that their ticket dropped in front of you.  If that happens to you the first reaction should be to put your hand on your own pocket or wherever your wallet/purse is.  Another favourite spot of theirs is on a moving escalator.  When you are about to arrive at the top, the one in front of you will slow down to prevent you from advancing while his accomplice behind picks your pocket!  So do watch out!  Don't let the pickpockets spoil your holiday/stay in Paris.  Outsmart them by picking them out before they pick your pocket!</font></font</font>

