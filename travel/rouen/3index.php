<td width=350 valign=top>The starting point of your visit to Rouen is undoubtedly Place de la Cathedrale (Cathedral Square).  As its name implies this is where the famous Cathedral is.  It is also here that you will find the Rouen Tourism Office from where you can get a free city map and information on what to see in Rouen.  After visiting the Tourism Office and the Cathedral here you can walk down Rue du Gros Horloge to admire the astronomical clock (picture on left).  In fact this is the second most important monument to visit in Rouen after the Cathedral .  Go up to the top for a panoramic view of the city.  If you keep on walking along this street to the end you will see the Old Market Place (Place du Vieux March&eacute;) on your right, with its very unusual structure that catches one's eyes from the outside (see photo below left).
<br>Rouen is a city steeped in history.  Joan of Arc was burned at the stake here on 30 May 1431 for heresy when she was only 19 years old.  Rouen then was under English rule.
