<br><br>
<FONT face=tahoma color=black size=3>If there is one subject that no one can avoid talking about (in whichever part of the world he might be staying), it is concerning his own health or that of another person - a close friend or a relative.  While we might be enjoying good health almost all of the time yet there comes a time somewhere along the line when our health gives way and we fall sick.  It is at this period that we begin to hear the name of the disease, maybe for the very first time, that we are suffering from.  And yet sometimes, doctors expect you to know all about a disease when they just drop the name of the disease after a diagnosis but is it always the case?
<br>You have all heard of HIV but that is only one of the better-known medical abbreviations often used in diagnostics. What if you hear someone is suffering from ALS, AMD, BPH, BPD, CVA, DVT, ED, GERD, HPV, IBD, IBS, LIS, ME, MND, MS, MSA, OCD, OSA, RA, SARC, SLE, STD, TIA or UTI?
<br />Instead of googling each and every time for a health problem and be faced with a long list of choices it is so much easier to have them all in one page so that when you need to look up on a particular health topic the carefully-selected site containing the basic information (its symptoms, causes, diagnosis, treatment, etc.) is already there, thus saving you much time and energy.
<br />To make matters worse, some of them are popularly known by their initial letters (as if everyone who is not in the health domain is aware of them or of what they represent).

<br />So here is a quick rundown of some of the diseases that you might hear of for the very first time.</font></font></font>
