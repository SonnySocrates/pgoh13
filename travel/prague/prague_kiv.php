<br />In fact as you walk along Charles Bridge, you will never miss it as you will always find a big crowd queueing up for their turn to touch the plaque at the bottom of the statue of St. John Nepomucene.  Touching it is supposed to bring good luck and fortune as well as make your wishes come true(?).
<br />The statue is one of many along Charles Bridge, a pedestrian bridge almost 10m wide and 516m long that is undoubtedly Prague's main landmark.  No tourist to Prague would leave the city without visiting it at least once.  Whether during the daytime or at night it is always animated with street performers and souvenir vendors trying to earn a living from tourists.
<br />But you will have to walk along the Charles Bridge again at night as it is only then that you can enjoy the magnificent illuminations of the Prague Castle.
