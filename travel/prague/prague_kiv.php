<p><table align=right><tr><td width=450><img src=http://pgoh13.com/prague/clock_closeup.jpg width=450></a><center><em>Close-up of the astronomical clock near the Old Town Square.</em></center></td></tr></table>

<FONT face=tahoma color=black size=3>Prague is only one hour and twenty minutes from Paris and is East Europe's most popular tourist destination.    Birthplace of Franz Kafka, one of the most influential writers of the 20th century, its principal attractions which no visitor to Prague should miss are:  the Old Town Square and the neighbouring Charles Bridge, the Prague Castle and the funicular train ride up to Petrin Hill for a relaxing promenade as well as the lift ride up to the top of Petrin Tower.  Architecture buffs will certainly want to visit the futuristic TV Tower to have a 360&deg; panoramic view of the city.
<br />Nothing less than a whole afternoon though should be set aside for a visit to the Prague Castle and its vast environments which constitute a little town by themselves.  But apart from its monuments, the charm of Prague is to be found in its meandering alleys and lanes that run from all parts of the Old Town Square where horse-drawn carriages make you feel that you are in another epoch.  Its labyrinth of cobbled streets are sure to make you lose your way now and then but here getting lost can be considered to be a luxury as serendipity is something that is never more true than here.   At the end of the day you will thank yourself for not having followed your initial programme!
<br />But if it is modernity that you are after, then head for Narodni street and Wenceslas Square or the district of Vinohrady and you will find yourself in the Prague of the present. 

