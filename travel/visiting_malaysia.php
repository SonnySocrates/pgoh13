<h1 style="background-color:midnightblue;color:yellow;display:inline-block;font-family:arial">&nbsp;&nbsp;Visiting Malaysia (An 

introduction)&nbsp;&nbsp;</span></h1>
<br>
<FONT face=tahoma color=black size=3>The following are recommended places to visit for a 

tourist who has just two or three weeks to spend in Malaysia.  If you have only two 

weeks (or if the sea is not your cup of tea), then the Perhentian islands can be left 

out without taking away too much from your Malaysian experience.  In fact, even if you 

have only two weeks to spend in Malaysia, you can easily squeeze in Melaka, Kota Bharu 

and Cameron Highlands into your programme as these are places that you need spend just 

one night (or two nights if your itinerary permits).  But within that short period you 

will be able to gain a better insight into Malaysia's past and present than if you were 

to visit the other states.  As for Pulau Perhentian it's not worth going unless you are 

able to stay for at least 3 nights (seeing that the motorized boat trip from Kuala Besut 

can take up to an hour each way, depending on which part of the two islands you are 

staying in) and that it only operates three times a day.  Click on the picture 

below to go straight to the city you are interested in.</font></font>

