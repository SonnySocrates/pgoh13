<strong>For those who want to know more about the subject</strong>
<br>Apart from the highly irresponsible "anything goes" approach to daily earnings and the number of page views, the rest of the information the above websites provide might be true since such information from internet are easily available to anyone who wants to have details about a website.  The sources of such information come from "Whois" (which every domain that is registered has to supply), Google (for the Page Rank), Alexa (for the world rank) and DMOZ (to see if a site is listed in its directory).  So you see, even you and I can provide the information about any website by just typing the website's url in these sites:
<br><a href=http://www.whois.com/whois/ target=_blank>1.  For "Whois"</a>
