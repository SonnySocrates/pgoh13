The Royal Palace is the centrepiece of Dam Square, which together with the Leidseplein Square and the Rembrandtplein Square, are the very pulse of Amsterdam city life.
<br />
One of the places to visit in Amsterdam is the flower market (Bloemenmarkt) which is actually a floating market as the stalls float in the Singel canal.  A more popular type of market not to be missed is the Albert Cuyp market along Albert Cuyp street, behind which can be found many ethnic restaurants.  It is perhaps the largest daytime market in the whole of Europe.
<br />
Apart from the <a href="https://www.rijksmuseum.nl/en?lang=en" target="_blank">Rijksmuseum</a> and the <a href="http://www.vangoghmuseum.nl/vgm/index.jsp?page=101&lang=en" target="_blank">Van Gogh Museum</a> (see below), art lovers also make it a point to visit the <a href=http://www.rembrandthuis.nl/cms_pages/index_main.html target=_blank>Rembrandt House Museum</a> at number 4, Jodenbreestraat.  Rembrandt lived here from 1639 till 1658 when it was auctioned due to his bankruptcy, after which he had to live in a small, rented house till his death in 1669.  Combine this visit with a visit to the flea market at Waterlooplein, which is not far away from here.
<br />
Queen's Day <em>(Koninginnedag)</em> is celebrated in a big way in Amsterdam on the 30th of April each year (or on the 29th if the 30th is a Sunday). On that day too "freemarkets" <em>(vrijmarkt)</em>, where everyone is allowed to sell things in the streets, are held all over Netherlands and many people are dressed in orange for the occasion.

