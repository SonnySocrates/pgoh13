Thus if you are in Paris in summer you could take the night train for the French Riviera (sleeping on the night train is not travel time wasted, you will agree).  Unless you are a jet-setter - in which case just replace the train by the plane.  
<br />But if the glamorous cities like Cannes, Nice and Monaco is not your cup of tea how about the more historical city of Rome?  Or the romantic city of Venise?  Both are also just a night train's journey from Paris.  And in one sweep you would have covered both France and Italy!
<br>I could go on and on.  Taking the night train from Paris to Barcelona or Madrid, for that matter.  So that's France and Spain for you without too much effort.  And what about Athens and Prague?  They're not too far away neither once you are in Paris (which I assume every tourist to Europe will make a point of stopping!)  With low-cost flights charging 30 euros or less (that is, if you book a few months in advance), flying from one major city in Europe to another nowadays is not much more expensive than travelling by train.   And besides, it only takes 2-3 hours of flying time from Paris to most of the top European cities mentioned here.  So if you have come from far to Europe, you might as well make the most of it and not just confine yourself to one or two cities, right?

