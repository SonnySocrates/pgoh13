<br>But it is this one that takes the cake: <em>http://botalizer.com/</em>
<br>It mentions that the website you are on now is earning $202 a day!  So, where did the other $200 go to every day?  Hope they will be able to tell me so I can give them half of it!
<br>Then there is the last-mentioned site (<em>netvaluator.com</em>) which, while giving the lowest of the "imagined" daily earnings among the five sites (but which still exceeds the actual daily earnings by over US$27), mentions an amount (US$29.39) with such precision so as to give you the impression that it must have come from a reliable source, which is not the case at all.  Although the above websites would try to justify the inexactitude of their figures by saying that they come from algorithms, the fact is that it is human programmers who write those algorithms so they should be held responsible for such misinformation.
