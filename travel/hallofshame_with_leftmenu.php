<strong>For those who want to know more about the subject</strong>
<br>Apart from the "anything goes" approach to daily earnings and the number of page views, the rest of the information the above websites provide might be true since such information from internet are easily available to anyone who wants to have details about a website.  The sources of such information come from "Whois" (which every domain that is registered has to supply), Google (for the Page Rank), Alexa (for the world rank) and DMOZ (to see if a site is listed in its directory).  So you see, even you and I can provide the information about any website by just typing the website's url in these sites:
<br><a href=http://www.whois.com/whois/ target=_blank>1.  For "Whois"</a>
<br><a href=http://www.prchecker.info/check_page_rank.php target=_blank>2.  For Google Page Rank</a> (which ceased to exist since the end of 2014)
<br><a href=http://www.alexa.com/ target=_blank>3.  For Alexa's world (global) rank</a>
<br><a href=http://www.dmoz.org/ target=_blank>4.  To see if a site is listed in DMOZ directory</a>
<br>But what you and I don't do - or will never think of doing - is to say what a website earns simply based on certain assumptions eg. its popularity through its Google Page Rank or Alexa's world rank or simply based on the number of advertisements that appear in its pages.  A popular website does not necessarily earn a great deal of money just as an honest and hardworking employee does not necessarily earn more money than someone who works only half as much.
