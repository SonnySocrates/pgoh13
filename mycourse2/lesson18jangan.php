<p style="font-style:italic;font-size:16px;">*By the way, in case you are already familiar with the ber- prefix for Malay verbs and you are wondering if there is a typo here let me assure you that there is none. The correct spelling is <font color=red>bekerja</font> and not berkerja. There is a reason for this. It's because the first syllable of kerja has er in it so having ber with er is a definite no-no though it beats me why they want to go to all that trouble just to make an exception. Anyway you know now, the correct spelling is <font color=red>bekerja</font> and not berkerja.</p>

<br>
<FONT face=tahoma color=black size=3><u>More sentences</u>
<br>
<span class=red>Jangan ketawa.  Ini bukan lucu.</span> (= Don't laugh.  This is not at all funny.)
<br>The opposite of <span class=red>ketawa</span> is <span class=red>menangis</span> as in this example:
<br>
<span class=red>Mengapa dia menangis?  Dia menangis kerana jatuh.</span>  (=Why is he crying?  He is crying because he fell)


<br><br>
<u>For those who want to know more:</u>
<br>
<span class=red>Jangan merokok di sini</span> is the same as <span class=red>Jangan hisap rokok di sini.</span> = Don't smoke (cigarettes) here.
<br>The usual term in public places where smoking is not allowed is:  <span class=red> Dilarang merokok.</span> (Smoking is forbidden.)	
<br><span class=red>Anda tidak boleh hisap rokok di sini.</span> = You cannot smoke (cigarettes) here.<br>
In <span class=red>Jangan lari, nanti jatuh</span> (Don't run or you will fall) the word <span class=red>nanti</span> is used to indicate the future tense.  By  itself the word <span class=red>nanti</span> usually means "to wait" as in <span class=red>Nanti sekejap</span> (or <span class=red>Tunggu sekejap</span>).
<br>
 <u>Note:</u> In order to make a command sound less of a command, we add <span class=red>ya</span> at the end of a phrase starting with <span class=red>Jangan</span>.  Thus <span class=red>Jangan bising ya, dia sedang tidur</span> is not a harsh command not to make noise but a gentle request equivalent to "Please do not make any noise as he is sleeping".<br>
<span class=red>sedang</span> is one of a number of "verb modifiers" present in Bahasa Malaysia.  Common verb modifiers are:<br>
(i) <span class=red>sudah, telah </span> and <span class=red> pernah</span> to indicate an action that has happened in the past<br>(ii) <span class=red>akan, kelak, nanti </span> and <span class=red>mungkin</span> to indicate something that will take place in the future and <br>
(iii) <span class=red>sedang, tengah </span> and <span class=red> masih</span> to indicate an action that is happening at the time the speaker makes his remark.<br>

<table align="left" width=97><tr><td><img src=digigit_anjing.jpg width=97 valign=top></td></tr></table>The answer to the reminder <span class=red>Jangan lupa datang esok, ya</span> (yes, again this is not an order but a gentle reminder since it is "cushioned" by  <span class=red>ya</span> at the end) is: <span class=red>Tidak, saya tidak akan lupa.</span>
<br>A common verb:  <span class=red>mari</span> (= to come)
<br>I don't think I have introduced this very common verb yet.  Please note that when you say "Come here" in Malay it is simply <span class=red>Mari sini</span> and not <span class=red>Mari di sini</span> while "Go there" is <span class=red>Pergi ke sana</span> though colloquially it is just <span class=red>Pergi sana</span> without the preposition.  But to the question <span class=red>Di mana?</span> you must always answer with <span class=red>Di sini</span> or <span class=red>Di sana</span>.  Thus the sentence "Your car key is over there" is translated as <span class=red>Kunci kereta anda ada di sana</span>.
<br>By the way the verb <span class=red>mari</span> is also used when you are inviting someone to do something eg. <span class=red>Mari, sila makan</span> (Come, please eat) or <span class=red>Mari, sila duduk</span>.  (Come, please sit).  Of course you can just say <span class=red>Sila duduk</span> or <span class=red>Sila makan</span>, and if you are pointing to the food on the table you can just say <span class=red>Silakan</span>.
<br>
Note that when <span class=red>di</span> is used as a preposition eg. <span class=red>di sana</span> (over there), <span class=red>di rumah</span> (at home) or <span class=red>di atas</span> (on top), it is a word in its own right and is therefore not joined to the word that comes after it.  But when  <span class=red>di</span></b> is part of a verb (normally  to make it passive as below) then it is joined to it.  Thus:
<br />(see illustration) <span class=red> Dia telah digigit oleh anjing</span> (= He was bitten by a dog).  You will remember from Lesson 9 that <span class=red>oleh</span> means "by" (in passive sentences).
<br />The Malay sentence above is more to help the English speaker understand its construction because actually <span class=red>oleh</span> is often left out in such a sentence so it is perfectly correct in Malay to say <span class=red> Dia telah digigit anjing</span> (literally "He was bitten a dog").</font>
