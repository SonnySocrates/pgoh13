<p style="max-width:750px;font-family:tahoma;font-size:16px;">You will learn in later lessons that most verbs in Malay have prefixes and suffixes affixed to them.  In the last sentence above, for example, you might hear: <font color=red>Saya hendak <u>ber</u>cakap dengan Encik Ahmad.</font><br>
In such a case you might be asked <font color=red>Berkenaan dengan apa?</font> or <font color=red>Tentang apa?</font> both of which mean "About what?" (On what subject or matter?)
<br>On your side you might need to say "I have to speak to Encik Ahmad about tomorrow's meeting" which in Malay would be <font color=red>Saya harus bercakap dengan Encik Ahmad berkenaan dengan mesyuarat esok</font> or <font color=red>Saya harus bercakap dengan Encik Ahmad tentang mesyuarat esok.</font>   So each time you need to talk about a certain matter or concerning something you can either say <font color=red>berkenaan dengan</font> or <font color=red>tentang</font>.  In the first case (<font color=red>berkenaan dengan</font>) you need always to use it with the preposition <font color=red>dengan</font> (think of it as "having to do WITH something").
<br><u>For the very ambitious (or should I say "model") student:</u>  There is yet a third word, also in common usage, to denote the same thing.  The word is <font color=red>mengenai</font> pronounced in four syllables (me-nge-na-ee) so don't use it unless you are able to pronounce it correctly!  Eg. <font color=red>Saya hendak berbincang dengan anda mengenai hutang anda.</font> (I wish to discuss with you about your debt.)
<br>
<br><u>Another sentence with <font color=red>dengan</font>:</u><br>
 <font color=red>Dia masih tinggal dengan ibu bapanya</font>  ("He is still living with his parents" OR "She is still living with her parents".)<br>
You have learnt in Lesson 3 that <font color=red>ibu</font> and <font color=red>emak</font> both mean "mother" and that <font color=red>bapa</font> and <font color=red>ayah</font> both mean "father".  However in this expression for "parents" you cannot use any word you like.  It has got to be <font color=red> ibu bapa</font> and nothing else.  And get the order right, the mother comes first before the father.
</p>
