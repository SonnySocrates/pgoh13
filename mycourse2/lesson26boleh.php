Further examples:<br><font color=red>Boleh saya jumpa  Encik Tan? </font>  (Can I meet Mr. Tan?)
<br><font color=red>Boleh saya bercakap dengan saudara sekejap? </font>  (Can I talk to you for a while?).  Note that <font color=red>saudara</font> is often used among friends of the same age group when addressing each other in the place of <font color=red>awak</font> or <font color=red>kamu</font>.
<br>
(To a taxi-driver) <font color=red>Boleh tunggu sebentar? </font> (Can you wait for a while?).
<br>And at the end of it all (especially if he had waited patiently and in good humour for you) you might want to say <font color=red>Simpan semuanya</font> (means "Keep the change" though literally the sentence means "Keep everything") when you hand him the fare.
<br>
To all intents and purposes there is no difference between <font color=red> sebentar </font> and <font color=red>sekejap </font>both of which mean "for a short while".
<br>
<font color=red>Bolehkah saya pinjam pen anda sekejap? </font> (=  Can I borrow your pen for a second?)<br>
Possible answer:  <font color=red>Boleh, memang boleh.  Ini dia. </font>(Yes, you can, of course you can.  Here it is.)<br>
Please note the difference in pronunciation in the first syllable of <font color=red>memang</font> (=of course) and <font color=red>menang</font> (=to win) both of which is spelt "me".
<br>The first syllable of <font color=red>memang</font> is pronounced as in the English word "may" while the first syllable in the word <font color=red>menang</font> is pronounced as in the first syllable of the name "Murphy". (More of this in Lesson 50.)
<br><font color=red>Dia memang pandai</font> means "Of course he is clever" (there is no doubt about it - it's a fact).
<br>To end this lesson let me introduce the expression "<font color=red>Mana boleh</font>!".  These two words, which you have already learnt separately, are about the most common words in the Malay language and yet, when put together they have a very special meaning.  Can you guess?  Well, taking the sense of each word in turn you would translate it literally as "Where can!".  The actual meaning is "How can!" - a protest or an exclamation meaning "It's impossible" or "I can't believe it!" (Not to be confused with "How come?" meaning "Why" translated simply as <font color=red>Mengapa</font>).
<br>WHAT? You don't even have a dollar in your pocket, a millionaire-to-be like you?  <font color=red>Mana boleh tuan!</font> ("How can man!" in Manglish)
<br>By the way have you heard of the <font color=red>Malaysia boleh!</font> slogan or the <font color=red>boleh</font> phenomenon in Malaysia?  If not you'll have to read this article on <a href="capital_ofrecords.php" target="_blank">"The World Record-Breaking Capital"</a> by Jack Boulware! 
