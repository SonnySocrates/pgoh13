<span style="font-size:x-large;color:midnightblue;">For those who want to know more:</span>
<br><br>
The word <font color=red>Sila</font> can be translated as "Please" in English and is usually used when you invite someone respectfully to do something.
<br>Example: If someone knocks at your door and you ask him to enter you would say <font color=red>Sila masuk.</font> (Please enter.)
<br>Another example: <font color="red">Sila datang ke rumah saya bila senang</font> means "Please come to my house when you are free."  But remember that <font color=red>Sila</font> is always used for a polite invitation only.
<br>You cannot translate "Please" into Malay with <font color=red>Sila</font> each and every time though because while you can invite someone to sit or drink, you cannot "invite" him to help you!
<br>In the case of asking a service of another or for help you have to use the word <font color=red>Tolong</font> (see Lesson 38) to  remove all ambiguity.  Example:  <font color=red>Tolong bantu saya.</font>  (Please help me.)
<br>There are borderline cases though.  The sentence "Please inform him that I will not be coming" can be translated either as <font color=red>Tolong beritahu dia saya tidak akan datang.</font> or <font color=red>Sila beritahu dia saya tidak akan datang.</font>  However you will never go wrong if you stick to using <font color=red>Tolong</font> when you are asking someone to do you a service.
 <br>When you want your guests to feel really at home you can say <font color=red>Buat seperti di rumah sendiri</font>  (literally) Make as if you are in your own house, or as we say in English:  Make yourself at home!<br>
When  <font color=red>Sila</font> is used with the suffix  <font color=red>kan</font> it doesn't need any other word to go with it as it has the sense of "Go ahead" or "Help yourself".  <br>Thus when someone asks if he can borrow something from you (eg.  <font color=red>Boleh saya pinjam pen anda sekejap?</font> meaning "Can I borrow your pen for a moment?") you can just say  <font color=red>Silakan</font> (the equivalent in English being "Sure, go ahead" or "Sure, just help yourself".
<br>
Another word that is often used in the place of  <font color=red>Sila</font> is  <font color=red>Jemput</font> (a verb meaning "to invite") or  <font color=red>Jemputlah</font>.  Thus when you invite someone to enter your house you can say   <font color=red>Jemputlah masuk</font>.<br> When asking guests to help themselves to the food that is displayed on the table you can simply say  <font color=red>Jemputlah</font> or <font color=red>Silakan</font> (both of which mean "Please help yourself") with your hand showing the table where the food is.  Similarly instead of saying <font color=red>Sila makan</font> you can also say <font color=red>Jemput makan.</font>

