Note that in the above answer <font color=red>Ya, ini dia</font> don't try to see whether <font color=red>dia</font> here stands for "he" or "she".  It stands for neither as <font color=red>ini dia</font> is an expression meaning "here it is" or "voil&agrave;" in French.
<br /><br>
<font face=arial color="darkblue">Further examples:</font>
<br />
<font color=red>Saya ada hadiah untuk anda.</font>  (I have a gift for you.)<br>
 <font color=red>Surat ini untuk siapa?   - Untuk dia.</font>  (Who is this letter for?   - For him.)

<br><br>
<img src=forthose_knowmore.jpg>
<br><br>In colloquial Malay the word <font color=red>untuk</font> in all the above sentences where a person is the recipient can be replaced by <font color=red>bagi</font>.  So the above sentences become:
<br>
<font color=red>Ada surat bagi saya?</font>  (Is there a letter for me?)<br>
<font color=red>Saya ada hadiah bagi anda.</font>  (I have a gift for you.)<br>
 <font color=red>Surat ini bagi siapa?   - Bagi dia.</font>  (Who is this letter for?   - For him.)
<br><br><u>Note:</u>  <font color=red>bagi saya</font> can also mean "as for me" eg. <font color=red>Bagi saya, perkara itu tidak berapa mustahak.</font> (= As for me, that matter is not too important.)

<br><br>Please note that you should not translate "for" (when indicating a period of time) by using the word "untuk" as you might think if you should translate it from English.  Thus you should not translate "I have been studying Malay for 6 months" as <font color=red>Saya sudah belajar bahasa Melayu untuk enam bulan.</font>  In this case you should use "selama" for "for" when you are expressing a period of time.  Thus the above sentence should be translated as <font color=red>Saya sudah belajar bahasa Melayu selama enam bulan.</font>

