<h3>For those who want to know more</h3>

<p>You might have noticed that unlike English and French, where the possessive pronoun precedes the noun, in Malay it is the other way round i.e. the noun comes first followed by the possessive pronoun.  Thus "my car" becomes   <font color=red>kereta saya </font> (<mark style="background-color:yellow;">car my</mark>) and "your friend" becomes  <font color=red> kawan anda </font> (<mark style="background-color:yellow;">friend your</mark>).
<br>Again don't try to look for the Malay equivalent of "is" in the sentences above.  There is none because <font color=red>Ini kereta saya </font> is literally <mark style="background-color:yellow;">This my car</mark> although it means "This is my car"!  
<br>1. <font color=red>Ini </font> is used for something within reach or when you are introducing someone.  When an object is not within reach we use   <font color=red>Itu  </font> eg.    <font color=red>Itu rumah saya.  </font> = That is my house (if you are pointing to it across the road to a friend).<br>
2.  Another word for "father" in Malay is   <font color=red>ayah  </font>.<br>
3. Another word for "mother" is   <font color=red>ibu</font>.<br>
4. Another word for "wife" is   <font color=red>bini</font>.<br>
5. From   <font color=red>anak  </font> (child)  we have   <font color=red>anak lelaki  </font> (son) and   <font color=red>anak perempuan  </font> (daughter).
<br>As you can guess from the above <font color=red>lelaki</font> by itself means "male" while <font color=red>perempuan</font> by itself means "female".
<br>Thus <font color=red>anak lelaki  </font> literally means "male child" while  <font color=red>anak perempuan  </font> means "female child" (the order is reversed, remember?)
</p>

