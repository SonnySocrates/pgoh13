<br><p style="max-width:800px;font-family:tahoma;font-size:16px;">You would have noticed that the short answers in Malay do not require the pronoun as it is understood.
<br>While in English the short answers would be: <em>Because I was sick, Because it rained, Because I have lost my passport</em> &nbsp;in Malay you need only say: <em>Because sick, Because rain, Because lost my passport</em>.
<br>Putting the above answers in a full sentence would produce the following:
<br><font color=red>Saya tidak datang kerana sakit.
<br>Saya datang lambat kerana hujan.
<br>Saya sedih kerana sudah hilang pasport saya.</font>
<br>
Another word that is  used  as much as <font color="red">kerana</font> is  <font color="red">sebab</font>. <br>Thus to the question <font color="red">Mengapa datang lambat? </font>the answer could just as well be <font color="red">Sebab hujan</font>.<br>
Okay, if <font color="red">mengapa</font> means "why", what does <font color="red">tidak mengapa</font> mean?  It is just an expression that means "It doesn't matter", "It's all right" or "Forget about it".  It's the same as <font color="red">tidak apa</font>.
<br>A similar expression is <font color="red">Tidak menjadi masalah.</font> (It's no problem.)

