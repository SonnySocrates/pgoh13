Just as in English, there are also different ways of asking  a person where he stays in Malay.  The most frequently-used among them is:<br>
<font color=red> Anda tinggal di mana? </font> to which the answer could be just the name of the street, e.g.<br>
<font color=red>Di Jalan Silang. </font>  (= In Silang Street) <br>
<u>Note:</u>  If the word <font color=red>tinggal </font> sounds familiar to you it is because it has already been introduced in Lesson 11 ( <font color=red>Selamat tinggal</font> = Goodbye).   But you say this only if you are taking leave of your host (who is staying behind).   If you are the host and you wish to say goodbye to your guest who is leaving you to return home or to go somewhere else you should say <font color=red>Selamat jalan</font> (May you have a safe journey). 
<br>A second way of asking a person where he stays is: <font color=red>Di mana rumah anda? </font><br>
The answer could be <font color=red>Di PJ </font>  =In Petaling Jaya.   (Petaling Jaya, by the way, is rarely called by its full name, it's always shortened to PJ, just as Kuala Lumpur is always shortened to KL). <br>
But if you want to know a person's house number (and not just the name of the road or the district where he lives) the question to ask is: <br>
<font color=red>Apa alamat rumah anda?</font> to which the answer could very well be: <br>
<font color=red>Nombor 20 Jalan Damansara.  </font>  BTW how do you say "twenty" in Malay?  <a href=lesson8nombor2.php>Click here if you have forgotten. </a><br>
There is a fourth way of asking where a person lives.  It is:<br>
<font color=red>Anda duduk di mana?  </font> Although <font color=red> duduk </font> usually means "to sit" (<font color=red>Duduklah </font>or <font color=red>Silakan duduk</font> means "Please sit") be aware that when you are asked this question in Malaysia you are being asked where you live and not where you are sitting. <br> I remember we once had an Indonesian woman journalist with us when we were attending a journalism course and when we asked her this question she was completely perplexed as we could all see where she was then sitting i.e. on a chair so why the question?<br> If you want to find out how long a person has been staying at a place the question is:
<br><font color=red>Sudah berapa lama anda tinggal di sini? </font><br>and if the answer is five years, you would hear: 
<font color=red> Sudah 5 tahun.</font> <br>Another useful sentence:<br>
<font color=red>Bolehkah anda beri alamat rumah anda kepada saya?</font>
<br>Can you please give me your house address?

