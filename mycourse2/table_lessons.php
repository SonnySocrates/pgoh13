<div style="font-family:arial;font-size:1.4em;">Hi, welcome!
<br>Bahasa Malaysia is Malaysia's national language and is formerly known as Bahasa Melayu (Malay language).  It is not only spoken in Malaysia but is also widely spoken in Indonesia, Brunei and Singapore. Some people say that Bahasa Malaysia is an easy language.  To a certain extent it is, but believe me, it is not so easy that you can afford to study it while listening to your favourite music at the same time!
<br />The truth is there is no EASY language in the world.  Everything is relative so when people say that Malay is an easy language what they really mean is that it IS easy when compared to studying a language like English, French or Mandarin, for example.  Thus if you might need to spend at least 300 hours studying English, French or Mandarin before you are able to use it in a simple conversation, you need only spend say, 100 hours studying Malay before you are able to do so.   This is because in Malay there is no past tense or past participles of verbs to study as in English, the verbs are not conjugated as in French and you don't have to worry about getting the tones right in order to be understood as you have to in Mandarin.

