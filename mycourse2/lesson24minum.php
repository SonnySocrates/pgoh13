<br><br>
Please refer to Lesson 14 on the use of <span class=red>hendak</span>.
<br />
"Beer" and "whisky" keep their English pronunciation though their spelling is changed in Malay to  <span class=red>bir</span> and  <span class=red>wiski</span> in conformity with Malay spelling rules.   Thus:
<br> <span class=red>Dia suka minum bir tetapi saya suka minum wiski.</span> = He likes to drink beer but I like to drink whisky.<br>
Another "foreign" drink that keeps its English pronunciation is "wine" though it is spelt the Malay way eg. <span class=red>Saya hendak minum wain.</span><br>
And after drinking so much whisky and wine don't be surprised if you become <span class=red>mabuk</span>.  Yes, you guessed it! &nbsp; <span class=red>Dia mabuk</span> means "He is drunk".
<br />
When asking a guest what he wants to drink, it is perhaps better to use <span class=red>hendak</span> (wish) than <span class=red>suka</span> (like).
<br>
However, instead of asking <span class=red>Anda hendak minum apa?</span> you can also ask  <span class=red>Anda mahu minum apa? </span> (What do you want to drink?) in which case the answer would be <span class=red>Saya mahu minum kopi. </span><br>
Note that <span class=red>mahu</span> is normally pronounced <span class=red>mau</span> in its spoken form.
<br>
As you've just learnt, the  Malay word for milk is <span class=red>susu</span> so coffee with milk is <span class=red>kopi susu</span> and tea with milk <span class=red>teh susu</span>.  Similar to <span class=red>teh susu</span> (except that it is covered with a thick layer of froth) is <span class=red>teh tarik</span>.  &nbsp;It means "pulled" milk tea.  You'll have to see the teaman in action tossing the tea from one receptacle to your teacup to understand what this really means.  <a href=#tehtarik_photos>Go below or click here for photos of the teh tarik seller.</a>
<br>Those who are new to Malaysia will soon find out that <span class=red>teh tarik</span> is a real social leveller in Malaysia and considered as its national drink. Whether you are King or just a common labourer everyone has a right to his glass of <span class=red>teh tarik</span>.  In fact when the price of fuel was increased in Malaysia even non-motorists protested as they felt that this would have an impact on the price of a glass of <span class=red>teh tarik</span>!   And there was talk about asking the first Malaysian astronaut to make teh tarik in space when he accompanied the Russian space launcher on October 6, 2007.  This proposal didn't materialize finally as the food and diet were in the hands of nutritionists.

