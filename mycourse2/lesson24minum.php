<br>
Coffee without milk (but normally with sugar already in it) is called <span class=red>kopi O</span> (pronounced "or") or <span class=red>kopi kosong</span> (<font color=red>kosong</font> by the way means "empty").  &nbsp;Same for tea - ask for <span class=red>teh O</span> or <span class=red>teh kosong</span> if you don't want milk.   If you want your coffee or tea with ice cubes just add the word <span class=red>ais</span> (as in "ice" - its English origin) or <span class=red>beng</span> (Chinese origin).  This is a good example of the versatility of the Malay language adopting words from other languages like all active and dynamic languages do.
<br>
Malaysians normally take their coffee and tea sweet so if you don't take sugar you have to add <span class=red>tanpa gula</span> (without sugar) or  <span class=red>tak mau gula</span> (don't want sugar)
<br>But what if it is not sweet enough?  Just say <span class=red>Tak cukup manis.  Tolong tambah sedikit gula lagi.</span> (It's not sweet enough.  Please add a bit more sugar.)
<br>In Indonesia coffee without sugar is called <span class=red>kopi pahit</span> and tea without sugar is called <span class=red>teh pahit</span>.  Quite logical as the word <span class=red>pahit</span> means "bitter".

<br>But whether you are in Malaysia or Indonesia, the same word <span class=red>kopi</span> is used for "coffee". You might have heard of Indonesia's "kopi luwak", which some coffee-drinkers (influenced no doubt by Jack Nicholson's character in the movie "The Bucket List") claim to be the best coffee in the world (if not the most expensive). Its notoriety is further enhanced by the fact that it is derived from the poop of an animal called the civet. But coffee experts debunk this "myth". If you are interested to read further on the subject <a href="https://coffeechronicler.com/coffee-cherry-taste/" target=_blank>go here.</a>

<br>Oh, I nearly forgot.  How do you say "I feel thirsty" in Malay?  It's <span class=red>Saya berasa dahaga</span>. 
<br>There is another word for thirsty.  It's <span class=red>haus</span>. So you can also  say <span class=red>Saya berasa haus</span>.
<br>
Incidentally "coffee shop" in Bahasa Malaysia is  <span class=red>kedai kopi.</span>
It is equivalent to the English pub or the French caf&eacute; and you go  there not just for a cup of coffee but also to have your meals <i>(nasi lemak, roti canai, char koay teow, etc)</i>  or to <span class=red>sembang</span> (chat) with your friends (which can be very loud at times, everyone feeling as much at home in a coffee-shop as in his own house!).
<br />By the way please note that the first syllable in <span class=red>sembang</span> is pronounced as in the English word "same" and not as the first syllable of <span class=red>sembilan</span> which, as you have already learnt in Lesson 5, means "nine".

<br />In fact the <span class=red>kedai kopi</span> is such an institution in Malaysia and the Chinese word for it <span class=red>kopitiam</span> ("tiam" meaning shop in Chinese) so widely used that the word has recently found its way into the Dewan Bahasa dan Pustaka's authoritative Malay dictionary <i>Kamus Dewan (edisi keempat) </i> which appeared in 2005 (the previous edition came out in 1994 and did not include the word) with this entry:
<br /><span class=red>kopitiam</span> = <span class=red>kedai kopi</span>
<br>
Another place where Malaysians like to hang out is the "mamak" stall ("mamak" being the word locals use for an Indian converted to Islam), though if you use the word to refer to an Indian Muslim it could stir up some hot Indian blood!  And knowing how spicy Indian curries are I would advise you  not to use it!   <a href=http://en.wikipedia.org/wiki/Mamak_stall target=_blank>Read more about the "mamak stall" in this Wikipedia article.</a>
<br>
From <span class=red>kedai kopi</span> meaning "coffee shop" (in Malay the adjective comes after the noun, remember?) you would have guessed that the Malay word for "shop" is <span class=red>kedai</span>.  Thus <span class=red> Saya akan pergi ke kedai kasut esok</span> means "I shall be going to the shoe shop tomorrow".
<br><font face=comic sans ms color="blue" size=4><u>Cultural note:</b></u></font> &nbsp;&nbsp;At social functions drinks are normally offered and received with both hands.
