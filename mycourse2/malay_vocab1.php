<b><i>This is in response to a number of requests from those of you who have completed my "Basic Course on Bahasa Malaysia".  From now on it is just a question  of enlarging your vocabulary through your own reading experience aided by the constant use of a good bilingual dictionary.
<br>In fact in any language-learning situation the building up of an extensive vocabulary is something that can be left to the very end.  It is of no  use having a huge vocabulary if you do not know how to string the words together to form a correct sentence.
<br>On the other hand once the language structure has been mastered (which I hope my course has succeeded in doing) you will only need a good bilingual dictionary at hand to be able to say whatever you want to say or understand new words when you come across them.  For example, if you have to say "I want to kiss you" in English, you are not going to say "I want to osculate you" (or do you prefer "I want to canoodle you" instead?) although if you should ever hear it spoken or see the written word, you only have to look up the word "osculate" or "canoodle" in an English dictionary to know that it simply means "to  kiss"!
<br>So on this note I am putting you in your own hands and can only wish you  <font color=midnightblue>Selamat belajar!</font>  By the way this is an "invented" expression as all wishes in Malay start with "Selamat".  - PJG</i></b>

