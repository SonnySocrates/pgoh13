<u>SENTENCES:</u><br>
<font color=midnightblue>Dia tidak pernah menceritakan apa-apa mengenai perkara itu kepada saya</font> = He  (or She) has never related anything concerning that matter (subject) to me.<br>
<font color=midnightblue>Saya mengucapkan tahniah di atas kejayaan anda dalam peperiksaan itu.</font> = I congratulate you on your success in the exam.<br>
<font color=midnightblue>Dia tidak memberi apa-apa sebab pun mengapa dia hendak meletak jawatan.</font> = He did not give any reason (explanation) at all for wanting to resign.<br>
<font color=midnightblue>Dia tidak mengalami sebarang kecederaan dalam kemalangan kereta itu.</font> = He did not suffer any injury in the car accident.<br>
<font color=midnightblue>Kerana dia rajin dan jujur dia telah diberikan tanggungjawab yang besar.</font> = Because he is hardworking and honest he was given a heavy responsibility.
