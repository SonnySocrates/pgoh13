<u>WORDS AND EXPRESSIONS:</u><br>
<font color=midnightblue>mangsa</font> =  a victim<br>
<font color=midnightblue>bekas guru</font> = a former teacher, ex-teacher<br>
<font color=midnightblue>rahsia</font> = a secret<br>
<font color=midnightblue>pernikahan (perkahwinan)</font> = marriage<br>
<font color=midnightblue>jiran</font> = a neighbour<br>
<font color=midnightblue>Sementara itu...</font> = Meanwhile...<br>
<font color=midnightblue>syarat</font> = a condition<br>
<font color=midnightblue>pameran</font> = an exhibition<br>
<font color=midnightblue>Baru-baru ini...</font> = Recently...<br>
<font color=midnightblue>kemungkinan</font> = a possibility<br>
<font color=midnightblue>sidang akhbar</font> = news conference<br>
<font color=midnightblue>calon</font> = a candidate

