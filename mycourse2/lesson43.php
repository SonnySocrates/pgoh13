<h3>For those who want to know more</h3>
<br><p><FONT face=tahoma color=black size=3>Note that <font color=red>Berapa harganya?</font>  is only used when you are asking the price of an article.  When you ask a taxi-driver the cost of the fare you would say <font color=red>Berapa tambangnya?</font> (How much is the fare?)
<br>You have learnt that <font color=red>Berapa</font> means "How many..." or "How much..."
<br>We have seen (in Lesson 2) <font color=red><em>nya</em></font> tagged on to nouns ( <font color=red>kereta<u>nya</u></font> = his car or her car).   Here  <font color=red>harga<u>nya</u></font> would mean "its price" and <font color=red>tambangnya</font> "its fare". <p>
 <font color=darkblue size=3><u>Words you will use or hear when you go shopping or when you are in the market:</u></font></font><br>
<FONT face=tahoma color=black size=3>
 <font color=red>murah</font> = cheap<br>
 <font color=red>mahal sekali</font>  (or  <font color=red>sangat mahal</font>) = very expensive<br>
 <font color=red>tidak mampu</font> = cannot afford<br>
 <font color=red>harga yang tinggi</font> = a high price<br>
 <font color=red>harga mati</font> or  <font color=red>harga tetap</font> = a fixed price<br>
<font color=red>tawar</font> = to bargain (as an adjective <font color=red>tawar</font> means "tasteless")
<br><br>
<font face=arial color="darkblue"><u>Money terms:</u></font></font>
<br><FONT face=tahoma color=black size=3>The Malay word for money is <font color=red>wang</font> though <font color=red>duit</font> is frequently used too eg.
<font color=red>Saya tidak ada wang</font> or <font color=red>Saya tidak ada duit</font> (I don't have money) and
<br><font color=red>Wang ini adalah untuk beli makanan</font> or <font color=red>Duit ini adalah untuk beli makanan</font>  (This money is for buying food.)
<br>On festive occasions (Hari Raya, Chinese New Year, etc.) you might hear the term <font color=red>duit raya</font>.  This is money put in a tiny envelope and given to children as a gift.  It originated from the Chinese custom of giving "angpow" (red packets containing lucky money called <em>ya1 sui4 qian2</em> in Mandarin) to children on the occasion of Chinese New Year.
<br><font color=black>Apart from <font color=red>ringgit</font> and <font color=red>sen</font> you might hear the following terms used in some states in Malaysia:
<br><font color=red>rial</font> (used especially in Kelantan) meaning <font color=red>ringgit</font>
<br><font color=red>kupang</font> (used especially in Penang) meaning <font color=red>10 sen</font>.  Thus <font color=red>dua kupang</font> would mean <font color=red>dua puluh sen</font>
</p>

