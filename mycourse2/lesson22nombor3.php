<center><a href="l22ringgit.wav"><img src=l22nombor3.gif width=400 height=50></a></center><br>

<p style="max-width:800px;font-family:tahoma;font-size:16px;">If you have mastered counting up to 99 in Lesson 8 this final lesson on numbers in Malay will be plain sailing to you.  You will  
now need to know only three more words (<font color=red>ratus, ribu</font> and <font color=red>juta</font>) to be able to count any number in Malay.
<br>Counting 3-digit figures in Malay is like counting them in English, the word "hundred" being replaced by <font color=red> ratus</font>.
<br>Similarly counting in thousands and millions in Malay is like counting them in English.  You only have to replace "thousand" by <font color=red> ribu</font> and "million" by <font color=red> juta</font>.<br>As examples speak better than words, here are a few numbers for you to practise:<br>
