<FONT face=tahoma color=black size=3>The first thing you will notice is the absence of the verb "to be" in Malay in the above sentences.
<br />In fact <span style="color:yellow;background-color:red;">the verb "to be" is simply not needed here</span>.
<br />A sentence without a verb?  Yes, it's possible in Malay.
<br />Those who had to struggle with the conjugations of the verb "to be" in French or Spanish will be greatly relieved to hear this!  &nbsp;(Having said that, people who feel lost without putting in a verb in a sentence can note that the word <font color=red>adalah </font> is sometimes used for the verb "is".  Thus <font color=red>Dia guru saya </font> and <font color=red>Dia adalah guru saya </font> both mean "He/She is my teacher".)
<br><br />Another thing you will notice is that while in English we have the pronoun before the noun (eg. my name, your name) in Malay it is just the opposite ie. <span style="color:yellow;background-color:red;">the noun comes first then the pronoun</span> (so in Malay we say "name my", "name your").  If you remember this it will serve you throughout this course as it is the same word order when it comes to adjectives and nouns (so "big car" becomes "car big" in Malay).

<br /><br />The third important thing to remember is something that you'd better get right from the very beginning and that is <span style="color:yellow;background-color:red;">the same pronoun "dia" is used for BOTH "he" and "she".</span>  Only the context will tell whether you're talking about a man or a woman.

<br /><br />
<font face=comic sans ms color="blue" size=4> <u><b>For those who want to know more:</b></u></b></font>

<br />Please note that the colloquial forms (<font color=red>Apa nama anda? </font> and  <font color=red>Apa namanya? </font>) are used in this lesson.
<br />The formal forms would be <font color=red>Siapa nama anda? </font> and  <font color=red>Siapa namanya? </font> <br>The formal forms do not make much sense though because <font color=red>Siapa </font> actually means "Who" as in <font color=red>Siapa dia?</font> = Who is he (or she)?
<br />while <font color=red>Apa </font> normally means "What" as in <font color=red>Apa ini? </font> (What is this?)
<br>You will notice that the suffix <font color=red>nya </font> is tagged on to the noun coming after "his" or "her".  But watch out, <font color=red>namanya </font> can mean "his name" as well as "her name" (depending on the context). 
<br>
So far you have learnt the Malay pronouns for I, you, he and she.
<br>
The Malay word for the pronoun "they" or "them" is <font color=red>mereka</font>.  To help you remember I am going to bring up three common names: Murphy, Raymond and Kazan, the famous Hollywood film director.  What is so special about these three people, you might ask.  Well, if you remember them, they're not only going to help you remember the Malay word for "they" or "them" but also help you to pronounce the word correctly.  How is this possible?  Well just pronounce the FIRST syllable of each of the three names and you will get  <font color=red>mereka</font> as it should be pronounced (Mur-Ray-Ka).  I hope this little mnemonic will be of help to you.  At least it will show you that the e vowel has got two different sounds in Malay (see Lesson 50), one the schwa sound as in Mur (the phonetic symbol for this sound being &#x0259;) and the other the ay sound as in Ray (if you know French, it's the sound of &eacute;). 
