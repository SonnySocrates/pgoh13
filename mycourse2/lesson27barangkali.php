<br><p style="max-width:800px;font-family:tahoma;font-size:16px;">For the last sentence, if you find the word <font color=red>harus</font> too difficult to remember you can use <font color=red>mesti</font> (= must) instead.  Thus <font color=red>Saya mesti pergi sekarang.</font></p>

Mnemonic:  <font color=red>harus</font> means "have to".  Both the Malay word and the English equivalent start with the letter "h".

<p style="max-width:800px;font-family:tahoma;font-size:16px;"><u>When there is uncertainty:</u>
<br>Other words that are often used in the place of <font color=red>barangkali</font> are:<br>
 <font color=red>mungkin</font> (=may, likely)  eg. 
<br><font color=red> Mungkin akan hujan esok.</font> (It may rain tomorrow.) and 
<br><font color=red>Dia mungkin datang.</font> (He may come.)
<br>
An expression to denote a possibility is  <font color=red>boleh jadi</font>  eg.
<br><font color=red>Boleh jadi dia tidak akan datang</font>  (It is possible that he will not turn up.)
<br><u>When there is obligation or certitude:</u>
<br>There are a number of words to convey the idea of obligation.  You have already seen one of them in the last sentence in the table above:
<br>The word is <font color=red>mesti</font> meaning "must".  Example:
<br><font color=red>Saya mesti pergi sekarang.</font>  meaning "I must go now".
<br>A word which can be used in its place is <font color=red>harus</font>, also having the sense of "must, should, have to, ought to".
<br>Thus the above sentence can also be put this way: <font color=red>Saya harus pergi sekarang.</font> (I have to go now.)
<br>Here's another example of the use of <font color=red>mesti</font>:
<br><font color=red>Saya mesti habiskan kerja ini sebelum pukul lima.</font>  (I must/have to finish this piece of work by 5.)
<br>Do you need some mnemonics to help you?  Just remember that <font color=red>mesti</font> and "must" both start with the letter "m"!

<br>A common word for certitude is <font color=red>tentu</font> meaning "sure".  Example:
<br><font color=red>Dia tentu gembira bila mendengar berita baik ini.</font>  (He/She is sure to be happy when he/she hears the good news.)
<br>Another example: <font color=red>Dia tentu akan datang.</font> (He/She is sure to come.)
<br>When you are sure about something (in the sense of "certain" or "positive"), you can use the word <font color=red>pasti</font> eg. I am sure he will win (<font color=red>Saya pasti dia akan menang.</font>)
<br>Quite similar in meaning is the word <font color=red>yakin</font> which is the word to use when you are confident or convinced about something or believe in something eg. <font color=red>Dia yakin dia akan lulus dalam peperiksaan itu.</font> (= He is confident that he will pass the examination.)
<br>When you want to say that there is an uncertainty over some future action you can say <font color=red>Belum tentu</font> or <font color=red>Belum pasti</font> eg. It is not certain that he will be able to play (<font color=red>Belum pasti dia dapat bermain.</font>)
<br>The word to use when you want to say that something is compulsory is <font color=red>wajib</font>.  Example:
<br><font color=red>Persekolahan adalah wajib untuk semua kanak-kanak.</font>  (Schooling is compulsory for all children.)
<br>Oh, I must not forget another common word <font color=red>perlu</font> which  has also the sense of obligation besides that of needing something.  It is sometimes translated as "necessary" eg. <font color=red>Semua orang perlu makan dan minum untuk hidup.</font>  (Everyone needs to eat and drink in order to live.)
