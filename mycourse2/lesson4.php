<h3>For those who want to know more</h3>

<p>
1. As you have seen earlier  the pronoun <font color=red>dia</font> (third person singular) can refer to a woman as well as a man.  Thus:  
<br> <font color=red>  Dia orang Amerika. </font> = He/She is American.
<br><font color=red>Dia orang Jerman. </font> = He/She is German.
<br> <font color=red>  Dia orang Belanda. </font> = He/She is Dutch.
<br> <font color=red>  Dia orang Rusia. </font> = He/She is Russian.
<br>Note that the pronunciation and spelling of Russia is changed to conform with Malay pronunciation (roo-si-ah) and spelling (only one S). 
<br> <font color=red>  Dia orang Singapura. </font> = He/She is Singaporean.
<br> <font color=red>  Dia orang Thai. </font> = He/She is Thai.
<br> <font color=red>  Dia orang Mesir. </font> = He/She is Egyptian.
<br> <font color=red>  Dia orang Israel. </font>(pronounced in 3 syllables i.e. Is-ra-el) = He/She is Israelite.  
<br>A more common word for this is <font color=red>orang Yahudi</font> (Jew).
<br>A word to describe a person's race is  <font color=red>bangsa </font> eg.  <font color=red>Dia bangsa Cina. </font> = He (or She) is Chinese or <font color=red>Dia bangsa Melayu.</font> = He (or She) is Malay.

<br><br>2. Note how a question can easily be formed from a statement by adding the question tag  <font color=red>kah</font> at the end.  Thus:<br>
<font color=red>Anda orang Perancis.</font> (= You are French.)<br>
<font color=red>Anda orang Peranciskah?</font> (= Are you French?)<br>
Do note however that you do not really need to add the suffix <font color=red>kah</font> to turn the statement into a question.  As in most languages, a rising tone on the last syllable of the statement is sufficient to indicate that it is a question.  Thus <font color=red>Anda orang Perancis?</font> (= Are you French?)

<br><br>3.  Note also that in this type of sentences the verb "to be" is not needed in Malay and that <font color=red>kah</font> is tagged on to the word preceding it and does not stand by itself.

<br><br>4.  The word for a foreigner is <font color=red> orang asing </font> but you might often hear people talking about <font color=red>orang putih</font> (literally: white person) or <font color=red>mat salih</font>.  They are referring to Caucasians (as opposed to Asians).  Note that Caucasian is used here to mean one of the main ethnic divisions of the human race or what is loosely called the white race.
<br>Incidentally, with the large number of Africans (mostly from Nigeria) living in Malaysia over the past decade or two, a new term has been created to refer to them.  You will learn about it in the lesson on colours later.

<br><br>5. You should find the word <font color=red>Inggeris</font> very easy to remember as it is the Malay spelling for "English" (ok, I agree with you, it is not an exact phonetic reproduction as the final "sh" sound becomes "s").  This is because in the Malay language the "sh" sound must always be followed by a vowel eg. <font color=red>syarat</font> or <font color=red>mesyuarat</font>.  More of this in Lesson 49.

<br><br>6. Saudi Arabia in Malay is <font color=red>Arab Saudi</font>.

<br><br>7. Morocco in Malay is <font color=red>negara Maghribi</font>.

<br><br><b>Recapitulation:</b>  After studying lessons 2 and 4 you would have noticed that names of languages always start with the word <font color=red> bahasa </font> and nationalities with the word <font color=red> orang </font> or <font color=red> rakyat </font> (see note below).  The same principle applies to the names of countries.  These start with the word <font color=red> negara </font> or <font color=red> negeri </font>. The following table will help you understand better.  And if you don't find your country here the chances are the name of the country is retained as it is and used with the appropriate word eg. if you are from Sweden you are <font color=red>orang Sweden</font> or <font color=red>rakyat Sweden</font>, your language is <font color=red>bahasa Sweden</font> and your country is <font color=red>negara Sweden</font> or <font color=red>negeri Sweden</font>.  The spelling though is sometimes changed to be in line with the Malay sound thus Canada becomes <font color=red>Kanada</font>, America becomes <font color=red>Amerika</font> and German becomes <font color=red>Jerman</font>.
<br>The word <font color=red>bangsa</font> is also sometimes used to describe a person's nationality though it is best to  keep it to describe his race eg. a Malaysian can be Malay (<font color=red>bangsa Melayu</font>), Indian (<font color=red>bangsa India</font>) or Chinese (<font color=red>bangsa Cina</font>) but they are all <font color=red>rakyat Malaysia</font>.
<br />But perhaps it is easier (and clearer) if you just ask the person where he comes from.  In Malay this would be <font color=red>Anda berasal dari mana?</font>  The full answer would be <font color=red>Saya berasal dari Perancis</font> (if you are from France). 
<br><br><b>Note:</b>
<br>In the table under the column with the heading "In Malay" below, you can easily replace <font color=red> negara </font> with <font color=red> negeri</font>.   The two words are quite often interchangeable.
<br>Note that as <font color=red> negeri </font> is also used for each of the states in Malaysia eg. <font color=red> negeri Pinang</font> (Penang) or <font color=red> negeri Kedah</font> perhaps it is better to stick to <font color=red> negara Malaysia</font> for the country.
<br>If you want to stress that you are a citizen of your country (and not simply someone who comes from that country) just replace <font color=red>orang</font> with <font color=red>rakyat</font>.
