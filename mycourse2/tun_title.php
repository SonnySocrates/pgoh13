<center><font face="arial" size="5">Still room for more Tuns and Tan Sris</font></font></center>
<br />
<font face="arial" size="2"><b>By V. Vasudevan  (From <a href=http://www.nst.com.my/ target="_blank">New Straits Times Online</a> of July 06, 2006)</b></font>

<p><font face="arial" size="2"><b>KUALA LUMPUR: There are at present 36 Tuns and 458 Tan Sris, with room for more.<br>
</b> 
These honorific titles, though regularly given out by the Federal Government, are controlled and must not exceed a certain number at any one time.<br />

Deputy Minister in the Prime Ministers Department Senator Datuk Abdul Rahman Suliman said there were two categories of "Tun": the Seri Maharaja Mangku Negara (SMN), which is limited to 25 people, and the Seri Setia Mahkota Negara (SSM), which cannot be bestowed on more than 35 people at a time.<br />

"However, the Government has only bestowed 14 SMN and 22 SSM to date. <br />

"This means there are 36 Tuns in the country. The Government can bestow the title Tun on another 24 people," he said to reporters at Parliament lobby.<br />

He was elaborating on a matter brought up by DAP MP for Tanjung Chow Kon Yeow in the House concerning titles and protocol.<br />

Rahman said the criteria for "Tun" recipients were that they should be outstanding in their field, should have contributed to the country, and be influential.<br />

He said similar conditions applied for the "Tan Sri" honorific, which fell under two categories: the Panglima Mangku Negara (PMN) and Panglima Setia Negara (PSN). Combined, the number is limited to 625; 75 of them PMN and 550 PSN.<br />

Rahman said to date, the Government had bestowed Tan Sri awards on 458 people.<br />

A similar arrangement existed for the title of "Datuk", which also came in two categories: the Panglima Jasa Negara (PJN) and Panglima Setia Diraja (PSD).<br />

Rahman said a register was maintained to keep track of recipients and ensure nobody was bestowed the same title by the Government twice.<br />

He said Agriculture and Agro-based Industries Minister Tan Sri Muhyiddin Yassin had received his "Tan Sri" while he was Johor Menteri Besar.<br />

He said civil  servants who received the titles "Datuk" or "Tan Sri" should be of a certain age: men above 40, and women, 35.<br />

During question time, Chow said it must be awkward for the minister when he has to deal with a civil servant who had a higher honorific than himself.<br />

Rahman said such an issue never arose.<br />

"Titles are just that and should not pose a problem to their work," he said.<br />

"I dont think any civil servant would disregard instructions because they have a higher title than the minister."
