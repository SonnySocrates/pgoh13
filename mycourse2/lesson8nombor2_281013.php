<span class="blue"> <b>For those who want to know more:</b></span>

<br>When <font color=red>satu</font> is added to another word it is often contracted to <font color=red>se</font> and tagged on to the following word.  Thus "satu" puluh (one ten or 10) becomes <font color=red>sepuluh.</font>
<br>This is also the case with <font color=red>sebelas</font> (= 11 - remember that in Malay all numbers from 11 to 19 end in <font color=red>belas</font>).
