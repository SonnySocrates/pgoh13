<H1><span style="background-color:midnightblue;">&nbsp;Lesson 8  &nbsp;Numbers (10-99)</span></H1>
<center><img src=l8nombor2.gif width=400 height=50></center><br>
 <font color=darkblue><b>BEFORE YOU BEGIN:</b></font></b>
 <br><FONT face=tahoma color=black size=3>If  you have mastered counting from one to nine in Lesson 5  you now need to know only two more words (<font color=red>belas</font> and <font color=red>puluh</font>) to be able to count from 10 to 99.
<br>Counting from 11 to 19 in Malay is simple when you remember how 13 to 19 are counted in English (as in four-teen, six-teen, eighteen, nine-teen).   The "teen" in the second syllable is replaced by <font color=red>belas</font>, that's all.   Study the examples in the first column. 
<br>Counting the tens (10, 20, 30, 40, etc.) in Malay is like counting the tens in English (note the second syllable in forty, six-ty, eighty, nine-ty).  The "ty" is replaced by <font color=red>puluh</font>, that's all.   Study the examples in the second column.  
<br>As for all the other numbers in-between 10 and 99 they are formed in exactly the same way as in English .  Thus when you have a number such as 47 you only have to say forty (<font color=red>empat puluh</font>) first, then seven (<font color=red>tujuh</font>).  For more examples see Column 3 in the table below (I have underlined the part in tens to help you).  Good luck!
