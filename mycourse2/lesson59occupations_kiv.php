<FONT face=tahoma color=black size=3>The words denoting the various occupations are best learnt by putting them into  4 groups (their meanings are given in the table below):<br>
(i) words that come after the word <font color=red> tukang </font> (meaning skilled workman or craftsman)  but are not joined to it (words in the first column)<br>
(ii) words that come after <font color=red>  juru </font> and are joined to it to form a single word (meaning a skilled worker other than a handicraftsman) as in the second column<br>
(iii) words that begin with the pe(N) prefix (words in the third column).  The rules regarding the use of the pe(N) prefix are explained in the <a href=appendix.php> Appendix</a>.<br>
(iv) words that don't follow any of the above rules but just stand by themselves are shown in the fourth column.  <br><br> Here is a table of some common occupations with their English equivalents:

