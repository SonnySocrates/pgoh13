<br><br>Both <font color=red>tukang gambar</font> and <font color=red>jurugambar</font> are used for a photographer.<br>
Similarly both <font color=red>juru kira-kira </font> and <font color=red>akauntan </font> (English word adapted to Malay spelling) mean an accountant.
<br>And you can use either <font color=red>pemberita </font> or <font color=red>wartawan </font> for a reporter or journalist.
<br>Actually the word <font color=red>kira</font> by itself means to count though it is frequently used in the sense of assuming something eg. <font color=red>Saya kira dia tidak akan datang</font> means "I think he is not coming."
<br><br>
Regarding the third group i.e. words beginning with the pe(N) prefix:<br />For the moment just be aware that the pe(N) prefix can take the following forms:  pe-, pem-, pen-, peng-, peny- depending on the initial letter of the root word. 

<br>The most usual way of asking a person's occupation is: <font color=red>Apa pekerjaan anda? </font> (= What's your job?) or <br>
<font color=red>Apa pekerjaannya?</font> (= What's his/her job?)<br>
 Note that when we talk about a person's occupation the verb "to be" is normally left out of the Malay sentence.  Thus to say "I am a journalist" you only have to say  <font color=red>Saya wartawan</font> (literally "I journalist") though you can also say <font color=red>Dia seorang wartawan</font> (= He is a journalist) or if you feel the need to put in a verb:  <font color="red">Dia adalah seorang wartawan.</font>
<br><br>DON'T BE DISCOURAGED!
<br />I don't remember if I have said this earlier.  Never, never be afraid to <font color=red>campur</font> (mix) English words with Malay if you don't know how to say them in Bahasa Malaysia.  All Malaysians do that so as a foreigner learning Malay you also have a right to it!  Even the well-educated Malays do that (in this case it's not because they don't know how to say the whole thing in Malay).  Somehow a sprinkling of English with Malay in a conversation makes the setting more easy-going or relaxed and less formal.  A good example is in using the English word "you" among friends in the place of <font color=red>anda, kamu</font> or  <font color=red>awak</font> as in: <br /><font color=red><i>Bila</font> you <font color=red>nak pergi?</font> (When are you going?).</i>  Or the boss could tell his secretary <i>"Make sure <font color=red>saya tak ada</font> appointment <font color=red> untuk esok ya</font>?".</i>  (Make sure I have no appointments for tomorrow, ok?).
<br>Even the people at the very top do <font color=red>campur</font> their Malay with English (or the other way round if you prefer).  This, for example, is part of a verbatim report of what the former Prime Minister Tun Dr Mahathir Mohamad said when talking to reporters:  "You <font color=red>tak boleh</font> publish ini <font color=red>semua</font> in the newspapers. It's all censored."  (From a Sun2Surf report dated April 13, 2006).
<br />So when your Malay vocabulary falls short and you have to bring in an English word or phrase, just do it and remember that you are in good company!
<br>In fact this sprinkling of English words with Malay is so prevalent that the Arts, Culture and Heritage Minister Datuk Seri Dr Rais Yatim was reported to have told The Star newspaper on April 18, 2004: "And most glaring is how language is being spoken in the country. That can also be a question for (the) Education (Ministry) but in the heritage department, we would like to say: Do not make the language so <font color=red>rojak</font> so as to make it unintelligible in the future. The French dont mix their language with other words. But in Malaysia, half our sentences is in English while the other half is in Malay. In the end, we dont pick up the good parts in either language."
<br>But then even Members of Parliament do this (mixing English and Malay in their speeches in the Malaysian Parliament) so much so that the Speaker had to bring this matter up as in the following report (though knowing the state of things in Malaysia I doubt this will have much effect):

<blockquote><em>KUALA LUMPUR, April 18, 2006 (Bernama) - Dewan Rakyat Speaker Tan Sri Ramli Ngah Talib Tuesday ticked off some members of the Dewan Rakyat who mixed up Malay and English when addressing the House, describing their act as polluting the Malay language. He said the reminder was made after it was found that, on several occasions, Members of Parliament (MPs) did not use the Malay language in full but alternated by speaking in English in the Dewan Rakyat.</em></blockquote>

Well, there are pedantics everywhere.  I personally feel that those who are truly bilingual (in whatever languages) will agree with me that certain things are best said in one language and other things in another so in the case of such people it is not uncommon for them to use two languages in the same sentence!

