<script>
    // Auto-format date while typing
    function formatDate(input) {
        let value = input.value.replace(/\D/g, '');
       
        if (value.length > 2) {
            value = value.substring(0,2) + '/' + value.substring(2);
        }
        if (value.length > 5) {
            value = value.substring(0,5) + '/' + value.substring(5,9);
        }
       
        input.value = value.substring(0,10);
    }

    function calculateAge() {
        var today = new Date();
        var inputStr = document.MF.madate.value.trim();
        
        // Clear result if input is empty or incomplete
        if (!inputStr || inputStr.length < 8) {
            document.getElementById('ageSentence').innerHTML = "Saya berumur __ tahun (pada hari ini)";
            return;
        }

        var numbers = inputStr.replace(/\D/g, '');
       
        if (numbers.length !== 8) {
            return;
        }

        var day = parseInt(numbers.substring(0,2));
        var month = parseInt(numbers.substring(2,4)) - 1;
        var year = parseInt(numbers.substring(4,8));

        var birthDate = new Date(year, month, day);

        if (birthDate.getFullYear() !== year ||
            birthDate.getMonth() !== month ||
            birthDate.getDate() !== day) {
            document.getElementById('ageSentence').innerHTML = "Saya berumur __ tahun (pada hari ini)";
            return;
        }

        var age = today.getFullYear() - birthDate.getFullYear();
       
        if (today.getMonth() < birthDate.getMonth() ||
           (today.getMonth() === birthDate.getMonth() && today.getDate() < birthDate.getDate())) {
            age--;
        }

        // Update only the red sentence
        document.getElementById('ageSentence').innerHTML = "Saya berumur " + age + " tahun (pada hari ini)";
    }
</script>






The <font color=red>pada hari ini</font> (meaning "on this day") is just to add a touch of humour, or if you prefer, to be more precise because, who knows, tomorrow could be your birthday and that would make all the difference!

<br><br>For more meaningful practice how about making a few sentences of the members of your family?  A full list of family members can be found in Lesson 29 (no harm going there now for this purpose).  Thus:
<br><br><font color=red>Emak saya berumur empat puluh tiga tahun.</font>  (My mother is 43 years old.)
<br><font color=red>Abang saya berumur sembilan belas tahun.</font>  (My elder brother is 19 years old.)
<br><font color=red>Adik perempuan saya berumur tujuh belas tahun.</font>  (My younger sister is 17 years old.)
<br><font color=red>Mary berumur lima puluh lapan tahun.</font>  (Mary is 58 years old.)
<br><font color=red>Suaminya berumur enam puluh dua tahun.</font>  (Her husband is 62 years old.)
<br>Please note that you can use the word <font color=red>berusia</font> instead of <font color=red>berumur</font>.
<br>Okay if you want to take the easy way out, study the above sentences in Malay and when you have learnt them just look at the English sentences and see if you can translate them into Malay.  Or write out your answers if you prefer.  Good luck.
