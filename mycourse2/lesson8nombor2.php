<h2 style="color:yellow;background-color:midnightblue">Berapa umur anda?  (How old are you)</h2>
<br><span style="max-width:800px;font-family:tahoma;font-size:16px;">We are now ready to practise asking and answering questions about a person's age.  By the way if there is any centenarian here please kindly excuse me as we have not come to 100 yet!  It will be in Lesson 22.
<br>So let's start with the standard question which is <font color=red>Berapa umur anda?</font>  (How old are you?) and the standard answer <font color=red>Saya berumur ____  tahun</font> or <font color=red>Umur saya ____  tahun</font>  (I am ____ years old). Like all languages, of course, there is more than one way of asking or answering but we will just stick to one model to make our learning easier.

<br><br>
Let's have your age in black and white to make it easier for practice.












