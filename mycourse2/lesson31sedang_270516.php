<br><font color=red>sedang</font> is used to indicate that someone is in the middle of doing something (what grammarians call the present continuous tense). 
 As in the examples above, where you need to add -ing to an English verb you would put  <font color=red>sedang</font> before the verb in Malay.
<br>
Actually the word <font color=red>sedang</font> is not completely new to you as you have already come across it in Lessons 7, 13, 18 and 29.  This will give you an idea of how important the word is in Malay.
<br>A frequent question you might want to ask is "What are you doing?"  In Malay it is <font color=red>Anda sedang buat apa?</font> while in Indonesian <font color=red>Anda sedang apa?</font> is enough.
<br>
It can also be used to describe something that was happening in some past action as in the following example:
<br><font color=red> Bila saya masuk ke pejabatnya dia sedang baca surat khabar</font>.  (When I entered his office he was reading a newspaper.)
