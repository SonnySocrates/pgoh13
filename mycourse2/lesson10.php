<h3>For those who want to know more</h3>

<br>
<p>As can be seen from the above, the 12 months of the year are all borrowed from English with adaptations to their spelling. 
Thus "March" is spelt <font color=red>Mac</font> because  "ch" in all Malay words is replaced with just the letter "c" though the pronunciation remains that of "ch" (as in "chair").
<br>Just as we normally precede the days of the week with the word <font color=red>hari</font> (eg. <font color=red>hari Isnin, hari Selasa</font>, etc.) so the months of the year usually start with the word <font color=red>bulan</font> eg. <font color=red>bulan Januari, bulan Februari</font>, etc. when you want to say January or February.
<br>And how do you say "in the month of March"?  In Malay we say <font color=red>pada bulan Mac</font> (literally "at the month of March").
<br>There is another meaning for <font color=red>bulan</font>, apart from "month".  It also means the moon.
<br>The Malay word for "date" with respect to the calendar is <font color=red>tarikh</font> or <font color=red>tanggal</font> (this word is more used in Indonesia).
<br>Example:  <font color=red>Poskad yang baru saja diterima ini bertarikh 30 Mac 2016.</font>  (The postcard which has just been received was dated 30 March 2016.)   I know this is quite a difficult sentence to read.  There is a little pause after <font color=red>Poskad yang baru saja diterima ini</font> before you finish the sentence as <font color=red>baru saja</font> means "just now" or "at this moment" while <font color=red>ini</font> actually refers to the postcard (<font color=red>Poskad ini</font> being "This postcard").



