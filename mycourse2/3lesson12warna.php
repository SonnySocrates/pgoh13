<p style="max-width:800px;font-family:tahoma;font-size:16px;"><em>*The above question only serves to put the study of colours in a meaningful context.  If, like me, you don't possess a car, there's nothing to be ashamed about!  In that case just answer <font color=red>Saya tidak ada kereta</font> or if you want to add a touch of regret, or perhaps just to impress the listener with your Malay <span class="fa fa-smile-o" aria-hidden="true" style="background-color:yellow"></span> <i class="fa fa-thumbs-o-up" aria-hidden="true" style="background-color:lightgreen"></i> then say <font color=red>Ah, malangnya saya tidak mempunyai kereta</font> (Ah, unfortunately I don't have a car).  Note the four syllables in <font color=red>mempunyai</font> (pronounced mem-pu-nya-ee) which simply means to own or possess.</em></p>


