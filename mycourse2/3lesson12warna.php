<p style="max-width:800px;font-family:tahoma;font-size:16px;"><font color=red>Orang putih</font> means a person of the white race (see Lesson 4).  It's not really pejorative or insulting, I can assure you.  The Indonesians make it even clearer by saying <font color=red>orang kulit putih</font> meaning a person with white skin (<font color=red>kulit</font> meaning "skin").  There is another word often used in Malaysian conversation for a Caucasian and that is <font color=red>mat salih</font> (again it has no pejorative connotation).  On a more formal level the term <font color=red>orang Barat</font> (meaning "Westerners") is used.  While on the subject, the word for a "foreigner" (i.e. anyone who is not a Malaysian) is <font color=red>orang asing</font> (<font color=red>asing</font> meaning "foreign").
<br>With more and more Africans, especially from Nigeria, making their presence felt in Malaysia (a recent phenomenon), a popular and simplistic term to refer to them is Pak Hitam (or Awang Hitam), <font color=red>hitam</font> being the Malay word for black, as you have learnt above.  (See: <a href="http://www.nytimes.com/2015/08/27/opinion/tash-aw-unwelcome-in-malaysia.html" target=_blank>"Malaysias Welcome Wears Thin" by Tash Aw in International New York Times</a>)  You will be advised though to use the politically correct word, which is <font color=red>orang Africa</font>, whether you are in Malaysia or Indonesia.

