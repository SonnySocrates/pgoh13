<table><tr><td><font color="darkblue"> <u>For those who want to know more:</u></b></font>
<br><FONT face=tahoma color=black size=3>To say that a colour is dark all you have to do is to put the adjective <font color=red>tua</font> (literally "old") after it.  Example:
<br>dark red = <font color=red>merah <u>tua</u></font>
<br>dark blue = <font color=red>biru tua</font>
<br>dark yellow = <font color=red>kuning tua</font>
<br>dark green = <font color=red>hijau tua</font>

<br><br>Similarly to say that a colour is light all you have to do is to put the adjective <font color=red>muda</font> (literally "young") after it.  Thus:
<br>light green = <font color=red>hijau <u>muda</u></font>
<br>light blue = <font color=red>biru muda</font>
<br>light yellow = <font color=red>kuning muda</font>
<br>light red = <font color=red>merah muda</font>
<br>
<p style="max-width:800px;font-family:tahoma;font-size:16px;">By the way the Malay word for "pink" is also <font color=red>merah muda</font> or <font color=red>merah jambu</font>, "jambu" ("guava" or "rose apple" in English) being the name of a Malaysian fruit that is pink in colour (on the outside at least).  As the rose colour is close to pink it is also called <font color=red>merah jambu</font> in Malay.

<br><br>At times you might want to say that something has the tinge, hue or shade of a certain colour (eg. reddish, yellowish, greenish, bluish, etc).  It's quite simple to do this in Malay.  Just repeat the colour word and add the prefix <font color=red>ke</font> and the suffix <font color=red>an</font> to it.  Thus:
<br>reddish = <font color=red>kemerah-merahan</font>
<br>yellowish = <font color=red>kekuning-kuningan</font>
<br>greenish = <font color=red>kehijau-hijauan</font>
<br>bluish = <font color=red>kebiru-biruan</font>

<br><br>
Just as <font color=red>coklat</font> takes its name from the colour of chocolate, the same is true for <font color=red>oren</font>, which takes its name from the colour of orange.  There is another word for this colour though and that is <font color=red>jingga</font>.
<br />
Apart from <font color=red>coklat</font> there is another word for "brown" in Malay and that is the word <font color=red>perang</font> (pronounced as pay-rang).   If you are heavily suntanned you would be described as <font color=red>perang</font> though if the word is used for hair it would mean "fair-haired" or blond rather than brown.  But as chocolate itself is brown in colour I have used the word <font color=red>coklat</font> but if you prefer the word <font color=red>perang</font> make sure you pronounce it as pay-rang (if you know French it will help your pronunciation to see it spelt as p&eacute;rang).  Unfortunately in Malay you don't have the &eacute; to help you with the pronunciation and the same word <font color=red>perang</font>, if it is pronounced p&#x0259;rang (p&#x0259; has the schwa vowel sound &#x0259; as in "per cent") means "war" .  So watch out, if you are not going to start a war, pronounce the word for "brown" as pay-rang and not p&#x0259;rang.  But why take the risk when you can use <font color=red>coklat</font> for the brown colour?
<br>As for gray, which is the background colour of all the pages in this website, the Malay word for it is <font color=red>kelabu.</font>

</p><br>
