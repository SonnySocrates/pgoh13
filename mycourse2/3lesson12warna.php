<h3 style="color:yellow">A short conversation in Malay</h3>
<div class="col-lg-4"><img class="img-responsive" src=tiga_payung.jpg></div>
<div class="col-lg-0.5"></div>
<br>Saya suka payung yang kuning itu.
<br>Dan anda?  Anda suka payung yang mana?
<br>Saya suka payung yang merah.
<br>Saya suka payung yang biru juga.
<br><em>Translation:</em>
<br>I like that yellow umbrella. (lit. I like the umbrella that is yellow.)
<br>And you.  Which umbrella do you like?
<br>I like the red umbrella.  (lit. I like the umbrella that is red.)
<br>I also like the blue umbrella.  (lit. I like the umbrella that is blue.)</p>
