<h3>For those who want to know more</h3>
<p style="font-size:16px;">The standard reply to sentences one and two is: <font color="red">Tak apalah. </font> (= It doesn't matter.)<br>
This is one  sentence you have to use each time you don't understand something to avoid misunderstanding:  <font color="red">Maafkan saya.  Saya tidak faham.</font>  (= Excuse me.  I do not understand).<br>
Please note that there are three syllables in the word <font color=red>Maafkan</font> (ma-af-kan).
<br>
To tell someone that you feel sorry for another person's plight you can say  <font color="red"> Saya kasihan sama dia</font>  (= I feel sorry for him/her).
<br>
To say that you regret having done something you can say <font color="red"> Saya menyesal perbuatan itu.</font><br>
(<font color="red">menyesal</font> comes from <font color="red">sesal</font> meaning "to regret" and the syllabification is: me-nye-sal)<br>
To excuse oneself for something unimportant you can say <font color="red">Jangan marah ya? </font>  (= Please don't be angry) and to put it in an even  lighter vein (this often works with a young woman!), you can say  <font color="red">Jangan marah ya, nanti lekas tua!</font> (= Please don't get angry or you will become old quickly!)  There you have another example of the word <font color="red">nanti</font> not in the sense of "waiting" but to indicate some forthcoming action or event.
<br>
If you should trip someone up, or just as bad, step on his toes (!) make sure you say  <font color="red">Maafkan saya</font> or <font color="red">Minta maaf</font>.  Malaysians are a friendly and forgiving race and such an apology will always be accepted with good grace (provided you don't repeat the accident the minute after, in which case I can't guarantee you that they will be as forgiving as I said they are!)<br>
Either of these two expressions or just  <font color="red">Maaf</font> alone is also used when interrupting someone or being obliged to make one's way between two people holding a conversation.  (But in such a situation an Indonesian would say <font color="red">Permisi</font>).  Translate this into English as the equivalent of "With your permission" and you would have no problem remembering to use it when the occasion arises.<br>
When you are unable to accept an invitation you can say <font color=red> Maafkan saya tetapi saya tidak dapat datang pada hari itu.</font> (= Please excuse me but  I will not be able to come on that day).<br>
Note how the verb  <font color="red">dapat</font> which usually means "to get" or "to obtain" is used here.   When followed by another verb (as in this sentence) it has the sense of being able to (<font color="red">Saya dapat</font> + a verb) or not being able to (<font color="red">Saya tidak dapat</font> + a verb) do the action implied in the verb that comes after it.<br>When used in this way it can always be replaced by <font color="red">boleh</font>.  Thus instead of saying <font color=red> Maafkan saya tetapi saya tidak dapat datang pada hari itu</font> you can say <font color=red> Maafkan saya tetapi saya tidak boleh datang pada hari itu.</font>
<br>The word <font color="red">Tolong!</font> used as an exclamation means "Help!" (try shouting this out when you are drowning!) but when it precedes another verb as in <font color="red">Tolong maafkan saya</font> it can be translated as "please" so that sentence means "Please excuse me."
