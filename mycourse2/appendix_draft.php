<h2>&nbsp;&nbsp;The pe(N) and me(N) prefixes&nbsp;&nbsp;</h2>
 <br>
<FONT face=tahoma color=black size=3> The <b>pe(N)</b> prefix indicates that a word is a <font color=red>NOUN</font> whereas the <b>me(N)</b> prefix  indicates that the word is a <font color=red>VERB</font>.
<br>The <b>pe(N)</b> prefix can take any of the following five forms:<br><b>pe-, pem-, pen-, peng-</b> or <b>peny-</b>
<br>Correspondingly the <b>me(N)</b> prefix can take any of the following five forms:
<br><b>me-, mem-, men-, meng-</b> or  <b>meny-</b>
<br>The form that it takes depends on the initial letter of the root word.   This rule is best illustrated by specific examples. In the following tables the second column shows the root word, the  third column the same word with the <b>pe(N)</b> prefix and the  last column shows  the  word with the <b>me(N)</b> prefix .  <br />It is unlikely that you will be able to find the word <font color=red><b>penulis</b></font>, for example, in a Malay-English dictionary as it will appear  under the word from which it is derived (<font color=red><b>tulis</b></font>).  <br />On the other hand if you should look up an English-Malay dictionary (eg. if you need to know the Malay word for "use")  the dictionary will normally give you the word <font color=red><b>memakai</b></font>.  You have to know that this is not a word by itself but is actually the word <font color=red><b>pakai</b></font> with the <b>me(N)</b> prefix attached to it!  Putting myself in the place of the student, I can imagine how difficult this question of prefixes is, especially for verbs.  The only way to really master this is to go daily to my page for advanced students (Building Up Your Malay Vocabulary) <a href=malay_sentences.php target=_blank>here.</a></FONT><br>
