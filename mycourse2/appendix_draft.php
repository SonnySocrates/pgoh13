<h2>&nbsp;&nbsp;Other prefixes in Malay&nbsp;&nbsp;</h2>
<br>
<FONT face=tahoma color=black size=3>Please note that the above are not the only prefixes and suffixes that exist in the Malay language.  The prefix <b>ter-</b> is quite frequently used and is attached to verbs to show that the action is unintentional or accidental (eg. <b>terjatuh</b> meaning "to fall" or <b>terdengar</b> meaning "to hear accidentally" i.e. to overhear).
<br>Another use of the <b>ter-</b> prefix (when it is attached to an adjective) is to denote the superlative case eg. <b>terbesar</b> meaning "biggest".
<br />Then there is a group of nouns which are formed by adding the prefix <b>ke-</b> and the suffix <b>-an</b>, such as the words <b>kebolehan</b> meaning "ability" from <b>boleh</b> and the word <b>kekalahan</b> meaning "defeat" from <b>kalah</b>.
