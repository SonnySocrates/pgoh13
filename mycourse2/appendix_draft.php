<h2>&nbsp;&nbsp;The ber- prefix&nbsp;&nbsp;</h2>
 
<br><FONT face=tahoma color=black size=3>There are a number of cases where the <b>ber-</b> prefix is used instead of the <b>meN-</b> prefix but I will only deal with the most common ones here.  The <b>ber-</b> prefix is used:
<ul>
<li>for an action that one performs upon or for oneself such as:
<b>berjalan (to walk), berlari (run), berdiri (stand), bercukur (shave), bersolek (make up), bermimpi (dream), bersembunyi (hide) and berfikir (think).</b>
In the case of verbs that already start with the letter "r" (such as rehat and renang) only "be-" and not "ber-" is prefixed making it <b>berehat (to rest)</b> and <b>berenang (swim)</b>.

<li>for verbs that indicate possession eg.
<font color=red>Sejak berumur lima tahun dia tidak lagi <b>beribu</b>.</font> (Since he was five he did not have a mother.)<br>
<font color=red>Orang kaya itu <b>berkereta</b> besar.</font>  (That rich man has a big car.)<br>
<font color=red>Durian <b>berduri</b>.</font> (The durian has thorns.)<br>
<font color=red>Saya tidak <b>berhak</b> meminta pekerjaan itu kerana saya bukan orang Malaysia.</font>  (I don't have the right to apply for that job because I am not a Malaysian.) 
<li>for verbs that indicate what a person is wearing eg. <font color=red>Dia <b>berbaju</b> batik.</font> (He is wearing a batik shirt), <b>berkasut</b> putih (wears white shoes), <b>berseluar</b> jean (wears jeans) and  <b>bercermin</b> mata (wears spectacles).
<li>for an action between two people or interaction with other people eg
<b>berlawan (to fight), bertumbuk (box), berkahwin (marry), bermain (play) and bergurau (joke).</b> 
<li>for verbs of "utterance" eg. <b>berkata (to say), bercakap (speak), berdoa (pray), berjanji (promise), berterima kasih (or bersyukur)</b> both meaning to give thanks
</ul>

<h2>&nbsp;&nbsp;The -kan suffix&nbsp;&nbsp;</h2>
 <br>
Note that the <b>meN-</b> prefix, if used together with the <strong>-kan</strong> suffix, can be used on reflexive verbs (which normally take the <b>ber-</b> prefix) in order to turn them into transitive verbs i.e. verbs that have to be followed by an object eg. <font color=red>Tukang rambut sedang <b>mencukurkan</b> pelanggannya.</font> (The barber is shaving his customer.)  But if you want to say that the barber is shaving himself, and not the customer, the sentence will read <font color=red>Tukang rambut sedang <b>bercukur</b>.</font>  In such a case <b>sendiri</b> (oneself) is not necessary as it is already implied in the <b>ber-</b> prefix used for reflexive verbs.
<br>Here is another example where the <b>meN-</b> prefix is used with the <strong>-kan</strong> suffix to turn the verb into a transitive verb:  <font color=red>Perempuan itu sedang menidurkan anaknya.</font> (The woman is putting her child to sleep.)  If you are wondering what <strong>menidurkan</strong> is all about, I don't blame you as the root word is <strong>tidur</strong> (to sleep).  Didn't I warn you that Malay affixes are difficult?

<br>
<h2>&nbsp;&nbsp;The -i suffix&nbsp;&nbsp;</h2>
<br>This is a suffix that I didn't think of introducing until I received the following email from a student in Spain asking me about it.  He writes as follows:
<em><blockquote>Hello,
<br>I was wondering if you could help me with a little question, if it's not too much of a nuisance.
<br>I came across this sentence:
<br>Kita rentasi gunung ini bersama
<br>So I looked up "rentasi", but I didn't find a translation for it, and after some trial and error I discovered that the actual word was "rentas" (= to cross, to take a shortcut), with -i added as suffix.
<br>After some more search I found this explanation on the -i suffix from http://en.wiktionary.org/wiki/-i#Malay
<br>It states that the -i suffix expresses "locative, repetitive, or exhaustive" action, which is too vague an explanation for me to learn how to use it.
<br>Is the -i suffix covered somewhere in your website? Or do you think it's not worth learning it "theoretically" and it's just better to wait until enough vocabulary exposure gives me a feel of how to use it?</blockquote></em>

To answer his question I would say that there are really no hard and fast rules concerning the -i suffix though grammarians, as is expected of them, will always try to come up with an explanation.  Malay prefixes and suffixes are already difficult for foreigners studying Malay and for a learner to go by the grammatical explanation above would be like banging his head against the wall.  So if you ask me I would say that the best way to learn the -i suffix is through usage.
<br>The example mentioned by the student (<em>rentasi</em>) is less used than the words using the -i suffix that I am going to mention below.  Come to think of it, the -i suffix is used quite frequently, despite the fact that it is not often given as much attention as the other prefixes and suffixes and you might have occasion to use (or at least come across) them in the sentences below.
<br>Like verbs with the -kan suffix mentioned earlier (sorry, I won't be going into the differences between them as that would be for the academicians!), the -i suffix verbs are all transitive verbs.  This means that you can add the me(N) prefix to them (though they can also do without) as well as turn them into the passive form -- in which case they must not have the me(N) prefix.
<br>Ok, let's get down to business, shall we?  The following words are often used with the -i suffix (as you can see most of them are already verbs in their root form while there are also adjectives (dekat, jauh, cukup) and prepositions (atas, hadap) that are transformed into verbs once the -i suffix is attached to them.
<br>sayang, masuk, periksa, jalan, naik, hadir, contoh, duduk, kahwin, kunjung, sudah, hubung, 
nikmat, ulang, ikut, dapat, temu, lalu, mula, ajar, amal, datang, dekat, jauh, cukup, pada, atas, hadap, dahulu
<br>The following illustrative sentences will show you how they are used in sentences:
<br>Ibu saya menyayangi bayi itu sebagai anaknya sendiri. (My mother loves the baby as if it's her own child.)
<br>When turned into the passive voice the above sentence becomes:
<br>Bayi itu disayangi ibu saya sebagai anaknya sendiri.  (The baby is loved by my mother as if it's her own child.)
<br>Here is another example:
<br>Pencuri memasuki (from masuk) rumahnya semasa dia makan di restoran.  (The thief entered his house while he was eating at a restaurant.)
<br>When turned into the passive voice the above sentence becomes:
<br>Rumahnya dimasuki pencuri semasa dia makan di restoran.  (His house was broken into by a thief while he was eating at a restaurant.)
<br>From the above you would have noticed that the -i suffix is commonly used with both sentences in the active as well as the passive voice.  I am sure you get the drift so I will not turn the sentences below into the passive voice from now on.
<br>Polis memeriksai semua emel yang terdapat dalam komputernya.  (Police examined all the emails that were found in his computer.)
<br>Dia akan menjalani satu pembedahan bulan hadapan.  (He will undergo an operation next month.) 
<br>Dia menaiki basikal abangnya.  (He is riding his elder brother's bicycle.)
<br>Lebih seribu orang telah menghadiri mesyuarat itu.  (More than a thousand people attended the meeting.)
<br>Dia mencontohi sebutan gurunya.  (He follows his teacher's pronunciation.)
<br>Sudah lama dia menduduki rumah ini.  (He has been living in that house for a long time.)
<br>Dia tidak sanggup mengahwini orang tua yang kaya itu.  (She is unwilling to marry the rich old man.)
<br>Ramai orang yang mengunjungi Menara Petronas tiap hari.  (Many people visit the Petronas Towers every day.)
<br>Dia sudahi suratnya dengan ucapan Selamat Berjaya.  (He ended his letter with wishes of success.)
<br>Saya akan menghubungi anda dalam sedikit masa lagi.  (I shall contact you shortly.)
<br>Dia sedang menikmati kuih yang dibuat oleh ibunya.  = He is enjoying the (Malaysian) cakes made by his mother.
<br>Saya akan mengulangi sekali lagi kerana nampaknya anda sudah lupa.  (I will repeat once again as it appears that you have forgotten.)
<br>Dia berjanji dia akan mengikuti tunangnya ke mana sahaja dia pergi.  (She promises that she will follow her fianc&eacute; wherever he goes.)
<br>Saya dapati bahawa dia tidak berapa jujur.  = I find that (meaning, "In my opinion") he is not quite honest.
<br>Dia akan menemui rakan sedarjahnya selepas meninggalkan sekolah 30 tahun dahulu.  (He will be meeting his classmate after leaving school 30 years ago.)
<br>Kita akan memulai pelajaran sekarang.  (We will start our lesson now.)
<br>The preposition "pada" (meaning at or on) is sometimes used as an adjective meaning "enough, sufficient".  It is as an adjective that the -i suffix is used with so memadai is like mencukupi.  Both words would be the equivalent of "suffice" in English.
<br>Wang sebanyak 100,000 ringgit setahun tidak memadai (you can also use mencukupi here) untuk perbelanjaan setahun di Universiti Harvard.  (A sum of 100,000 ringgit is not sufficient pour a year's expenditure at Harvard University.)
<br>The preposition "atas" (meaning "above") is often used as a verb with the -i suffix so mengatasi means to overcome.   This is easy to remember as the verb would mean to "get above, on top of something" i.e. to overcome eg.
<br>Jangan ragu, semua masalah dapat diatasi.  (Don't worry, all problems can be surmounted.)
<br>Similarly menghadapi means "to confront, face or face up to" if you prefer eg. a problem.  The root word "hadap" (hadapan) means "front" eg.
<br>Dia tidak takut menghadapi semua masalah dalam kehidupan. (He is not afraid to confront all the problems in life.)
<br>An adjective that can be transformed into a verb with the -i suffix is dekat (near).  Thus
<br>Dia mendekati saya lalu meminta wang daripada saya.  (He approached me and asked me for money.)
<br>mendahului
<br>mempelajari
<br>mengamali
<br>melalui
<br>mendatangi means "to bring about" eg.
<br>Tabiat suka berjudi akan mendatangi berbagai-bagai kesusahan kelak.  (The gambling habit will bring about lots of difficulties in future.)
<br>These are about the most common examples of verbs using the -i suffix that I can think of.   Just learn them off by heart without worrying too much about why you use the -i suffix in those sentences.   After all, you only need to know how to use it correctly, not to explain to anyone why you use it!




