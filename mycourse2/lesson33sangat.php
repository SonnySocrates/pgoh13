<u>More examples:</u><br>
 <font color=red>Dia sangat pandai </font>  (He is very clever)<br>
 <font color=red>Kopi ini sangat panas </font>  (This coffee is very hot)<br>
  Note the difference between the above sentence and this: <font color=red> Kopi ini TERLALU panas, saya tidak dapat minum sekarang.</font> (= This coffee is TOO hot, I am not able to drink it now.)
<br>Another word that is more frequently used in the place of <font color=red>mustahak</font>, especially in Indonesia, is <font color=red>penting</font>.  But both words are equally used in Malaysia so you will have to remember both of them.





