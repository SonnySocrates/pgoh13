There is another word for "very" though it is  a little bit more formal than <font color=red>sangat</font>.  The word is <font color=red>amat</font> eg. <font color=red>Barang ini amat mahal</font> (This item is very expensive) or <font color=red>Dia amat pandai</font>.  (He is very intelligent.)
<br />The word <font color=red>barang</font> is quite a useful word to know as you can use it when you don't know the name of an object or are not bothered to call it by its name.   Its equivalent in English would be "thingummy" or "thingy".   If you want to find out what the name of something is in Malay, for instance, you could just point at the article and ask <font color=red>Apa nama barang ini?</font> 
<br>And what is another word for <font color=red>barang</font>, do you remember?  It also starts with the letter "b" (you will find it in Lesson 19).
<br>As you would have noticed in the table above, the two very commonly-used adjectives (<font color=red>murah</font> meaning "cheap" and <font color=red>marah</font> meaning "angry") can get you mixed up as they are quite similar to each other.  In order to be clear which is which, how about shouting out now in a very angry manner <font color=red>Saya MAAAAAAAAAAAAAAAAARAH!</font>   (= I'm ANNNNNNNNNNNNNGRY!).  It might work, I don't know ;-)  But first make sure that your wife or husband or children or office colleagues are not around or they will think that you are <font color=red>gila</font> (mad)!
