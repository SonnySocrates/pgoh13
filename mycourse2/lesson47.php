<p>If you can remember that <font color=red>banyak</font> means "plenty of" or "a lot of" it is easy to remember that <font color=red>kebanyakan</font> means "the majority of" or "most of" (whatever you are talking about).  In our daily conversation how often do we need to say "most of" or "the majority of" so this word <font color=red>kebanyakan</font> can come in really handy.
<br>A synonym of <font color=red>senang</font> (= easy) that is frequently used in Indonesia as well as in Malaysia is <font color=red>mudah</font> while Indonesians often use the word <font color=red>gampang</font> eg. <font color=red>Itu gampang sekali!</font> (Well that was easy!) or <font color=red>Berbicara itu gampang, yang sukar adalah mengerjakannya.</font> (= It is easy to talk, what is difficult is to carry it out.)  By the way <font color=red>mengerjakan</font> is a good example of how a noun (in this case <font color=red>kerja</font> meaning "work") can be turned into a transitive verb by adding the prefix <font color=red>me(N)-</font> and the suffix <font color=red>-kan</font> (this is a VERY difficult aspect of Malay) and a full explanation with examples are given in the <a href="http://pgoh13.com/mycourse/appendix.php" target=_blank>appendix here.</a>
