<br><FONT face=tahoma color=black size=3>Note the two meanings of <font color=red>senang</font>:
<br>The first meaning is "simple, easy" eg. <font color=red>Kerja itu senang sahaja.  Dia pun boleh buat. </font> (That job is really simple.  Even he can do it.)   The word <font color=red>pun</font> is used here in the sense of "<em>even to the extent of</em>".  (By the way the little word <font color=red>pun</font> has got quite a number of other uses and if you'd like to learn more about these I have put them together in one page, following an email inquiry from a student.  Go here for <a href=usesof_pun.php>Various uses of the Malay word <font color=red>pun</font>.</a>)
<br>The second meaning of the word <font color=red>senang</font> is "free, not busy" (You have come across this already in Lesson 17) eg. <font color=red>Datanglah ke rumah saya bila senang.</font>  (Come to my house when you are free.)
<br>Note that <font color=red>senang hati</font> and <font color=red>suka hati</font> also mean "happy" (apart from <font color=red>gembira</font> given above).

<br>A word you might want to add to your Malay vocabulary is <font color=red>santai</font>. It has so many equivalents in English, among them, "cool, relaxed, casual, informal, take it easy, chill". A word to use on your friend when he gets on your nerves! <font color=red>Santai saja, kawan!</font> (Take it easy, buddy!)

