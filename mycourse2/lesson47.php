<br><p style="max-width:800px;font-family:tahoma;font-size:16px;">
Note that while <font color=red>ramai orang</font> means "many people", when it is reversed and becomes <font color=red>orang ramai</font> then it takes on a different meaning.  In fact <font color=red>orang ramai</font> means "the public".  Examples:
<br><font color=red>Ramai orang berkumpul di dalam dewan itu untuk mendengar ucapannya.</font> (= Many people gathered in the hall to hear his speech.)
<br><font color=red>Orang ramai bertepuk tangan apabila dia mulai bercakap.</font> (= The public clapped their hands when he started to speak.)
