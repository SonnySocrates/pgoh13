<p>As important as verbs and nouns, adjectives are an integral part of any language's basic vocabulary.  I have grouped the most common (and therefore important) ones here. These are adjectives that you are likely to use in a conversation with friends.
<br>Actually you can consider this one lesson as the equivalent of three lessons so please take your time over it.  Don't rush to the next lesson before you have memorized the new vocabulary here.  I know there are lots of new words to be learnt off by heart here but these are the very basis of your Malay vocabulary so again give yourself plenty of time over this lesson.
<br>To make matters worse, as you will see in the list of "Common Synonyms" below, it is not enough to learn just one Malay word for one English word because quite often there are two Malay words, one of which is used about 50% of the time and the other is used in the other 50%.  And these are very, very common words, so there is no way you can escape by just learning one of the two words!
<br><br>

