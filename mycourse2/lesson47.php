<br><br>
<font color=red>tinggi </font> (tall)<br>
<font color=red>pendek </font>  (short)<br>
The above pair of opposites is used to talk about the height of people eg.
<br><font color=red>Emaknya pendek tetapi bapanya tinggi</font>.
<br>(His/Her mother is short but his/her father is tall).
<br><br>
<font color=red>tinggi </font> (high)<br>
<font color=red>rendah </font>  (low)<br>
The above pair of opposites is used to describe buildings, trees, hills, tables, chairs etc.  Example:
<br>
<font color=red>Adakah Gunung Kinabalu lebih tinggi daripada Gunung Everest?  Tidak, Gunung Kinabalu adalah lebih rendah daripada Gunung Everest.</font>
<br>(Is Mount Kinabalu higher than Mount Everest?  No, Mount Kinabalu is lower than Mount Everest).<br><br>
