<h1 span style="display: inline;color:yellow;background-color:midnightblue;font-family:arial;font-size:30px;">&nbsp;Introduction to the Malay Language&nbsp;</h1><br>
<br>
<div style="font-family:arial;font-size:1em;">Hi, welcome!
<br>Bahasa Malaysia is Malaysia's national language and was formerly known as Bahasa Melayu (Malay language)*.  It is not only spoken in Malaysia but is also widely spoken in Indonesia, Brunei and Singapore. Some people say that Bahasa Malaysia is an easy language.  To a certain extent it is, but the truth is there is NO easy language in the world.  Everything is relative so when people say that Malay is an easy language what they really mean is that it is easy WHEN COMPARED to studying a language like English, French or Mandarin, for example.  <br>Thus if you might need to spend at least 300 hours studying English, French or Mandarin before you are able to use it in a simple conversation, you need only spend say, 100 hours studying Malay before you are able to do so.   This is because in Malay there is no past tense or past participles of verbs to study as in English, the verbs are not conjugated as in French and you don't have to worry about getting the tones right in order to be understood as you have to in Mandarin.
<br />But there is still a core vocabulary to be learnt - for colours, common adjectives, common verbs, names of objects around you, relationships, greetings, numbers, days of the week, telling the time, etc. etc.  Not to mention about an aspect of Bahasa Malaysia that is particularly difficult for foreigners to master, namely the use of prefixes and suffixes in verbs.
<br />So it is still 100 hours of solid study and hard work!  And by 100 hours I don't mean 4 days and 4 hours of non-stop studying (if this is possible at all)! It's more likely to be an hour's concentrated study every day over a period of three and a half months.
<br />So don't expect me to lead you up the garden path by telling you how easy the Malay language is.  But if you are determined there is no reason why you should not be able to pick it up on your own by following this course.  Good luck!  - pgoh
<br /><br><em>Grateful thanks to Michelle Nor Ismat, a native speaker, for volunteering to give a second reading to the course lessons.</em>
