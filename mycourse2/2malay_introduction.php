*The language was known as "Bahasa Malaysia" with the creation of Malaysia in 1963, having been decreed as the national language of Malaysia.  In 1986 it was replaced by the term "Bahasa Melayu" (Malay language).  However in 2007 Malaysia's Parliament decided that as it is Malaysia's national language, it should be called "Bahasa Malaysia" and this is the name that is used for the language today.</span>
