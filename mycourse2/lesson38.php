<br><p style="max-width:800px;font-family:tahoma;font-size:16px;">
<font color=red>Tolong</font> &nbsp;&nbsp;<img src=please.gif>&nbsp;&nbsp;is the word to use when you are asking a service or help from someone.<br>
Thus "Can you help me?" would be <font color=red>Bolehkah anda tolong saya?</font> or if you want to speak like the locals do, that is, colloquially, just say <font color=red>Boleh tolong sikit?</font> (literally Can you help a bit?)
<br>The usual answer is simply <font color=red>Boleh</font> (= Can) or if you feel like impressing your interlocutor answer with <font color=red>Memang boleh.</font> (Of course I can help you!)  Make sure you pronounce it as <em>may-mang</em> (remember that the <em>e</em> vowel has got two pronunciations in Malay, the other more common one being er).
<br /><u>Note:</u>  The usual answer to requests beginning with the word <font color=red>Tolong</font>, for example, <font color=red>Tolong jawab telefon</font> (Please answer the phone) is:  <font color=red>Baiklah.</font> (= okay)<br>
When used alone (usually as an exclamation) <font color=red>Tolong!</font> means Help!  Thus if you shout out loudly <font color=red>TOLONG!</font> you are asking the people around you for help as you are  in trouble (as when you are being forcibly robbed or attacked but certainly not when you are drowning!)  In this last case it is likely to be the bystanders who will shout out <font color=red>TOLONG!</font> on your behalf!
<br>
Much easier though is to ask someone to close the window:  <font color=red>Tolong tutup tingkap</font>.  You can also say: <font color=red>Tolong tutup jendela</font>  as both <font color=red>tingkap</font> and <font color=red>jendela</font> mean "window".  But as <font color=red>jendela</font> is the word that is more frequently used in Indonesia than <font color=red>tingkap</font> you might as well stick to this word.
<br>
An expression meaning "If you don't mind, please..." which is perhaps not so easy to learn but which is sure to impress people with your politeness (and your Malay!) when you use it, is: <font color=red>Kalau tidak keberatan, tolong...</font> followed by whatever favour you want the person to whom you are saying this to do eg.  if you wish somebody to explain something to you all over again you can say: <font color=red>Kalau tidak keberatan</font> <i>(a little pause)</i> <font color=red>tolong terangkan kepada saya sekali lagi.</font> (= If you don't mind, please explain it to me again).
<br />The word <font color=red>keberatan</font> (= unwillingness, objection) comes from the word <font color=red>berat</font> which means "heavy" as in <font color=red>Barang ini berat</font> (This thing is heavy).

