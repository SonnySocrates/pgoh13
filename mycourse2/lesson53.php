<p>The word <font color=red>pernah </font> is used to indicate that you have already been to some place or have already done some action as the following examples will show:<br>

It is also used in questions eg. "Have you ever been to London?" is <font color=red>Anda pernah ke London?</font>
<br>Note that besides <font color=red>jiran</font>, the word <font color=red>tetangga</font> is equally used for "neighbour", both in Malaysia as well as in Indonesia.
<br>In the sentence <font color=red>Saya pernah melawat ke Paris</font> English speakers might not often think of putting the preposition <font color=red>ke</font> (meaning "to") if it's translated as "I have already visited Paris".  Don't worry too much about this and just say <font color=red>Saya pernah melawat Paris</font>.  The preposition however comes naturally to a local speaker.  But if you really want to use the preposition then try to think of its English translation as "I have already been TO Paris."
<br><br>
<img class="img-responsive" src=forthose_knowmore.jpg>
<br>
As the above statements  indicate that something happened at a particular time in the past  it is usually followed by questions of place and time as the following examples will show: <br>
<font color=red>Saya pernah berjumpa dia.</font> (I have already met him.)<br>
<font color=red>Bila itu? </font> (When was that?) <br>
<font color=red>Sudah 10 tahun.</font> (10 years ago). <br>
<font color=red>Di mana?</font>  (Where?) <br>
<font color=red>Di rumah kawan saya.</font> (At a friend's house). <p>
<font color=red>Saya pernah melawat ke Paris.</font> or <font color=red>Saya pernah ke Paris.</font>  (I have already been to Paris.)<br>
<font color=red>Bila itu? </font> (When was that?) <br>
<font color=red>Pada tahun lalu.</font> (Last year.) <br>
<font color=red>Dengan siapa?</font>  (With whom?) <br>
<font color=red>Seorang diri.</font>  (Alone).<p>
What if you wish to say that you have never done something or never been to a place?
<br>To be able to say this all you have to do is to add <font color=red> belum</font> or <font color=red> tidak</font> before <font color=red>pernah</font>.  Thus: <br>
<font color=red>Saya belum pernah ke Amerika Syarikat. </font> (I have never been to the United States).  Incidentally the word <font color=red>syarikat</font> alone means a "company" (a business company). <br>
<font color=red>Dia belum pernah melihat salji. </font> (He has never seen snow). <br>
<font color=red>Saya tidak pernah buat kerja ini. </font>  (I have never done this type of work before). <br>
Note that<font color=red> tidak pernah</font> is often contracted to<font color=red> tak pernah</font> in its spoken form.  Use these two words also when you answer a question with the word "Never".
<br><br>

<table class="table" style="background-color:beige"><tr><td style="padding:10px">I didn't realize it could be confusing for a learner to choose between <font color=red>sudah</font> and <font color=red>pernah</font> until I received this email from Jean Kagi (reproduced with permission):
<br /><br />
<i>Dear Mr P. Goh,
<br />
I'm enjoying studying the Malaysian Language with your online course, 
but I have just one problem. I can't understand or "feel" the subtle 
difference  between the use of  <font color=red>sudah</font> and <font color=red>pernah</font>. I think I have it 
worked out but then I read a new sentence and I'm confused again! Even 
trying through French and German, I can't work it out.  I don't like to 
bother you, but I would appreciate it if  you could tell me either that 
it really is difficult or  if there is some obvious explanation that I 
just can't find.</i>
<br /><br />
As the problem could be shared by others I've decided to publish my reply here:
<br />
<i>Hi,
<br />
Thanks for your email and sorry that I can only get back to you now.
<br />
Ok let's put it this way.  Both <font color=red>sudah</font> and <font color=red>pernah</font> indicate that you have done 
an action.
<br />
If it is something that you do often eg. eat, bathe, sleep, telephone a 
friend, send a letter, etc then use <font color=red>sudah</font>.  In such cases you just cannot 
use <font color=red>pernah</font>.
<br />
But if it is something that perhaps you do only once in your lifetime eg. go 
to a far distant country, meet the president of your country, talk to 
someone ten years ago though he doesn't remember it, went dancing with 
your film idol (how lucky can you get!), studied a foreign language and then gave 
it up, been up in a spaceship, been neighbours with someone (but not 
anymore), been a teacher once (but not anymore), etc then use <font color=red>pernah</font>.  I 
hope you get the drift.
<br />
Also when you use <font color=red>pernah</font> it is not for something that happened yesterday but 
it is for something that took place in some distant past.  If you are thinking in English try to think on the lines of "I have done (whatever action) BEFORE" to justify using <font color=red>pernah</font>.  The action is not something that happened recently (yesterday or last week, for example) but a certain amount of time (a year or several years) has lapsed since that action happened.
<br />
However if it is still confusing I would suggest you forget about <font color=red>pernah</font> completely and just 
use <font color=red>sudah</font> under all circumstances.  You cannot go wrong there.  And if you 
hear someone use <font color=red>pernah</font> you know you can substitute it with <font color=red>sudah</font>.  Don't 
crack your head over this.  It's not such a big issue after all to use one 
or the other correctly - especially for a foreign learner.
<br>Besides in sentences such as <font color=red>Saya pernah melawat ke Paris</font> even the locals would say <font color=red>Saya sudah melawat ke Paris</font>.
</i></td></tr></table>

