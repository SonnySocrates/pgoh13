<H1 style="color:yellow;background-color:midnightblue">Lesson 64  Signboard language</H1>


<p style="font-family:arial;font-size:18px">In this lesson I will familiarize you with some of the Malay terms used on signboards everywhere in Malaysia.

<table align=right width=289><tr><td valign=top><img src=signs_inLRT.jpg width=289></td></tr><tr><td><font size=4 color=darkblue>A signboard in the Kelana Jaya LRT station</font></td></tr></table>

<font size=4><font color="red">Keluar</font> = Exit
<br><font color="red">Masuk</font> = Entrance
<br><font color="red">Awas</font> = Warning/Attention
<br><font color="red">Tolak</font> = Push
<br><font color="red">Tarik</font> = Pull
<br><font color="red">Bahaya</font> = Danger
<br><font color="red">Tandas</font> = Toilet
<br><font color="red">Berhati-hati dengan penyeluk saku</font> = Beware of pickpockets.
<br><font color="red">Sila beratur</font> = Please queue up.
<br><font color="red">Jangan jadi diri anda mangsa ragut</font> = Don't let yourself fall victim to snatch thieves.
<br>Sign in a railway station:
<br><font color="red">Pusat simpan bagasi</font> = Left luggage centre
<br>Sign on entering bus:
<br><font color="red">Sila sedia tambang tepat.  Baki tidak akan dipulangkan.</font> = Please prepare the exact fare.  No change will be given.
<br>Sign in an LRT counter:
<br><font color="red">Kad perjalanan bulanan</font> = Monthly travel card
<br>Sign on highway (remember it's "left hand drive" in Malaysia):
<br><font color="red">Ikut kiri kecuali memotong</font> = Keep to the left unless overtaking.
<br>Signboard in the Kelana Jaya LRT station (photo on right):
<br><font color="red">Dilarang merokok</font> = Smoking prohibited.
<br><font color="red">Dilarang makan atau minum</font> = Eating or drinking prohibited.
<br><font color="red">Dilarang membuangkan sampah</font> = Littering prohibited.
<br><font color="red">Dilarang melekatkan gula-gula getah</font> = Sticking of chewing gum prohibited.
<br><font color="red">Dilarang menampal pelekat</font> = Pasting of posters and bills prohibited.
<br><font color="red">Dilarang barangan merbahaya</font> = Hazardous material prohibited.
<br><font color="red">Dilarang membawa binatang</font> = Animals prohibited.
<br><font color="red">Dilarang basikal</font> = Bicycle prohibited.
<br><font color="red">Dilarang membawa kargo</font> = Commercial cargo prohibited.
<br><font color="red">Dilarang bermain muzik</font> = Playing music prohibited.
<br><font color="red">Dilarang meminta sedekah</font> = Begging prohibited.
<br><font color="red">Dilarang lepak</font> = Loitering prohibited.
<br><font color="red">Dilarang masuk</font> = No entry.




