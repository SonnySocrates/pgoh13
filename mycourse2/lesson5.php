<h3>For those who want to know more:</h3>
<p>Note that the adjective always comes after the noun in Malay.  Thus "telephone <u>number</u>" is translated as  <font color=red><u>nombor</u> telefon. </font><br>Once you are clear about this it is easy to understand why "your <u>telephone number</u>" becomes <font color=red><u>nombor telefon </u>anda. </font>
<br>Learn the Malay words for 1 to 9 to perfection as you only need to know five more words (<font color=red>belas, puluh, ratus, ribu, juta </font>) to be able to read any number at all in Malay.   More of this later (in Lessons 8 and 22).

<br>The Malay word for "zero" is <font color=red>kosong</font>.  Thus <font color=red>Kami menang (dengan) tiga kosong</font> means "We won 3-0". The telephone number 041695827 is read as <font color=red>kosong empat satu enam sembilan lima lapan dua tujuh</font>.
<br>In this connection a useful sentence to learn (yes, it's always good to read out the telephone number a second time to avoid any misunderstanding) is:
<br><font color=red>Saya akan baca sekali lagi</font>.  (I will read it again).
<br>Or <font color=red>Bolehkah anda baca nombor itu sekali lagi?</font>  (Will you read that number again?).
<br>Note that while <font color=red>kosong</font> is often used for games results and for telephone numbers, you might also hear the word <font color=red>sifar</font> for "zero" instead. Thus <font color=red>Walaupun kecil, risikonya bukan sifar, jadi berhati-hati</font> can be translated as "Even if it is unlikely, there is no zero risque, so be careful" i.e. its occurrence cannot be completely ruled out.

<br>Could I introduce ordinal numbers at this stage?
<br />In English we would say "first, second, third, fourth, fifth, sixth, seventh, eighth, ninth."
<br>In Malay it's <font color="red">yang pertama, yang kedua, yang ketiga, yang keempat, yang kelima, yang keenam, yang ketujuh, yang kelapan</font> and <font color="red">yang kesembilan</font>.
<br>What do you notice?  Yes, I'm sure you would have noticed that all of them (except for "first") start with <font color="red">yang ke</font> followed by the number in question.  Quite simple, isn't it?
<br>Okay, let's see it in practice:
<br>You know that Fatimah has got 3 brothers and 4 sisters and you want to know where she is situated.  You would ask her <font color="red">Fatimah anak yang keberapa</font>?  (Yes, it is quite common in Malay to say the name of the person you are speaking to instead of saying "you").
<br>And if she is the sixth of the eight siblings she would reply <font color="red">Saya anak yang keenam.</font>
<br>You will now be able to say "for the first time, for the second time, for the third time," etc.
<br>But first let me introduce the word <font color="red">untuk</font> which simply is the Malay word for "for".  Another new word is <font color="red">kali</font> which means "time" in the sense of "number of times".
<br>So "for the first time" is <font color="red">untuk pertama kalinya</font>
<br>"For the second time" is <font color="red">untuk kedua kalinya</font>
<br>"For the third time" is <font color="red">untuk ketiga kalinya</font>
<br>As to the question "How many times?" (<font color="red">Berapa kali?</font>) the answer is straightforward as in English: <font color="red">satu, dua, tiga,</font> etc.

