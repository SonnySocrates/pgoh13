<center><img src="nasi_lemak.jpg"></center>

About the most popular Malaysian dish is called <span class=yellow>nasi lemak</span> (photo above), a humble yet appetizing dish (on the spicy side for most foreigners though) comprising rice cooked in coconut milk and served with almost anything (basic items being anchovies, fried peanuts, cucumber slices, chicken, hard-boiled egg and chili paste).
<br>Then there is <span class=yellow>char koay teow</span> (photo below), a dish that all Malaysians (and especially Penangites) can never have enough of at any time of the day or night.   You can find the <a href=http://rasamalaysia.com/char-kuey-teow/ target=_blank>Char koay teow recipe here.</a>  By the way all the mouth-watering photos on this page come from the malaysatayhut.com website.
<center><img src="charkoayteow.jpg"></center>

<br>
Other popular Malaysian food dishes are: <span class=yellow>rojak, satay (sat&eacute;), laksa, roti canai, mi goreng, bihun, popiah...</span> Sorry, I can't go on.  I'm getting <span class=yellow>lapar</span> already, are you? (I'm introducing this word only in Lesson 33 but I think you can guess its meaning here).  Need a clue?  It starts with the letter "h"!
<br>My God, there is also the <span class=yellow>rendang daging</span> or beef rendang.  How could I have forgotten this dish?  It's simply out of this world.  The beef will simply melt in your mouth, if that is possible.  But watch out, it can be a bit spicy on your palate!
<br><br>
<center><img src="beef_rendang.jpg"></center>
Another favourite dish of Malaysians, taken at all times of the day and night, is called <span class=yellow>roti canai</span>.  
<br><center><table><tr><td><img src=roti_canai.jpg width="700">
</td></tr></table></center>
<br>Find out for yourselves what all the above dishes taste like when you are in Malaysia!

<br>In fact while the watchword of many tourists in Malaysia is "Shop till you drop" the watchword of Malaysians is, without doubt, "Eat till you drop." No wonder someone has said that eating is one of the main hobbies of Malaysians!
<br>By the way food is such an important aspect of Malaysian life that instead of the traditional <font color=red>Apa kabar?</font> form of greeting Malaysians would instead ask <font color=red>Sudah makankah? </font> (Have you eaten?) or <font color=red>Sudah makan belum? </font>
<br>And if your answer is <font color=red>Belum</font> (= Not yet) please don't expect to be invited to lunch (for all you know your interlocutor might already have had his).  It is just a conversation starter and will give you an opportunity to talk about the wonderful restaurant you just went to or the amount of work you have that prevented you from going out for lunch!

<br>It's time now for me to introduce the Malay word for vegetables, which is <span class=yellow>sayur</span>.  "Oh, another word to learn" do I hear you protesting?  Protest not, as this word will probably save the life of your vegetarian friend!
<br>Another important word to learn is rice, the staple food of Malaysians.  If you want plain white rice in a restaurant ask for <span class=yellow>nasi putih</span> (<span class=yellow>putih</span> means "white" remember?) while fried rice is <span class=yellow>nasi goreng</span>.  Yes, you guessed rightly.  The word <span class=yellow>goreng</span> means "fried".  Thus "fried fish" would be <span class=yellow>ikan goreng</span> and "fried chicken" <span class=yellow>ayam goreng</span> (the adjective always comes AFTER the noun, remember?)
<br>Please note that there are two distinct words for cooked and uncooked rice in Malay.  Rice that is already cooked (as in all the examples above) is <span class=yellow>nasi</span> while uncooked rice that you buy from the supermarket is called <span class=yellow>beras</span>. So if you wish to buy 3 kilos of rice to stock for one month you will have to say <span class=yellow>Saya hendak beli tiga kilo BERAS</span>, and not <span class=yellow>nasi</span>, ok?
<br>
While on the subject of food you'll probably need to use these two adjectives at one time or another in your eating experience:
<br>(i) <span class=yellow>sedap</span> which means delicious.  In fact if you wish to praise your hostess's cooking you can add the suffix <span class=yellow>nya</span> to it.  Thus after putting some food into your mouth and tasting it you can say <span class=yellow>Ah, sedapnya&nbsp;&nbsp;<img src=sedap.gif></span> making the <span class=yellow>nya</span> drag on for a full second or two!  You can be sure  that this will please your hostess no end!  I know, earlier on you have learnt that <span class=yellow>nya</span> is tagged on to a noun to indicate possession (<span class=yellow>bukunya</span> means his or her book).  But <span class=yellow>sedapnya</span> here could be translated as "Isn't it delicious!"  Another word for delicious is <span class=yellow>enak</span> (it's pronounced as ay-nak).  So each time you want to say that a dish is yummy just say <span class=yellow>sedap</span> or <span class=yellow>enak</span>!  Or if you want to surprise everybody in the room, use the slang word that only locals (Malaysians or Singaporeans) use, and that is "shiok".   <span class=yellow>Ah shiok sekalilah!</span> (translated freely as "This is heavenly, man!" or "It is simply out of this world!")   Coming from a foreigner you can be sure that this will bring about no end of laughter!
<br>(ii) The second word you are likely to use when eating is <span class=yellow>pedas</span> which means "spicy".  I hope you will not have to use this word (though I doubt it very much) seeing that quite often even a local would find the curry a bit too spicy for his palate!  So if you are able to say to your host and table companions <span class=yellow>Tidak, tidak pedas</span> then I'll have to take my hat off to you.  But don't say it just to please your host and then suffer in silence for the whole night with a burnt tongue!  Because you are likely to be served the same curry the next time you are invited to his house!  So if I were you I would just say <span class=yellow>Sedap sekali tetapi pedas sedikit untuk saya.</span>  (It's yummy but a tad spicy for me).  You can be sure you'll be invited again for being so appreciative of the food but this time you'll be served with a watered-down version of the curry.
<br>In fact there is not one curry but different types of curry.  The Malays, Chinese and Indians all have their own versions of curry.  To make matters worse some States also have their own brand of curry!
<br>By the way you might have heard of the expression <span class=yellow>bahasa rojak</span>.  It simply means incorrect Malay or Malay mixed with other languages.  This term comes from a popular Malaysian dish called <span class=yellow>rojak</span>, a mixture of raw fruits and vegetables in a hot sauce.
<br>
<H2><span style="background-color:midnightblue;text-align:left;"><font color=yellow>&nbsp;&nbsp;POPULAR MALAYSIAN FRUITS&nbsp;&nbsp;</font></span>&nbsp;</H2>
<br>
Now we come to the names of fruits (called <span class=yellow>buah</span> or <span class=yellow>buah-buahan</span> in Malay).  Typical Malaysian fruits (the first two have no equivalent name in English) are: <span class=yellow>durian, rambutan</span> and <span class=yellow>manggis</span> (mangosteen).
<br>Ah, durian.  The word is enough to make me take the first plane back to Malaysia for it.  And yet foreigners often complain about its "stinking" smell.  Actually if you don't like it it's simply because you're not a Malaysian, that's all.  And as a foreigner you can be forgiven for not liking it.  After all not many Malaysians appreciate cheese as much as you do.  The first of the photos below is that of the durian, also known as the "king of Malaysian fruits" that, understandably, is banned from 4 and 5-star hotels even in Malaysia because foreigners complain of its "stinking" smell.
