There is also a yellow, grape-sized tropical fruit you find everywhere in Malaysia when it's in season called <span class=yellow>langsat</span>, with varieties called <span class=yellow>duku, dokong</span> and <span class=yellow>duku langsat</span>.  I particularly like "duku langsat" as when fully ripe it is really heavenly to the palate and one could continue eating one after another without stopping!  Like grapes too they come in bunches.  Not only humans love them, bats love them too - yes, that nocturnal flying mammal with wings that are a constant nightmare to "langsat" farmers.

