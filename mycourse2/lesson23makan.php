<br><u>Note:</u>The three main meals of the day all start with the word <span class=yellow>makan</span> meaning "to eat" (what else?) followed by the time of the day when it is taken i.e. <span class=yellow>pagi</span> (morning), <span class=yellow>tengah hari</span> (noon) and <span class=yellow>malam</span> (night).  Thus breakfast is <span class=yellow>makan pagi</span>, lunch is <span class=yellow>makan tengah hari</span> and dinner is <span class=yellow>makan malam</span>.  Just think of the literal translation in English ("eat in the morning", "eat in the afternoon", "eat at night" and you won't go wrong).  Did I hear you say "Elementary, my dear Watson"?  You're right.  Nothing can be easier than this!
<br><span class=yellow>lewat</span> (the first syllable is pronounced as lay-) means late (as to be late for an appointment) so <span class=yellow>lewat malam</span> means "late at night" and accordingly supper, which is taken late at night, would be <span class=yellow>makan lewat malam</span>.
<br>If you are used to saying "Bon app&eacute;tit" as the French do I doubt if you will  find an equivalent expression in Malay as Malaysians, being epicureans (and who can blame them, with the rich diversity of appetizing dishes that they have), normally waste no time in preliminaries when it comes to eating!  You might just hear <span class=yellow>Jemput makan</span> (Please eat) or  <span class=yellow>Silakan</span> (Please help yourself)  or simply <span class=yellow>Makan, makan</span> (Eat, Eat!) just before the "opening ceremony", that's all.  And if I were you I would not keep my host waiting, because if you are not in a hurry to eat, he is (i.e. unless he is a Malaysian diplomat, in which case he has been trained to be patient even though he is craving to eat!)
<br>However if you really must find something equivalent for "Bon app&eacute;tit" I guess you can say <span class=yellow>Selamat makan</span> since we always start any wish at all with the word <span class=yellow>Selamat</span> so you can't go wrong there.  But if you want to impress your host (or cause a lot of laughter), you can always say <span class=yellow>Selamat menjamu selera</span>  (I can already hear your Malaysian hosts exclaiming "Wow, where did you learn your Malay from?")   Well, if you want to give me some publicity, this is the occasion!  Actually  <span class=yellow>menjamu</span> means "to serve" while <span class=yellow>selera</span> (pronounced as sir-lay-ra) means "appetite".
<br>You have already learnt that you go to a <span class=yellow>kedai kopi</span> for a drink.  Where then do you go for food?  Well, "restaurant" has its counterpart in Malay (<span class=yellow>restoran</span>) or you can also say <span class=yellow>kedai makanan</span> (though you only use this for a very simple restaurant) - the word <span class=yellow>makanan</span> meaning food in general.  Thus:
<br><span class=yellow>Saya belanja lima ratus ringgit tiap bulan untuk makanan.</span> (I spend 500 ringgit a month on food).
<hr>
<font color=midnightblue>
*There are two words for "We" in Bahasa Malaysia, the "we inclusive" (i.e. the person to whom you are addressing is included) and the "we exclusive" (i.e. the person to whom you are addressing is not included).   When a Malaysian says <span class=yellow>Kami makan nasi tiap-tiap hari </span>(We eat rice every day) he is implying that the person to whom he is addressing (probably an European) is not included in what he says.  If a Malaysian addresses another Malaysian (all Malaysians eat rice every day!) he will say <span class=yellow>Kita makan nasi tiap-tiap hari</span> (We eat rice every day).


