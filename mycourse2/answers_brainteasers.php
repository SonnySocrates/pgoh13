<h2>3 men in a motel (the missing dollar)</h2>
<p style="max-width:800px">
This brainteaser is an example of a false problem.  You cannot add the 27$ to the 2$ as the 2$ that is now with the bellboy is actually part of the 27$ they finally paid for the room! <br>
So there is no question of 1$ being "missing"!<br>
In fact you have to add the 27$ to the 3$ that the men got back.  Although the 3 men paid 27$ they didn't give that money to the hotelkeeper.  The fact is that the  2$ extra that they paid is with the bellboy.<br>
So the original 30$ is distributed  as follows:<br>
hotelkeeper 25$, bellboy 2$, 3 men 3$.<br>
Beware of ostensible reasoning!</p>
