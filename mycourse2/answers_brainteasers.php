<h2>Mensa brainteasers</h2>
<FONT face=tahoma color=black size=3>
<br><strong>Number 1:</strong>
<br>The answer is 5.60&pound;.
<br>Explanation:
<br>17 shirts at 8&pound; each = 136&pound;
<br>17 shirts plus 85 shirts equals 102 shirts
<br>102 shirts at 6&pound; = 612&pound;
<br>612&pound; - 136&pound; = 476&pound;
<br>So the 85 shirts were sold for 476&pound;
<br>Which means that the sale price of each shirt was 476/85 = 5.60&pound;
<br><br>
<strong>Number 2:</strong>
<br>The answer is 2.4km
<br>Explanation:
<br>Let's say x km is the answer (x representing the unknown)
<br>x km times 60 mins divided by 40km minus 2 mins = x km times 60 mins divided by 90km
<br>So 1.5x minus 2 = 2x divided by 3
<br>2x = 3 times (1.5x minus 2)
<br>2x = 4.5x minus 6
<br>2x minus 4.5x = minus 6
<br>-2.5x = -6
<br>2.5x = 6
<br>Therefore x = 2.4km
<br><br>
<strong>Number 3:</strong>
<p style="max-width:800px">The answer is 40mph
<br>Explanation:
<br>Let's say the distance is x miles (x representing the unknown)
<br>Total time for the two trips (in hours) is x/50 plus x/33.33 which is the same as 2x/100 plus 3x/100 which will give you 5x/100 or x/20 hours
<br>Since the total distance for the two trips is 2x miles, the average speed for the entire journey (to and fro) is 2x divided by x/20 which will give you 40x/x equals 40mph.
<br>The answer is 40mph</p>
