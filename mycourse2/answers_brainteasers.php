<h2>Riddle of the 68 camels</h2>
<p style="max-width:800px">
Mathematically, and on the surface, there is no way one can divide the 68 camels in the way the father wanted.  What the lawyer did was to "lend" 4 of his own camels to the 68 making 72 camels in all.  Now half of this is 36, one-third is 24 and one-ninth is 8 making a total of the father's 68 camels.  So he gave 36 of these to the eldest, 24 to the second and 8 to the youngest.  He then takes his 4 camels back home with him!  The sons won't complain neither for each of them would have got more than what he was really entitled to!
<br />I have since come across another such puzzle.  Here it is not 68 camels but 17 camels that have to be divided in the same proportions as above.  You should be able to solve this easily now.
<br />And with the same logic you'll be able to resolve Ali Baba's enigma of dividing his 39 camels among four sons so that the eldest got half, the second a quarter, the third an eighth and the youngest a 10th. In this story it was not a lawyer but a stranger who came riding along on his camel that helped them solve their problem before riding off and leaving them completely stunned.
<br />Hey, there is a mathematical explanation behind all this.  But I'll leave this to your maths teacher!</p>
