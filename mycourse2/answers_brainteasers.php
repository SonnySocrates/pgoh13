<strong>Number 4:</strong>
<p style="max-width:800px">The answer is 50 miles
<br>Explanation:
<br>Let's say the whole distance to be travelled is x miles - which means he has travelled only (x - 10) miles since there is still 10 miles to be travelled
<br>1st hour: Distance travelled in the lst hour is x/3 while distance remaining is 2x/3 miles
<br>2nd hour: Distance travelled in the 2nd hour is 2x/9 while distance remaining is 4x/9 miles
<br>3rd hour: Distance travelled in the 3rd hour is x/9 while distance remaining is x/3 miles
<br>4th hour: Distance travelled in the 4th hour is x/6 while distance remaining is also x/6 miles
<br>This means that x/6 = 10 and therefore x = 60
<br>So the total distance is 60 miles and the car has travelled 50 miles.
<br>To check if our answer is correct, let's substitute x with 60 in the above assumptions:
<br>1st hour: Distance travelled in the lst hour is x/3 = 20 miles
<br>2nd hour: Distance travelled in the 2nd hour is 2x/9 = 13 1/3 miles
<br>3rd hour: Distance travelled in the 3rd hour is x/9 = 6 2/3 miles
<br>4th hour: Distance travelled in the 4th hour is x/6 = 10 miles
<br>Total: 50 miles</p>
