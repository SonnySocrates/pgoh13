<br><br><p style="max-width:800px;font-family:tahoma;font-size:16px;">
I don't know if the word "clerk" is very widespread in your country (I sometimes see "office workers" being used instead but in Malaysia if you ask people what type of jobs they hold you'll often be told <font color=red>Saya kerani</font> (I'm a clerk) so learn the word <font color=red>kerani</font> well (at least know what it means when you hear it).  Does that word ring a bell?  Do you think you have come across that word already?  In that case the chances are that you're confusing it with the word <font color=red>kerana</font> which means "because" and which you have already learnt in Lesson 25.
<br>While we're at it I guess there's no harm bringing up two more words quite similar so you won't ever mix them up.  One is <font color=red>kereta</font> meaning "a car" and the other <font color=red>kelabu</font> meaning "grey" in colour.
<br>
And in case you have an enemy and want to make it known you might need to learn this sentence!
<br><font color=red>Dia bukan kawan saya.  Dia musuh saya.</font> (He is not my friend.  He is my enemy).
<br>There is a second word for enemy (starting with s*) but I don't think you have that many enemies to need to learn another word for it!
<br><br>
Further examples with <font color=red>siapa</font>:<br>(When answering the telephone) <font color=red>Helo, Peter di sini.  Siapa itu? </font> (Hello, Peter here.  Who's calling?)<br>
<font color=red>Siapa orang lelaki itu?  - Dia guru bahasa saya. </font> (Who is that man?  - He is my language teacher.)<br>
<font color=red>Siapa orang perempuan itu?  - Dia isteri saya. </font> (Who is that woman?  -  She is my wife.)
<br />In all the above examples I have used <font color=red>Siapa</font> at the beginning of the sentence.  However it can also appear at the end, as in the following sentences:
<br /><font color=red>Anda cari siapa?</font>  (Who are you looking for?)
<br /><font color=red>Saya cari kawan saya.</font>  (I'm looking for my friend.)
<br /><font color=red>cari</font> or <font color=red>mencari</font> means to search or to look for someone or something eg.
<br /><font color=red>Dia sedang mencari kunci keretanya.</font>  (=He is searching for his car keys.)
<br />Incidentally (as you will learn in Lesson 49) the pronunciation of the syllable "ca" in <font color=red>cari</font> is not ka but cha as in cha-cha-cha so the word is pronounced as cha-ri.
<br /><font color=red>Ini untuk siapa?</font>  (Who is this for?)
<br /><font color=red>Ini untuk bapa saya.</font>  (This is for my father.)
</p>


<h1><span style="background-color:midnightblue;">&nbsp;&nbsp;Family Relationships&nbsp;&nbsp;</font></h1>
<br><br>
<p style="max-width:800px;font-family:tahoma;font-size:16px;">I think this is a good place to bring in the Malay words for the most important members of the family.
Here is the core vocabulary for family members (some of which you have already come across but I'm adding them here to make the list complete)
<br>One thing to note though.  Apart from the linguistic aspect, there is also the cultural aspect to be kept in mind when you are talking about your siblings in Malay.  This is because while in English we can just say "He is my brother" or "She is my sister" and leave it at that (that is, without revealing if he or she is older or younger than yourself), this is simply not possible in Malay where you have to specify each time if it is your younger or elder brother or sister you are talking about.  The only possible case for ambiguity is in the case of a younger sibling where you can leave out the gender eg.  <font color=red>Adik saya tinggal di New York</font> will leave the listener in doubt as to whether you are talking about a younger brother or a younger sister.  (To make it clear you will have to say <font color=red>adik lelaki</font> for a younger brother or <font color=red>adik perempuan</font> for a younger sister).  This will not be necessary though, if the person is  with you, as the situation will show if it's your younger brother or sister so in such a case you can just say <font color=red>Ini adik saya</font>.
<br>Another thing to note is that just as Malaysians are fond of using the word "uncle" or "auntie" when addressing a man or woman who is older than you (out of politeness), <font color=red>adik</font> can also be used to address a child, even if he is unrelated to you.  A woman could also address her husband lovingly as <font color=red>abang</font>.  In such a case it doesn't mean that he is her elder brother at all!  So let's look at the list of the most common family members below:
<br><br>father = <font color=red>bapa</font> or <font color=red>ayah</font>
<br>mother = <font color=red>emak</font> or <font color=red>ibu</font>
<br>daughter = <font color=red>anak perempuan</font>
<br>son = <font color=red>anak lelaki</font>
<br>brother (younger) = <font color=red>adik lelaki</font>
<br>brother (elder) = <font color=red>abang</font>
<br>sister (younger) = <font color=red>adik perempuan</font>
<br>sister (elder) = <font color=red>kakak</font> (Note that in Indonesia <font color=red>kakak</font> can mean not only "elder sister" but also "elder brother").
<br>husband = <font color=red>suami</font>
<br>wife = <font color=red>isteri</font>
<br>aunt = <font color=red>emak saudara</font> (spoken form: <font color=red>mak cik</font>)
<br>uncle = <font color=red>bapa saudara</font> (spoken form: <font color=red>pak cik</font>)
<br>grandfather = <font color=red>datuk**</font>
<br>grandmother = <font color=red>nenek</font>
<br>grandson = <font color=red>cucu lelaki</font>
<br>granddaughter = <font color=red>cucu perempuan</font>
<br>nephew = <font color=red>anak saudara lelaki</font>
<br>niece = <font color=red>anak saudara perempuan</font>
<br>cousin = <font color=red>saudara sepupu</font>
<br>father-in-law = <font color=red>bapa mertua</font>
<br>mother-in-law = <font color=red>ibu mertua</font>
<br>son-in-law = <font color=red>menantu lelaki</font>
<br>daughter-in-law = <font color=red>menantu perempuan</font>
<br>brother-in-law (older than you) = <font color=red>abang ipar</font>
<br>brother-in-law (younger) = <font color=red>adik ipar lelaki</font>
<br>sister-in-law (older) = <font color=red>kakak ipar</font>
<br>sister-in-law (younger) = <font color=red>adik ipar perempuan</font>
<br>step-mother = <font color=red>ibu tiri</font>
<br>step-father = <font color=red>bapa tiri</font>
<br>adopted child = <font color=red>anak angkat</font>
<br><br><p style="max-width:800px;font-family:tahoma;font-size:16px;">Quite a handful eh?  The best way to study this category of vocabulary is to write the names of your family members on a piece of paper and then their relationships to you eg.
<br><font color=red>Tom adalah adik lelaki saya</font> (replace "adik lelaki" with abang or bapa saudara or suami, etc.)
<br><font color=red>Mary adalah adik perempuan saya</font> (replace "adik perempuan" with kakak or emak saudara or isteri, etc.)
<br>So whenever you are asked <font color=red>Siapa dia?</font> (= Who is he/she?) you will have your answer ready.
<br>Besides this is a much more meaningful way of learning such words than if you were to learn them by rote off a list.

<br><br>* The word is <font color=red>seteru</font>.
<br>**Please note that Datuk (with a capital letter) is a title awarded to prominent Malaysians by the Sultans or King on their birthdays.  It is usually followed by the person's name though you normally use the word by itself without the name when you are addressing the person directly.  But if you are a nondescript like me, you can still "earn" that title by becoming a grandfather!
</p>
