<p style="max-width:750px">The Malay language has a very simple structure when you want to ask somebody if he has done something or not eg.
Have you eaten? Have you bathed? Have you paid?
<br>You simply start the question with the word <font color=red>sudah</font> followed by the relevant verb (eg. eat, bathe, pay) and finishing the question with <font color=red>belum</font> (meaning "not yet").
<br>Thus:
<br>Have you eaten? = <font color=red>Sudah makan belum?</font>
<br>Have you bathed? = <font color=red>Sudah mandi belum?</font> 
<br>Have you paid? = <font color=red>Sudah bayar belum?</font> 


<br><br>
<p style="max-width:750px">Please note that "I have not eaten" cannot be translated as  <font color=red>Saya tidak sudah makan.</font> If you wish to say the opposite of  <font color=red>sudah</font> you have to replace    <font color=red>sudah</font> with  <font color=red>belum</font> (= "not yet") which has already been dealt with in Lesson 13.<br>
A word that is often used in the place of  <font color=red>sudah</font> is  <font color=red>telah</font>.  However   <font color=red>telah</font> is mainly used in the written form, not in the spoken.
<br>Yet another word to indicate an action that took place in the past is   <font color=red>pernah </font> which will be explained in Lesson 53.</p>

