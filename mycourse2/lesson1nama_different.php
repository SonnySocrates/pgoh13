<p>The first thing you will notice is the absence of the verb "to be" in Malay in the above sentences.
<br />In fact <span style="color:yellow;background-color:black;padding:0 4px;">the verb "to be" is simply not needed here</span>.
<br />A sentence without a verb?  Yes, it's possible in Malay.
<br />Those who had to struggle with the conjugations of the verb "to be" in French or Spanish will be greatly relieved to hear this!  &nbsp;(Having said that, people who feel lost without putting in a verb in a sentence can note that the word <b>adalah </b> is sometimes used for the verb "is".  Thus <b>Dia guru saya </b> and <b>Dia adalah guru saya </b> both mean "He/She is my teacher".)
<br />Another thing you will notice is that while in English we have the pronoun before the noun (eg. my name, your name) in Malay it is just the opposite ie. <span style="color:yellow;background-color:black;padding:0 4px;">the noun comes first then the pronoun</span> (so in Malay we say "name my", "name your").  If you remember this it will serve you throughout this course as it is the same word order when it comes to adjectives and nouns (so "big car" becomes "car big" in Malay).

<br />The third important thing to remember is something that you'd better get right from the very beginning and that is <span style="color:yellow;background-color:black;padding:0 4px;">the same pronoun "dia" is used for BOTH "he" and "she".</span>  Only the context will tell whether you're talking about a man or a woman.</p>

<h2><span style="background-color:midnightblue;">For those who want to know more:</span></h2>

<p>Please note that the colloquial forms (<b>Apa nama anda? </b> and  <b>Apa namanya? </b>) are used in this lesson.
<br />The formal forms would be <b>Siapa nama anda? </b> and  <b>Siapa namanya? </b> <br />The formal forms do not make much sense though because <b>Siapa </b> actually means "Who" as in <b>Siapa dia?</b> = "Who is he (or she)?" while <b>Apa </b> normally means "What" as in <b>Apa ini? </b> (What is this?)
<br />You will notice that the suffix <b>nya </b> is tagged on to the noun coming after "his" or "her".  But watch out, <b>namanya </b> can mean "his name" as well as "her name" (depending on the context). 
<br />
So far you have learnt the Malay pronouns for I, you, he and she.
<br />
The Malay word for the pronoun "they" or "them" is <b>mereka</b>.  To help you remember I am going to bring up three common names: Murphy, Raymond and Kazan, the famous Hollywood film director.  What is so special about these three people, you might ask.  Well, if you remember them, they're not only going to help you remember the Malay word for "they" or "them" but also help you to pronounce the word correctly.  How is this possible?  Well just pronounce the FIRST syllable of each of the three names and you will get  <b>mereka</b> as it should be pronounced (Mur-Ray-Ka).  I hope this little mnemonic will be of help to you.  At least it will show you that the e vowel has got two different sounds in Malay (see Lesson 50), one the schwa sound as in Mur (the phonetic symbol for this sound being &#x0259;) and the other the ay sound as in Ray (if you know French, it's the sound of &eacute;). 

