<br><br>
<font color=red>Tidak apa</font> (or <font color=red>tidak mengapa</font>) means "It doesn't matter".  It is quite often shortened to <font color=red>Tak apa</font> in its spoken form.
<br><font color=red>Tidak ada</font> (not to have) is often abridged to<font color=red> tiada</font> eg. <font color=red>Dia tiada wang.</font>= He has no money.<br>
<u>Further examples:</u><br>
 <font color=red>Dia  tidak sakit.</font>  (He is not sick.)<br>
 <font color=red>Sepuluh ringgit sahaja?  - Tidak mahal! </font>  (Only 10 ringgit?  It's not expensive!)<br>
Another way of saying this is <font color=red>Hanya sepuluh ringgit?</font> (Literally "Only 10 ringgit?")<br>
   <font color=red>Pejabat saya tidak jauh dari sini.</font>  (My office is not far from here.)<br>
 <font color=red>Saya tidak dengar berita itu.	</font>  (I didn't hear that piece of news.)<br>
 <font color=red>Saya tidak kenal dia.</font>  (I don't know him.)<br>
 <img src="tidak_bagus.gif">&nbsp;<font color=red>Ini tidak bagus.</font>  (This is not good.)<br>
<font color=red>Cerita itu tidak benar.</font>  ( That story is not true.)<br>
<font color=red>Itu tidak betul.</font>  ( That is not true.)<br>
<FONT face=tahoma color=black size=3> <u>Note:</u> There is often some confusion for beginners over the use of the word <font color=red>tidak</font> and <font color=red>bukan</font>, both of which mean "not".  Generally, when "not" is used with a noun or pronoun (eg. He is not my friend or It was not him)  <font color=red>bukan</font> and not  <font color=red>tidak</font> is used while <font color=red> tidak</font> is normally used with verbs and adjectives (except for adjectives of colour which take <font color=red>bukan</font>) .  As I do not wish to confuse the learner by introducing both  <font color=red>tidak</font> (more in Lesson 48) and  <font color=red>bukan</font> at the same time I have waited till Lesson 51 to show how  <font color=red>bukan</font> is used.  
