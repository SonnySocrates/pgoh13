<p style="max-width:750px;font-family:tahoma;font-size:16px;">1. The word <font color=red>yang</font> is also widely used in forming the superlative eg. <font color=red>Dia yang tua sekali</font> (He is the oldest).  You will learn about this in Lesson 58.  In fact there is another way of saying this and that is <font color=red>Dia yang tertua</font>.  In this case the prefix ter- turns an adjective into the superlative.
<br>2. Do you see the difference between <font color=red>yang mana</font> and <font color=red>di mana</font>?  The first means "which one (of several)" as dealt with in this lesson while <font color=red>di mana</font>, which you have already learnt, means "where" eg. <font color=red>Di mana pen saya?</font> (=Where is my pen?)

