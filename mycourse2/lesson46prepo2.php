<font face=arial color="darkblue"><strong style="font-size:large;">Mnemonics:</strong>
<br>
<FONT face=tahoma color=black size=3>If you are confused whether to use <font color=red>ke</font> or <font color=red>kepada</font> and <font color=red>dari</font> or <font color=red>daripada</font> perhaps it might help if you remember that just as the word "place" has fewer letters than "people", similarly in Malay the shorter words (<font color=red>ke, dari</font>) are used for "place" and the longer (<font color=red>kepada, daripada</font>) are used for "people".<br>

Other prepositions that are used with people eg. <font color=red>untuk</font> (for) and <font color=red>dengan</font> (with) have been dealt with in Lessons 39 and 41 respectively. </font></font>
<br><br>
<font face=arial color="darkblue"><strong style="font-size:large;">More sentences:</strong></font>
<br><br><FONT face=tahoma color=black size=3> <font color=red>Anda hantar e-mel kepada siapa?
</font>	(To whom are you sending the e-mail?)
<br>You can also use the word <font color=red>kirim</font> instead of <font color=red>hantar</font> for "sending" a letter or email.
<br><font color=red>Saya hantar e-mel kepada emak saya.</font>  I am sending it to my mother.
<br>You can also use the word <font color=red>ibu</font> instead of <font color=red>emak</font> for "mother".
