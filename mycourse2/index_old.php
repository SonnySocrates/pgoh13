<h2 style="background-color:midnightblue;color:yellow;font-family:arial;font-size:30px;display:inline-block;">What this site is all about&nbsp;&nbsp;</h2>
<br><FONT face=tahoma color=black size=3><em>The World At Your Fingertips</em> is a site that strives to make your surfing life easier.  It does this by attempting to harness all internet resources together in one place for easy access for my personal use and also to share them with anyone who might find it useful.  You will also find the Google Search box at the top of every page waiting for your search query.  This is <strong>not</strong> a customized search that earns revenue and is limited in its scope but gives exactly the same results as the official Google site (Web, Images, Map, News, etc.)   Besides, if you are not from an English-speaking country, you will get the search results in English here, not results from the local version of Google in another language which you are likely to get even if you should go to the google.com website.  This is because Google detects which country you are from and delivers the results in your country's national language (if it is not English).</em>
<br>The following are the highlights of the site:
<br>&nbsp;&nbsp;&nbsp;&nbsp;1.  Its homepage has a link to the top 5 newsfeeds from the BBC, CNN, New York Times International Edition, The Telegraph UK, The Australian and Toronto Star, Canada.  At any time of the day or night you will never miss out if something big happens.   And the green menu bar at the top takes you straight to your email, Facebook, Skype, PayPal or other accounts with just one click.

  You will be able to find lots of computer tips and a list of indispensable software programs to help you get the best out of your computer.  There are also step-by-step instructions on how to use some popular software such as TeamViewer, Partition Wizard, PrintEdit, ManageFlitter, Cobian Backup, RssReader, PIXLR, Spybot, Paragon Backup & Recovery or Hola! to access blocked websites.

<br>&nbsp;&nbsp;&nbsp;&nbsp;2.  Practical information of any kind that is often needed by anyone in whichever part of the world he might be can be found here.   Do you need to know the time at this hour in some foreign country?  Or its weather conditions?   Do you need to know the equivalent of some foreign currency in your own money?   Do you need to know the exact location of the hotel that you are about to book?  Or the equivalent of centimeters in inches?  Or maybe you need to translate instructions into a foreign language or understand what some foreign instructions mean?  These, and more, can be accessed from the "INFO" options in the top menu bar (dropdown menu) or the vertical menu on the left column of every page.
<br />&nbsp;&nbsp;&nbsp;&nbsp;3.   There is also "Today's song" where a song from the current Top 10 of the U.S., U.K., Australian and Canadian charts is featured daily.  And you are likely to find your favourite song together with the lyrics in <a href=http://pgoh13.com/mobile/ target=_blank>Songs of Today, Songs of Yesterday</a>, which is created with smartphone users in mind.  There are 1,100 popular English songs in this collection.  Then there are the standard French songs by the likes of Serge Gainsbourg, Edith Piaf, Juliette Greco and Yves Montand that are timeless and have found their way to even the remotest parts of the world.  You will be able to listen to them in  <a href="http://pgoh13.com/m.frenchsongsandlyrics.php" rel="nofollow" class="otherlinks" target=_blank>The Greatest French Songs Of All Time</a>.  The lyrics are provided so you can easily practise singing them and improve your French at the same time!

