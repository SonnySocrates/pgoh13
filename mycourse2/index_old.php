<br />&nbsp;&nbsp;&nbsp;&nbsp;5.  There is also a full course covering all aspects of learning Pinyin (the romanized version of Mandarin).  This unorthodox course is really like no other in that a few grammatical rules which have always been a bugbear to foreign learners of Mandarin have been "bent" in order to make the study of the Chinese language easier - without sacrificing much in terms of making yourself understood.  It starts from the known to the unknown by giving equivalent English sounds for pinyin spellings such as xi,  zh ou q.  It is indeed possible to speak Chinese Mandarin without knowing a single Chinese character (I know, more than one prospective student have been put off simply at the sight of these intimidating Chinese characters!)  <a href="http://pinyin.pgoh13.com/chinese_introduction.php" rel="nofollow" title="Learning Pinyin" class="otherlinks">More....</a>



