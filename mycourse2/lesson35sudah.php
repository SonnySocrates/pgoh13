<br><br>
Please note that "I have not eaten" cannot be translated as  <font color=red>Saya tidak sudah makan.</font> If you wish to say the opposite of  <font color=red>sudah</font> you have to replace    <font color=red>sudah</font> with  <font color=red>belum</font> (not yet) which is already dealt with in Lesson 13.<br>
A word that is often used in the place of  <font color=red>sudah</font> is  <font color=red>telah</font>.  However   <font color=red>telah</font> is mainly used in the written form, not in the spoken.
<br>Yet another word to indicate an action that took place in the past is   <font color=red>pernah </font> which will be explained in Lesson 53.
