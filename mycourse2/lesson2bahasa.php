<p style="max-width:800px;font-family:tahoma;font-size:16px;">
The Malaysian language (Bahasa Malaysia) is actually the Malay language (Bahasa Melayu).  As such the two terms are interchangeable.
<br>In Lesson 1 <font color=red>saya</font> was used as a possessive (<font color=red>nama saya</font> = "my name" and <font color=red>buku saya</font> = "my book").  Here it is used as a pronoun.   Another widely-used word for "I" is <font color=red>aku</font> (perhaps more used in Indonesia than <font color=red>saya</font>).
<br>Oh, by the way while in English and many other languages we have different forms for I (such as <em>me, my, mine</em>) in Malay there is just one form: <font color=red>saya</font>.  Cool!
<br><font color=red>Dia </font>can mean he or she depending on the context.  Thus:
<br><font color=red>Dia kawan saya.</font>  (= He is my friend - if the person referred to is a man)
<br><font color=red>Dia kawan saya.</font>  (= She is my friend - if the person referred to is a woman)
<br><font color=red>Dia marah.</font>  (= "He is angry" or "She is angry" depending on the context)
<br>The two words in Malay to distinguish between the two sexes are:
<br><font color=red>lelaki</font> for "male" and
<br><font color=red>perempuan</font> for "female".
<br>Thus to avoid any ambiguity in the above sentences you could say:
<br><font color=red>Orang lelaki itu kawan saya.</font>  (= That man is my friend) or
<br><font color=red>Orang perempuan itu kawan saya.</font>  (= That woman is my friend)
<br>By the way while "male" and "female" are also used to describe animals in English <font color=red>lelaki</font> and <font color=red>perempuan</font> should never be used for animals in Malay.
<br>The male of an animal is described as <font color=red>jantan</font> and the female as <font color=red>betina</font>.
<br>Thus a cock is <font color=red>ayam jantan</font> and a hen is <font color=red>ayam betina</font>.
<br>In written form <font color=red> Dia</font> is often replaced by <font color=red>Ia </font> which is also used to indicate objects and animals (equivalent of "it").  
<br>By the way newspapers often use <font color=red>beliau</font> for "he" or "she" (but this use is restricted to an elderly or respectable person and is not used for a criminal).
<br><font color=red>nya</font> is tagged on to a noun to mean "his" or "her" eg.<font color=red> isterinya</font> (his wife),<font color=red> suaminya </font>(her husband) or <font color=red>anaknya</font> (his child or her child - depending on the context).   Similarly <font color=red>kawannya</font> can mean "his friend" or "her friend" and <font color=red>keretanya</font> can mean "his car" or "her car", again depending on the context.
</p>

