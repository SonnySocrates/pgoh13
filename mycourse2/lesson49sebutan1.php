<br>However if the Malay pronunciation is relatively phonetic in nature there are two sounds (<font color=red>ng</font> and <font color=red>ny</font>) which are a different kettle of fish altogether.  For many of you these sounds are not found in your native languages so be prepared for some hard phonetical exercises (try to get a Malay friend to correct you).  For a few other sounds (like <font color=red>c</font> and <font color=red>sy</font>), it's just a question of finding their equivalences.  Thus if you speak English just remember that <font color=red>c</font> is pronounced like ch (as in "chair") and <font color=red>sy</font> is pronounced like sh (as in "shut") and you will be all right.  Come to think of it the words "fashion", "tuition" and "fiction" are used in Malay too with the same pronunciation but spelt <font color=red>fesyen, tuisyen</font> and <font color=red>fiksyen</font>.  That should help you a bit with getting the <font color=red>sy</font> sound right! 
