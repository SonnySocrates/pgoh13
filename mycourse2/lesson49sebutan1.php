The Malaysian government imposed <font color=red>bahasa baku </font> (standard pronunciation of Malay) some years ago but met with tough resistance and had to discard it due to lack of popular support (for example, it's almost impossible to make Malays in certain states pronounce <font color=red>apa</font> as "apah" and not as "apeu" as they have been used to pronouncing for generations.)
<br><br>
<table style="background-color:beige;border:1px solid black;padding:10px;"><tr><td width="80%">SOME OBSERVATIONS:   Apart from the sounds of particular letters the following points should also be noted:
<br />
1.  As I have mentioned in the very first lesson the final "a" in words like <font color=red>apa, pada, ada, nama, siapa, saya, mana</font>, etc. is often pronounced as "er" (as in the final vowel in "butter") in quite a number of states.  There was an effort by the Malaysian government to stardardize Malay pronunciation (through <font color=red>bahasa baku</font>) but since old habits die hard this system was eventually scrapped.  In this course for foreigners I have pronounced the final "a" as "ah" which is also what is being taught in schools.
<br />
2. In words like <font color=red>masuk, umur, tidur, telur, dapur, buruk, cukup, datuk, gemuk, untuk, duduk, puluh, penuh, campur, tunjuk</font>, etc the "u" in the second vowel has not got the sound of "oo" as in <font color=red>buka</font> or <font color=red>baju</font> but has rather the sound of "oh" (or the <font color=red>oa</font> in "c<font color=red>oa</font>t").  In fact before the common spelling system with Indonesia was implemented the "u" in the second syllable of the above words was replaced with an "o" (which is closer to the actual pronunciation).  Thus the old spelling for those words was: <font color=red> masok, umor, tidor, telor, dapor, burok, cukop,datok, gemok, untok, dudok, puloh, penoh, campor, tunjok</font>.
<br />
3.  In the same way the letter "i" in the second vowel of the following words do not have the "ee" sound: <font color=red>masih, pilih, tarik, balik, fasih, lebih, bilik</font>.  This again came about with the implementation of the common spelling with Indonesian.  In fact the pronunciation of the above words is closer to that of their previous spelling, namely, <font color=red>maseh, pileh, tarek, balek, faseh, lebeh, bilek</font>.
<br />
4. The letter "r" (unless when it is the first letter of a word) is hardly pronounced in Malaysia eg. <font color=red>kotor</font> is often pronounced like "koto" and <font color=red>telur</font> is often pronounced like "telo".  This is not the case in Indonesia - the Indonesians always roll their r's.</font></td></tr></table>

