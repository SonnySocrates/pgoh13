<H1><span style="background-color:midnightblue;">&nbsp;&nbsp;Lesson 49  <font color=red>Sebutan</font>  (Pronunciation - Part 1)&nbsp;&nbsp;</span></H1>

<center><a href="l49cemburu.wav"><img src=l49sebutan1.gif width=400 height=50></a></center><br>

<FONT face=tahoma color=black size=3>If you have been following the lessons till now you would have noticed that Malay pronunciation is not much different from English pronunciation (it's even closer to Spanish for the vowel sounds) and that the consonants do not pose any problem at all for English-speaking people except for the sound <font color=red>c</font> which needs some explaining.	<br>
When Malaysia and Indonesia agreed to standardize their spelling in 1972 Malaysia discarded its "ch" for "c" so <font color=red>chari</font> and <font color=red>chuchi</font> became <font color=red>cari</font> and <font color=red>cuci</font>.  Indonesia, in its turn, discarded its "tj" for "c" so <font color=red>tjari</font> and <font color=red>tjutji</font> became <font color=red>cari</font> and <font color=red>cuci</font>, "tj" being the older Dutch spelling of "ch" that Indonesia adopted from its Dutch masters and both Malaysia and Indonesia compromised on using just  <font color=red>c </font> for the "ch" sound.  Thus "c" has not at all the sound of "c" in English as in "catch" but is equivalent to the sound of "ch" in "cheese".   If you are wondering how the palate-enticing but "killing" curry so typical of Malaysian and Indonesian gastronomy is spelt just spell it phonetically, so <font color=red>kari</font>.  If it helps think of the "c" sound in Malay as the equivalent of the "c" sound in such Italian words as <em>arrivederci</em> or <em>cappuccino</em> (the 3rd syllable, not the first!) and you should be able to pronounce <font color=red>cuci</font> (to wash) correctly.  You will now understand why the word "sandwich", which is pronounced the same way in Malay, is spelt <font color=red>sandwic</font> (yes, without the h at the end since the "c" alone has the sound of "ch").
