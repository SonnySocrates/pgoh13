<font color=red>Tidak sengaja</font> = Not purposely, unintentionally
<br>Can you try to pronounce this very common word <font color=red>sengaja</font> (= purposely, intentionally) on your own?  How do you break up the syllables?
<br>The answer:  se-nga-ja.  Practise the "ng" sound above (as in <font color=red>bunga</font> meaning "flower") which is pronounced bu-nga.  I know it's going to be a real challenge.
<br>By the way Italians will be familiar with the word <font color=red>bunga</font> as the phrase "bunga bunga" is quite popular in Italy.  Excuse me for the digression.  If you are curious to know what that
means look it up in Wikipedia!
<br>Once you are able to pronounce the ng sound, try to learn this sentence off by heart as it could come in handy one day when you find yourself in an "explosive" situation!
<font color=red>Maafkan saya, itu tidak sengaja.</font> (= Please excuse me, that was not done on purpose.)
<br>If you need reminding <font color=red>maafkan</font> is pronounced as three syllables (ma-af-kan).
<br><br>
<u>The "h" sound:</u>
<br><FONT face=tahoma size=3>If you are used to a language where "h" is not pronounced (such as French) please note that "h" is almost always pronounced in Malay.  Thus <font color="red">bahu</font> meaning "shoulder" is pronounced "ba-hoo".  If you should pronounce it as "ba-oo" it would be a totally different word (the word <font color="red">bau</font> meaning "smell").  (The exceptions are found in words such as <font color="red">tahu</font> and <font color="red">mahu</font> which are also written as <font color="red">tau</font> and <font color="red">mau</font> and therefore can be pronounced without the "h" sound.)  
