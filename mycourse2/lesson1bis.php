<p style="max-width:800px;font-family:tahoma;font-size:16px;">When you are asked your name in a social context in Malaysia you don't have to worry too much about whether you should give your first name, your family name or both your first name and family name together.  Just give whatever you want though normally Malaysians give the name they would like friends to call them by and not the entire name. 
<br /><em>Apa nama anda?</em>    (literally "What is your name?", which is not wrong for most Malaysians and makes your learning so much easier, though your Malay teachers will probably tear me to pieces for this!)
<br>Ok, to be more correct (and to please your Malay teachers!) the correct form is:
<br><em>Siapa nama anda?</em>    (which means literally "Who is your name?")

<table><tr><td>
 <u><FONT face=tahoma color=black size=3>Note on the pronunciation used:</u>
<p style="max-width:800px;font-family:tahoma;font-size:16px;">Although in a number of states in Malaysia the final "a" in <font color=red>Apa,  nama </font> and <font color=red>saya </font> is pronounced er (as in the second vowel in "butter") and represented by the phonetic symbol &#x0259;, I am here pronouncing it exactly as the "a" in the first syllable (which has the sound of "ah").  This is in keeping with the requirements of <font color=red>bahasa baku </font> (standard Malay pronunciation) which, incidentally, has since been scrapped by the Malaysian government after its attempt at standardizing Malay pronunciation failed.  As they say habits die hard and the Malays in some states are so used to pronouncing the final "a" as "er" that nothing can make them change their pronunciation.
<br />In fact the learning of Bahasa Malaysia can be complicated by the fact that many states (Kelantan, Negeri Sembilan, Pinang, etc.) possess their own dialect or local accents.  This is called <font color=red>bahasa daerah</font> (or <font color=red>loghat</font>) in Bahasa Malaysia.
<br />By the way I prefer that you learn the Malay sounds bit by bit from each lesson instead of presenting you all the vowel and consonant sounds in the very first lesson.  On the whole, Malay pronunciation is not a problem for English language speakers.  There are a small number of difficult sounds though and these are summarized in two entire lessons (Lessons 49 and 50). 

<br><br>
If this lesson is not enough for you and you want to learn all about the intricacies of the Malay pronoun <a href="lesson1_malaypronoun.php">go here.</a>  There you will understand why I have to use <font color=red>anda</font> throughout the course for "you" when words like <font color=red>kamu</font> or <font color=red>awak</font> are normally used (of course if you know this please substitute <font color=red>kamu</font> or <font color=red>awak</font> for <font color=red>anda</font> each time).

