<p>I don't know if the word "clerk" is very widespread in your country (I sometimes see "office workers" being used instead but in Malaysia if you ask people what type of jobs they hold you'll often be told <span class=yellow>Saya kerani</span> (I'm a clerk) so learn the word <span class=yellow>kerani</span> well (at least know what it means when you hear it).  Does that word ring a bell?  Do you think you have come across that word already?  In that case the chances are that you're confusing it with the word <span class=yellow>kerana</span> which means "because" and which you have already learnt in Lesson 25.
<br>While we're at it I guess there's no harm bringing up two more words quite similar so you won't ever mix them up.  One is <span class=yellow>kereta</span> meaning "a car" and the other <span class=yellow>kelabu</span> meaning "grey" in colour.
<br>
And in case you have an enemy and want to make it known you might need to learn this sentence!
<br><span class=yellow>Dia bukan kawan saya.  Dia musuh saya.</span> (He is not my friend.  He is my enemy).
<br>There is a second word for enemy (starting with s*) but I don't think you have that many enemies to need to learn another word for it!
<br><br>
Further examples with <span class=yellow>siapa</span>:<br>(When answering the telephone) <span class=yellow> Helo, Peter di sini.  Siapa itu? </span> (Hello, Peter here.  Who's calling?)<br>
<span class=yellow>Siapa orang lelaki itu?  - Dia guru bahasa saya. </span> (Who is that man?  - He is my language teacher.)<br>
<span class=yellow>Siapa orang perempuan itu?  - Dia isteri saya. </span> (Who is that woman?  -  She is my wife.)
<br />In all the above examples I have used <span class=yellow>Siapa</span> at the beginning of the sentence.  However it can also appear at the end, as in the following sentences:
<br /><span class=yellow>Anda cari siapa?</span>  (Who are you looking for?)
<br /><span class=yellow>Saya cari kawan saya.</span>  (I'm looking for my friend.)
<br /><span class=yellow>cari</span> or <span class=yellow>mencari</span> means to search or to look for someone or something eg.
<br /><span class=yellow>Dia sedang mencari kunci keretanya.</span>  (=He is searching for his car keys.)
<br />Incidentally (as you will learn in Lesson 49) the pronunciation of the syllable "ca" in <span class=yellow>cari</span> is not ka but cha as in cha-cha-cha so the word is pronounced as cha-ri.
<br /><span class=yellow>Ini untuk siapa?</span>  (Who is this for?)
<br /><span class=yellow>Ini untuk bapa saya.</span>  (This is for my father.)
</p>


<h1><span style="background-color:midnightblue;">&nbsp;&nbsp;Family Relationships&nbsp;&nbsp;</span></h1>
<br>
<p>
<font face=tahoma size=3>I think this is a good place to bring in the Malay words for the most important members of the family.
Here is the core vocabulary for family members (some of which you have already come across but I'm adding them here to make the list complete)
<br>One thing to note though.  Apart from the linguistic aspect, there is also the cultural aspect to be kept in mind when you are talking about your siblings in Malay.  This is because while in English we can just say "He is my brother" or "She is my sister" and leave it at that (that is, without revealing if he or she is older or younger than yourself), this is simply not possible in Malay where you have to specify each time if it is your younger or elder brother or sister you are talking about.  The only possible case for ambiguity is in the case of a younger sibling where you can leave out the gender eg.  <span class=yellow>Adik saya tinggal di New York</span> will leave the listener in doubt as to whether you are talking about a younger brother or a younger sister.  (To make it clear you will have to say <span class=yellow>adik lelaki</span> for a younger brother or <span class=yellow>adik perempuan</span> for a younger sister).  This will not be necessary though, if the person is  with you, as the situation will show if it's your younger brother or sister so in such a case you can just say <font color=red>Ini adik saya</font>.
<br>Another thing to note is that just as Malaysians are fond of using the word "uncle" or "auntie" when addressing a man or woman who is older than you (out of politeness), <span class=yellow>adik</span> can also be used to address a child, even if he is unrelated to you.  A woman could also address her husband lovingly as <span class=yellow>abang</span>.  In such a case it doesn't mean that he is her elder brother at all!  So let's look at the list of the most common family members below:<br>
<br>father = <span class=yellow>bapa</span> or <span class=yellow>ayah</span>
<br>mother = <span class=yellow>emak</span> or <span class=yellow>ibu</span>
<br>daughter = <span class=yellow>anak perempuan</span>
<br>son = <span class=yellow>anak lelaki</span>
<br>brother (younger) = <span class=yellow>adik lelaki</span>
<br>brother (elder) = <span class=yellow>abang</span>
<br>sister (younger) = <span class=yellow>adik perempuan</span>
<br>sister (elder) = <span class=yellow>kakak</span> (Note that in Indonesia <span class=yellow>kakak</span> can mean not only "elder sister" but also "elder brother").
<br>husband = <span class=yellow>suami</span>
<br>wife = <span class=yellow>isteri</span>
<br>aunt = <span class=yellow>emak saudara</span> (spoken form: <span class=yellow>mak cik</span>)
<br>uncle = <span class=yellow>bapa saudara</span> (spoken form: <span class=yellow>pak cik</span>)
<br>grandfather = <span class=yellow>datuk**</span>
<br>grandmother = <span class=yellow>nenek</span>
<br>grandson = <span class=yellow>cucu lelaki</span>
<br>granddaughter = <span class=yellow>cucu perempuan</span>
<br>nephew = <span class=yellow>anak saudara lelaki</span>
<br>niece = <span class=yellow>anak saudara perempuan</span>
<br>cousin = <span class=yellow>saudara sepupu</span>
<br>father-in-law = <span class=yellow>bapa mertua</span>
<br>mother-in-law = <span class=yellow>ibu mertua</span>
<br>son-in-law = <span class=yellow>menantu lelaki</span>
<br>daughter-in-law = <span class=yellow>menantu perempuan</span>
<br>brother-in-law (older than you) = <span class=yellow>abang ipar</span>
<br>brother-in-law (younger) = <span class=yellow>adik ipar lelaki</span>
<br>sister-in-law (older) = <span class=yellow>kakak ipar</span>
<br>sister-in-law (younger) = <span class=yellow>adik ipar perempuan</span>
<br>step-mother = <span class=yellow>ibu tiri</span>
<br>step-father = <span class=yellow>bapa tiri</span>
<br>adopted child = <span class=yellow>anak angkat</span>
<br><br>Quite a handful eh?  The best way to study this category of vocabulary is to write the names of your family members on a piece of paper and then their relationships to you eg.
<br><span class=yellow>Tom adalah adik lelaki saya</span> (replace "adik lelaki" with abang or bapa saudara or suami, etc.)
<br><span class=yellow>Mary adalah adik perempuan saya</span> (replace "adik perempuan" with kakak or emak saudara or isteri, etc.)
<br>So whenever you are asked <span class=yellow>Siapa dia?</span> (= Who is he/she?) you will have your answer ready.
<br>Besides this is a much more meaningful way of learning such words than if you were to learn them by rote off a list.

<br><br>* The word is <font color=red>seteru</font>.
<br>**Please note that Datuk (with a capital letter) is a title awarded to prominent Malaysians by the Sultans or King on their birthdays.  It is usually followed by the person's name though you normally use the word by itself without the name when you are addressing the person directly.  But if you are a nondescript like me, you can still "earn" that title by becoming a grandfather!

