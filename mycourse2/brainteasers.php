<strong>Cheryl's Birthday Puzzle</strong>
<p style="max-width:800px"><em>Another exercise in logical deduction and analytical thinking.  If you are an expert in Sudoku puzzles you will be familiar with the idea of using given clues to eliminate alternatives in order to get closer to the solution, which is quite the case here.</em>
<br />Albert and Bernard just became friends with Cheryl, and they want to know when her birthday is. Cheryl gives them a list of 10 possible dates:
<br>May 15 16 19
<br>June 17 18
<br>July 14 16
<br>August 14 15 17
<br>Cheryl then tells Albert and Bernard separately the month and the day of her birthday, respectively.
<br><em>Albert:</em> I don't know when Cheryl's birthday is, but I know that Bernard does not know, too.
<br><em>Bernard:</em> At first, I didn't know when Cheryl's birthday is, but I know now.
<br><em>Albert:</em> Then I also know when Cheryl's birthday is.
<br>When is Cheryl's birthday?
