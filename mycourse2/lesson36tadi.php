<br>More examples:
<br><span class=red>Dia marah dengan saya tadi kerana saya tidak tolong dia</span> = He was angry with me just now because I didn't help him.
<br>Please note that the colloquial form uses the word <span class=red>sama</span> instead of <span class=red>dengan</span>.
<br>So don't be surprised at all if you hear the above sentence as:
<br><span class=red>Dia marah sama saya tadi kerana saya tidak tolong dia</span>.
<br>In short if you want to ask someone if he (or she) is angry with you (or at you), you can either say:
<br><span class=red>Anda marah dengan sayakah?</span> or <span class=red>Anda marah sama sayakah?</span> but I think it's easier for English speakers to use <span class=red>dengan</span> as it translates literally into "with". 

<br><br>
<img src=forthose_knowmore.jpg>
<br><br>
Instead of <span class=red>tadi</span> (just now), you might sometimes hear <span class=red>sekejap tadi</span> which means "a short while ago".
<br>Please don't mix this up with <span class=red>sekejap lagi</span> which means "in a short while".
<br>Thus while <span class=red>tadi</span> or <span class=red>sekejap tadi</span> is used for the immediate past, <span class=red>sekejap lagi</span> is used for the immediate future.
<br>So if you should ask <span class=red>Bila hendak keluar?</span> (When are you leaving?) and you get the answer <span class=red>Sekejap lagi</span> you will know that the person you are talking to is leaving in a short while.
<br>When you visit a person at his house or at his office you might sometimes be told <span class=red>Tunggu sekejap</span>.  It means "Wait for a while" eg.
<br><span class=red>Dia akan datang sekejap lagi.  Tunggu sekejap.</span> (He will be coming in a short while. Wait a while.) or
<br><span class=red>Dia akan balik sekejap lagi.  Tunggulah sekejap.</span> (He will be back soon. Please wait a while).  You might also hear <span class=red>Dia akan pulang sekejap lagi</span> as <span class=red>pulang</span> and <span class=red>balik</span> are synonyms.
<br>The suffix <span class=red>lah</span> is often added to a word to "soften" what might appear to be a "command" word (in this case not "You wait here" but "Please wait here".)  The Malays are an easy-going and courteous race so you will hear the suffix <span class=red>lah</span> sprinkled all over the place (linguistically speaking of course!)
<br>Similarly <span class=red>Berhenti sekejap</span> means "Stop a while" eg. <span class=red>Saya penat.  Berhentilah sekejap</span> (I'm tired.  Please stop for a while.)  Again you can see how the suffix <span class=red>lah</span> is used here to soften what could otherwise be interpreted as a command.
<br>In the above sentence I have introduced another useful word to add to your Malay vocabulary.  The word is <span class=red>penat</span> and as you have seen, it means "tired".
<br>There's another word meaning the same as <span class=red>penat</span> and that is <span class=red>letih</span>.
<br>Thus you can also say:
<br><span class=red>Saya letih.  Boleh berehat sekejap?</span> (I'm tired.  Can we rest a while?)

<br>For practice try to get someone to ask you the following questions and answer them with <span class=red>tadi</span> or <span class=red>sekejap tadi</span>.  By the way <span class=red>tadi</span> rhymes with "barely" and "hardly", both of which can mean "only just" (if this can help you to remember the Malay word).  
<br><span class=red>Bila dia datang?</span>  (When did he come?)
<br><span class=red>Bila dia telefon?</span>  (When did he phone?)
<br><span class=red>Bila dia keluar?</span>  (When did he leave?)
<br>Or perhaps if your friend doesn't know Malay it's easier if it is you who ask the questions and your friend only has to reply <span class=red>tadi</span>!  &nbsp;Have fun!
