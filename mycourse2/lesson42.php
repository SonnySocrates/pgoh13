<br><p style="max-width:800px;font-family:tahoma;font-size:16px;">While <font color=red>hendak</font> is used to indicate a desire or wish to do something there is another little word that is used when one has to or is obliged to do something.  The word is <font color=red>kena</font>.  The following sentences will illustrate its use:
<br><font color=red>Saya kena pergi sendiri</font> (I have to go myself) implying that no one else can do it on my behalf.
<br><font color=red>Dia kena buat sendiri</font> (He has got to do it all by himself).
<br>The word <font color=red>kena</font> however is also used to indicate the passive voice eg.
<br><font color=red>Dia kena gigit anjing</font>.  (He was bitten by a dog.)
<br><font color=red>Dia kena pukul orang</font>.  (He was beaten up by some people.)
<br>In the case where <font color=red>kena</font> is used to denote the passive voice it is often replaced by <font color=red>di</font> joined directly to the verb.
<br>The above two sentences become:
<br><font color=red>Dia digigit anjing</font>. 
<br><font color=red>Dia dipukul orang</font>.
<br>As you can see from the above sentences <font color=red>di</font> is always joined to the verb to form a single word as it is not a preposition.
<br>Example of <font color=red>di</font> used as a preposition:  <font color=red>Dia ada DI kedai kasut.</font>  (he is IN the shoe shop.)
<br>More of this in Lesson 48.
<br><br>
The prefix  <font color=red>mem</font> is often affixed to verbs beginning with the letter "b"   so when you come across the word  <font color=red>membeli</font> you know it's  the same as <font color=red>beli</font> (more on prefixes and suffixes later).<br>
<font color=red>membeli-belah</font> means "shopping" as illustrated in the sentence <font color=red> Isterinya suka pergi membeli-belah</font>  (His wife likes to go shopping.) <br> A shopping complex or a shopping mall is:  <font color=red>pusat membeli-belah</font>.	<br>
And in case you don't know, mall-visiting for window-shopping or just to escape the hot air outside is a favorite pastime of Malaysians on weekends!
<br>
When going shopping,  you might need to use the following sentences:  <font color=red>Mahal!  Boleh kurang sedikit?</font> (It's expensive.  Can you lower the price a bit?)<br>
or <font color=red>Terlalu mahal!  Saya tak cukup wang.</font> (It's too expensive!  I don't have enough money)
<br>or you might hear the shopkeeper say <font color=red>Paling kurang seratus ringgit</font> = The lowest (price) is 100 ringgit.
<br>The word <font color=red>paling</font> is often used to indicate the superlative (more of this in Lesson 58).
<br>Note that<font color=red> tak cukup</font> is the short form of <font color=red>tidak cukup</font> and means "not sufficient".
<br>By the way <font color=red>kurang</font> is the opposite of <font color=red>lebih</font> (= more) and <font color=red>lebih kurang</font> is the Malay expression for "more or less".
<br>Another interesting use of the word <font color=red>kurang</font> is in the expression <font color=red>kurang ajar</font> which is the ultimate insult that you can hurl at a Malaysian (you are unlikely to find swear words in Malay in this course so note this expression preciously if you think you might need to use it one day!)  It simply means badly brought up, having no manners or being badly educated. 
<br><br>
<u>Further examples:</u><br>
<font color=red>Di manakah anda beli tali pinggang ini?</font>  (Where did you buy this belt?)<br>
<font color=red>- Di pasar malam. </font> (In the night market.)<br>
Other articles you might want to buy:  <font color=red>payung</font> (umbrella), <font color=red>kemeja</font> (shirt) and <font color=red>seluar panjang</font> (trousers).
<br>Talking about <font color=red>kemeja</font> I can't help singing the praise of the Malaysian <font color=red>batik</font> shirt.  Where else in the world can you have an audience with the King (if you are lucky enough to have one) or with the Prime Minister (always possible during the Hari Raya Open House although you might have to queue up for hours) wearing only this shirt (preferably with long sleeves) but with the shirt-tail hanging out and without wearing a tie!  It is perfectly respectable, believe it or not!  The long-sleeved batik shirt is meant to be worn without tucking in and without a tie.  Even the King wears it this way.
<br>Incidentally it is reported that Malaysia's future astronaut can wear batik clothes and eat <i>roti canai</i> but not durian.
<br>Unfortunately I have just read that the batik attire is not allowed if you wish to visit the Parliament Building.  The ruling stipulates that the dress code while in the Parliament Building is as follows: "Men – to wear the traditional Baju Melayu complete with songkok and sampin or long trousers, a long-sleeved shirt and a tie."  &nbsp;A footnote says that "Jeans, batik shirt and T-shirt are not allowed".  What a pity especially as it has been accepted everywhere else.  
<br>Next a word about <font color=red>kasut</font>.  Make sure you remove them before entering your Malay friends' house.  Even if they should tell you it's alright you should remove them if you see that they themselves are barefooted, in socks or in sandals.  By doing so you will be winning their respect and admiration.  In fact when I was in Port Dickson I was at a cybercafe that required its customers to remove their shoes before entering.  So be prepared to follow the local customs if you have to.
<br>Incidentally, we also talk about so many "pairs" of shoes in Bahasa Melayu.  Thus if you wish to say that you have three pairs of shoes you should say <font color=red>Saya ada tiga pasang kasut</font>.
<br>This leads on naturally to the word <font color=red>pakai</font> meaning "to wear".  The following sentences will illustrate how the word is used:
<br><font color=red>Dia selalu pakai kemeja putih dan tali leher ke pejabat.</font> = He always wears a white shirt and tie to office.
<br><font color=red>Saya pakai kasut baru untuk perkahwinan abang saya.</font> = I wear new shoes for my elder brother's wedding.
<br><font color=red>Dia sudah berkahwin tetapi dia tidak pakai cincin di jarinya.</font>  = He (or She) is already married but doesn't wear a ring on  his/her finger.
<br><font color=red>Jangan pakai seluar yang buruk itu.</font>  = Don't wear that old (worn) pair of trousers.
<br>Sometimes though <font color=red>pakai</font> is used not in the sense of "wear" but in the sense of "use" eg.
<br><font color=red>Kereta saya sudah rosak.  Saya pakai kereta isteri saya hari ini.</font>  = My car has broken down (is out of order).  I'm using my wife's car today.
<br>It can also be used in the sense of using a telephone.  Thus <font color=red>Bolehkah saya pakai telefon anda?</font> means "May I use your telephone?"
