<p style="max-width:800px;font-family:tahoma;font-size:16px;">MORE EXAMPLES:
<br><font color=red>Dia marah dengan saya tadi kerana saya tidak tolong dia</font> = He was angry with me just now because I didn't help him.
<br>Please note that the colloquial form uses the word <font color=red>sama</font> instead of <font color=red>dengan</font>.
<br>So don't be surprised at all if you hear the above sentence as:
<br><font color=red>Dia marah sama saya tadi kerana saya tidak tolong dia</font>.
<br>In short if you want to ask someone if he (or she) is angry with you (or at you), you can either say:
<br><font color=red>Anda marah dengan sayakah?</font> or <font color=red>Anda marah sama sayakah?</font> but I think it's easier for English speakers to use <font color=red>dengan</font> as it translates literally into "with". 

<br><br>
<img src=forthose_knowmore.jpg>
<br><br>
Instead of <font color=red>tadi</font> (just now), you might sometimes hear <font color=red>sekejap tadi</font> which means "a short while ago".
<br>Please don't mix this up with <font color=red>sekejap lagi</font> which means "in a short while".
<br>Thus while <font color=red>tadi</font> or <font color=red>sekejap tadi</font> is used for the immediate past, <font color=red>sekejap lagi</font> is used for the immediate future.
<br>So if you should ask <font color=red>Bila hendak keluar?</font> (When are you leaving?) and you get the answer <font color=red>Sekejap lagi</font> you will know that the person you are talking to is leaving in a short while.
<br>When you visit a person at his house or at his office you might sometimes be told <font color=red>Tunggu sekejap</font>.  It means "Wait for a while" eg.
<br><font color=red>Dia akan datang sekejap lagi.  Tunggu sekejap.</font> (He will be coming in a short while. Wait a while.) or
<br><font color=red>Dia akan balik sekejap lagi.  Tunggulah sekejap.</font> (He will be back soon. Please wait a while).  You might also hear <font color=red>Dia akan pulang sekejap lagi</font> as <font color=red>pulang</font> and <font color=red>balik</font> are synonyms.
<br>The suffix <font color=red>lah</font> is often added to a word to "soften" what might appear to be a "command" word (in this case not "You wait here" but "Please wait here".)  The Malays are an easy-going and courteous race so you will hear the suffix <font color=red>lah</font> sprinkled all over the place (linguistically speaking of course!)
<br>Similarly <font color=red>Berhenti sekejap</font> means "Stop a while" eg. <font color=red>Saya penat.  Berhentilah sekejap</font> (I'm tired.  Please stop for a while.)  Again you can see how the suffix <font color=red>lah</font> is used here to soften what could otherwise be interpreted as a command.
<br>In the above sentence I have introduced another useful word to add to your Malay vocabulary.  The word is <font color=red>penat</font> and as you have seen, it means "tired".
<br>There's another word meaning the same as <font color=red>penat</font> and that is <font color=red>letih</font>.
<br>Thus you can also say:
<br><font color=red>Saya letih.  Boleh berehat sekejap?</font> (I'm tired.  Can we rest a while?)

<br>For practice try to get someone to ask you the following questions and answer them with <font color=red>tadi</font> or <font color=red>sekejap tadi</font>.  By the way <font color=red>tadi</font> rhymes with "barely" and "hardly", both of which can mean "only just" (if this can help you to remember the Malay word).  
<br><font color=red>Bila dia datang?</font>  (When did he come?)
<br><font color=red>Bila dia telefon?</font>  (When did he phone?)
<br><font color=red>Bila dia keluar?</font>  (When did he leave?)
<br>Or perhaps if your friend doesn't know Malay it's easier if it is you who ask the questions and your friend only has to reply <font color=red>tadi</font>!  &nbsp;Have fun!

