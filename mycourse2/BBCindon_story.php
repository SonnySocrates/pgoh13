<p><FONT face=tahoma color=black size=3>Seorang
pria Kanada menawarkan tiket pesawat gratis ke Eropa, Amerika, dan
India kepada perempuan senegaranya, tapi dengan syarat perempuan
tersebut harus mempunyai nama yang sama dengan mantannya.
<br>Penawaran
tiket gratis bermula ketika Jordan Axani, 28, putus dengan
kekasihnya, Elizabeth Gallagher, pada Maret lalu.

