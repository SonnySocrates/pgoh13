<br>Padahal,
keduanya telah memesan tiket jalan-jalan ke Milan, Praha, Paris, New
York, dan Delhi pada Desember 2014 hingga Januari 2015.
<br>Adapun
tiket untuk mantan kekasihnya ingin dia berikan secara percuma.
Namun, sang penerima tiket harus bernama sama dengan mantannya guna
menghindari kerepotan administrasi.
<br>Axani
kemudian mengiklankan tiket itu dalam situs Reddit.
<br>&quot;Saya
butuh bantuan Anda,&quot; tulisnya. &quot;Jadi begini, pada bulan
Maret saya sudah memesan tiket perjalanan keliling dunia untuk
merayakan Natal bersama pacar saya.
<br>&quot;Namun,
ketika hubungan kami berakhir, saya masih punya rencana untuk
melakukan perjalanan, sedangkan dia tidak.
<br>&quot;Dan
karena saya benci melihat tiket untuk berkeliling dunia itu akan
sia-sia, saya mencari seorang perempuan Kanada bernama Elizabeth
Gallagher untuk bisa menggunakan tiket tersebut.&quot;
<br>Dia
mengatakan kepada BBC bahwa sejak dia mengiklankan tiket itu, dia
telah menerima puluhan email dari sejumlah wanita Kanada bernama
Elizabeth.
<br>Namun,
dia mengaku &quot;belum memilih satupun dari mereka&quot;.
<br>Axani
menggambarkan dirinya sebagai &quot;seorang pria benar-benar normal
berusia 28 tahun yang tinggal di Toronto&quot;. Lalu, dia &quot;tidak
mencari imbalan dalam bentuk apa pun&quot;.
<br>&quot;Saya
tidak mencari persahabatan, percintaan, narkotika, perdagangan, atau
mengambil sejumlah foto <I>selfie</I> 
dengan Anda di depan pasar Natal di Praha,&quot; tulis Axani.
<br>&quot;Harapan
saya adalah bahwa hal itu akan menjadi pengalaman hidup yang akan
mereka ceritakan kepada anak-anak suatu hari nanti,&quot; katanya.</p>

