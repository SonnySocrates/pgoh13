<br>And if you want more of Malaysian euphemisms (in English this time) instead of saying that you are going to "urinate" you can say that you are going to "put out the fire" while if you need to poop, you can  say you're going to the bank!  You know you are understood when the listener gives a chuckle instead of asking how much money you are going to withdraw!  And also if you are still in your pyjamas and not dressed up for your banker!
<br>Yet another example of euphemism in the Malay language is when you want to say that someone has died.  The Malay word for "die" is <span class=red>mati</span>.  But the Malays find it too harsh a word to use so instead of saying <span class=red>Dia sudah mati</span> (= He has died) they would often say <span class=red>Dia sudah meninggal dunia.</span> (= He has left the world).  By the way you might not be able to find the verb <span class=red>meninggal</span> in a Malay dictionary.  This is because it comes from the verb <span class=red>tinggal</span>.  Full explanation on the formation of Malay prefixes (a real nightmare for students!) in the <a href=appendix.php target=_blank>Appendix</a>.
<br>By the way the old spelling of <span class=red>air</span> (which in Malay does not mean the air that we breathe but the water that we drink!) was "ayer" which will give a better indication of its pronunciation (that is, as two syllables <em>ah-ear</em>).
<br>But enough of all this digressing - let's get back to our lesson!



<p><strong>More questions:</strong><br>
<span class=red>Di mana perhentian bas? </span> <font color=black>(Where is the bus-stop?)<br>
<span class=red>Di mana lapangan terbang? </span>  (Where is the airport?)<br>
<span class=red>Di mana stesen kereta api? </span>  (Where is the railway station?)<br>
Note that "station" is spelt <span class=red>stesen</span> and not <span class=red>stesyen</span> as it would normally be if pronounced the English way.  (More on this in <a href="lesson49sebutan1.php">Lesson 49</a>).

<p><strong>Cultural note:</strong><br>

When one approaches a stranger with a question one does not normally put the question abruptly to him.  It is customary for one to start with a preliminary question:  <span class=red>Boleh saya tumpang tanya?</span> meaning "Do you mind if I ask you a question?" or <span class=red>Boleh saya tumpang tanya sedikit?</span> (Can I ask you a bit?)<br>As a stranger it is better to get the go-ahead before you ask the real question though Malaysians might just take the answer for granted and start asking the real question immediately after eg. <span class=red>Boleh saya tumpang tanya, apa nama jalan ini?</span> meaning "Don't mind if I ask you the name of this road?"

<br>Anyway in my whole life I have never got "No" for an answer to this question (the preliminary question  I mean) as the Malays are without doubt a very friendly race (and all Malaysians too, for that matter).
<br>It's best to learn this expression by itself without worrying too much about the meaning of the word <span class=red>tumpang</span>.  But if you are really curious to know what it means I won't hide it from you. It is normally used in the following two cases:
<br>In the sense of making use of someone's facilities.  Thus if you are putting up at a friend's place you would say you  <span class=red>tumpang</span> at his place.  This is quite a common practice among Malaysians who when travelling usually put up at their friends' or their relatives' house instead of staying in  hotels.  It's not so much that they are trying to save money on hotel charges (the gifts that they bring along would have paid for the hotel room!) but it's just part of their cultural heritage, that's all (this is slowly changing with the times though).
<br>Sorry, I've digressed a bit.  To come back to our subject.  The word  <span class=red>tumpang</span> is also used when you are asking for a lift in a friend's car eg.  <span class=red>Boleh saya tumpang kereta anda ke bandar?</span> (Can I get a lift in your car to the city?)
<p><strong>Mnemonics</strong>:<br>
It's so easy to confuse the words for "left" and "right" more so when they are learnt at the same time. From the pedagogical point of view only one of these two words should be introduced in class until it sinks in, then only the other in order to avoid confusion.  Just imagine turning left when you are told to turn right just because you mixed up the two words.  Scary, isn't it?
<br>However since this is not so practical in an online course, if you need to make use of mnemonics to help you not to mix them up  I have one for you.  Look at the two words and their English equivalents.  The Malay word for "left" is <span class=red>kiri</span>  while the word for "right" is <span class=red>kanan</span>.  What do you notice?  Firstly, that both words in Malay start with the letter "k" (not much of a help - only adds to the confusion).  And secondly that whether it is in English or in Malay "left" has got four letters and "right" five.  Now you will never ever mix them up again, will you?
<br>A quick check before I say goodbye for today, is <span class=red>kiri</span> right or left?

