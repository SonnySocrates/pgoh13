Possible answers to the above questions:<br>
1.  <span class=red>Di belakang</span>  (At the back) or  <span class=red>Di tingkat atas</span> (On the top floor)<br>
2.  <span class=red>Di bawah meja</span> (Under the table)  or <span class=red> Dekat pintu</span> (Near the door)<br>
3.  <span class=red>Di sebelah kiri</span> (To the left)  or  <span class=red>Di sebelah kanan</span> (To the right)<br>
&nbsp;&nbsp;&nbsp;&nbsp;<span class=red>Di sebelah</span> here means "to the side of".<br>
4. <span class=red> Di hadapan anda</span> (In front  of you)   or <span class=red>Di sana</span> (Over there)<br>
5.  <span class=red>Di kedai kopi</span>  (In the coffee shop) or <span class=red>Di pejabat</span> (In the office)<br>
 6.   <span class=red>Di atas meja</span> (On the table) or <span class=red>Di dalam beg anda</span> (In your bag)<br>
But if you don't know the answer to any of the above questions you can simply say <span class=red>Saya tidak tahu</span> or you can surprise your friend by using a very "Malay" expression meaning the same thing: <span class=red>Entahlah</span>&nbsp; <img src="tidaktau.gif"> (<span class=red>entah</span> meaning "don't know").
<br>In case you have forgotten that the verb "to be" is often left out in Malay, you will be reminded that this is so from the questions above.  So in Malay you only need to ask "Where washroom?, Where my shoes?, Where Petaling Street?, Where he?, Where my key?"  Don't laugh - after all Malay is not English!
<br />While <span class=red>di</span> can mean "in" or "at" as in the sentence <span class=red>Dia ada di rumah</span> (He is at home) I would suggest that you study <span class=red>di mana</span> to mean "where" (although <span class=red>mana</span> alone also means "where").   If it can help you in any way <span class=red>di mana</span> literally means "in where".
<br />By the way you will notice that <span class=red>di</span> is not joined to the word <span class=red>mana</span> so as to become a single word.  In fact <span class=red>di</span> is only joined to verbs so that they become passive as you would have seen in earlier lessons (eg. <span class=red>Dia telah dipukul oleh dua orang samseng</span> means "He was beaten up by two gangsters").  When used with adverbs, however it is not joined to them to become a single word but is detached from them eg. <span class=red>di atas</span> (= above), <span class=red>di bawah</span> (= below), <span class=red>di sana</span> (= over there) and not <em>diatas, dibawah, disana</em>.  Not that it is that important, as some Malaysians themselves do not get it right, but good to know.
<br>
<strong>Note:</strong>
<br>
You might want to replace <span class=red>bilik air</span> with <span class=red>tandas</span> (= toilet) or with a much less exquisite word <span class=red>jamban</span>.  Any of these three words would be understood in Malaysia for the washroom or toilet though in some inward areas of the country <span class=red>jamban</span> could be just the place where you defecate and may not even have a washbasin.  So better stick to <span class=red>tandas</span> or <span class=red>bilik air</span> if you can.  Incidentally while "bathroom" can also mean "washroom" in American English, in Malaysia it can only mean the place where one goes to take one's bath, in which case the word is <span class=red>bilik mandi</span> (<em>mandi</em> meaning "to bathe").  So only if you intend to go for your bath should you ask <span class=red>Mana bilik mandi?</span>
<br>Okay, since we are on this topic let me introduce the two relevant words (apart from the obvious <span class=red>cuci tangan</span> = wash hands).  The two words are:
<br><span class=red>kencing</span> meaning "to urinate" and
<br><span class=red>berak</span> (the first syllable is pronounced as "bay") meaning "to poo, defecate".
<br>Or if you prefer to use a more refined language, you can also say <span class=red>buang air kecil</span> for the first and <span class=red>buang air besar</span> for the second.  If you are curious to know what they mean well the first means "throw little water" and the second "throw big water" - very illuminating indeed!  Incidentally this concept of "little" and "big" is also used in Mandarin where to urinate is <em>siao3 bian4</em> and to defecate is <em>da4 bian4</em>, <em>siao3</em> meaning "little" or "small" and <em>da4</em> meaning "big".  Wonder if other languages think alike or is it just Asians who think that way.
