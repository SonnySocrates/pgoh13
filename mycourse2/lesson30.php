<br><p style="max-width:800px;font-family:tahoma;font-size:16px;">
Phew!  After the very "heavy" lesson on family relationships in Lesson 29 I propose to give you a "breather" with this comparatively short lesson.
<br><br>
<img src=forthose_knowmore.jpg>
<br><br>
In the second sentence the word <font color=red>sudah</font> indicates that some action has already taken place, in this case the coffee that was formerly hot has turned cold.
<br>Don't worry too much about the word <font color=red>masih</font> at the moment. I will be devoting a whole lesson to this very useful word later (in Lesson 52).
<br>As you can see from the last two illustrative sentences above, the word <font color=red>panas</font> means "hot", both for coffee and for the weather but not for spicy food. The word to use here is <font color=red>pedas</font>. A word to learn if you want to impress your host whose curry is (normally) so spicy that it really burns your tongue inside out. <font color=red>Ah, pedasnya!</font> from you with a big grin (if you can still afford to grin, that is) is sure to bring the whole house down with laughter!
<br>From the word <font color=red>panas</font> (=hot) we have the expression <font color=red>panas hati</font> (hati=heart) which means "got worked up".  This is a more refined way of saying that someone is angry (=<font color=red>marah</font>)
<br>There is another common word for "hot" and that is <font color=red>hangat</font> (pronounced as ha-ngat).  I know, foreigners will find the second syllable quite difficult to pronounce so better stick to <font color=red>panas</font>.  Lesson 49 will give you an idea of the ng sound in Malay.
<br>You might need to say that you want coffee with sugar (<font color=red>gula</font>) or without sugar.  Just remember that the word "with" in Malay is <font color=red>dengan</font> and the word "without" is <font color=red>tanpa</font>.  So <font color=red>Saya hendak kopi dengan gula</font> in the first case and <font color=red>Saya hendak kopi tanpa gula</font> in the second.
<br>The same for tea.  <font color=red>Saya hendak teh dengan gula</font> (I want tea with sugar) and <font color=red>Saya hendak teh tanpa gula</font>  (I want tea without sugar).
<br>However if you want your coffee or tea with milk (<font color=red>susu</font>) you only have to say <font color=red>kopi susu</font> or <font color=red>teh susu</font>.  You don't have to say <em>kopi dengan susu</em> or <em>teh dengan susu</em> although there is no harm if you should do so.
<br>In the coffee-shop you are likely to hear people asking for <font color=red>kopi O</font> (pronounced "or") or <font color=red>kopi kosong</font> which is coffee without milk or sugar but to avoid any possible misinterpretation of your order I would suggest that you stick to the above (by using <font color=red>dengan</font> for "with" and <font color=red>tanpa</font> for "without").
<br>And while if you are sitting for a Malay examination you will have to write <font color=red>Tolong beri saya secawan kopi susu</font> (= Please give me a cup of coffee with milk), in the coffee-shop you need only say <font color=red>Kopi susu satu</font> or if you are buying  another cup for me as well, then <font color=red>Kopi susu dua</font>.   Not very academic no doubt, but in such a case you can be sure that you will be considered as a local and not as an <font color=red>orang putih</font> (= Caucasian, <font color=red>putih</font> means "white", remember?) or an <font color=red>orang asing</font> (= foreigner).
