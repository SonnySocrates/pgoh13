<br />
Hi,
<br />Perhaps I should not have introduced prefixes and suffixes at all in this basic course on Malay as they only confuse students in the end.  But since you are bound to see them everywhere I thought you (students) should have an understanding of how they are formed - that's all.
<br />Actually if you have just an understanding of how they are formed (as you have) it is good enough.  Don't worry about using them in your own sentences.  For beginners, using the root word of the verb is really good enough.

<br />Besides, the prefixes are not often used in the spoken form,
even among the Malays.  It is mostly used in newspaper reports and official documents.  So don't worry unnecessarily about something that is not that important, finally.
<br />As for the word <font color=midnightblue>hendak</font> it does not take a prefix as most of the time it is not used as a verb by itself but as an auxiliary or helping verb meaning to want or wish TO DO (which is the real verb) something as in <font color=midnightblue>Saya hendak beli barang itu</font>. (I wish to BUY that object.)
<br />But <font color=midnightblue>hendak</font> can also be used as a verb in its own right i.e. when there is no other verb coming immediately after it.  In such a case it takes on the meaning
of "need" or "require" and can take a prefix (plus the suffix -i).  Thus "I need help" in Malay (with <font color=midnightblue>hendak</font> as the verb) is <font color=midnightblue>Saya mengehendaki pertolongan</font> though you can simply say <font color=midnightblue>Saya perlu pertolongan</font>.  As you can see the first translation with the word <font color=midnightblue>mengehendaki</font> is actually beyond the reach of a beginner and also beyond the scope of a basic course in Bahasa Malaysia.
<br />Incidentally there are occasions when not just one, but two prefixes, are attached to the root word as in the case of  <font color=midnightblue>memperkatakan</font> where the root word is <font color=midnightblue>kata</font> or <font color=midnightblue>terperdaya</font> (sometimes spelt <font color=midnightblue>terpedaya</font> meaning deceived or tricked) where the root word is actually <font color=midnightblue>daya</font>!
<br />Then there are the exceptions.  Thus although normally a verb that starts with the letter t loses it when it takes on the men- prefix (eg. <font color=midnightblue>tari</font> becomes <font color=midnightblue>menari</font>) yet some words don't lose their t eg. <font color=midnightblue>ternak</font> becomes <font color=midnightblue>menternak</font>.
<br />To add to the complexity of the subject, sometimes a root word can take on two different prefixes, example <font color=midnightblue>mengaji</font> and <font color=midnightblue>mengkaji</font> both come from the word <font color=midnightblue>kaji</font>, though there are slight differences in meaning between them.
<br />So my advice to you as a beginner is: Don't worry too much about them.
<br />Hope this helps.  - pgoh13

