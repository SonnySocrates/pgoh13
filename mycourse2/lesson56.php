<br><p>Note that <font color=red>gigi</font> is NOT pronounced as the title of the 1958 musical film "Gigi" nor as the "gi" in "gin".  Instead it is pronounced as the "gi" in "give".

<br>It is important to be able to name the parts of the body in Malay as any sickness or health problem associated with that particular part of the body can simply be expressed by putting the word <font color=red>sakit </font> before it as we will see in the next lesson.

<br><br>
<img class="img-responsive" src=forthose_knowmore.jpg>
<br>
Note that the same Malay word is used for singular or plural.  Thus <font color=red>gigi </font> is translated as  "tooth" or "teeth" according to the context.
<br>From the word <font color=red>mata</font> (eye) we have the word <font color=red>matahari</font> (<font color=red>hari</font>, as you have seen in Lesson 7, means "day").  So what can <font color=red>matahari</font> mean?  Why, "eye of the day", of course, which is the Malay way of referring to the sun!
<br>For the more ambitious among you (or simply lovers of nature) the following are the Malay words for "sunrise" and "sunset":
<br><font color=red>matahari terbit</font> = sunrise (<font color=red>terbit</font> means "rise")
<br>and
<br><font color=red>matahari terbenam</font> = sunset (<font color=red>terbenam</font> means "hidden", "set" or "submerged")
<table><tr><td><img src="sunset78.jpg"></td></tr></table>
<br><p style="max-width:800px;font-family:tahoma;font-size:16px;">To continue with the word <font color=red>mata</font>.  Please don't apply blindly the common rule that the plural in Malay is formed by duplicating the word, as <font color=red>mata-mata</font> means "policeman" (ok, eyes of the people if you like).  But <font color=red>mata-mata</font> in Indonesian means "a spy" (<font color=red>perisik</font> in Malaysia).
<br> And what does <font color=red>mata-mata gelap</font> ("dark" policeman) mean?  Well, if I were to tell you that in English we don't say "dark eyes" but "private eyes" you would have been able to guess the meaning.  Yes, <font color=red>mata-mata gelap</font> is the Malay word for a detective.
<br>Incidentally the plural form of a word in Malay is not needed when it is evident that there are more than one.  Compare:
<br><font color=red>Buku di atas meja itu belum lagi dibaca.</font>  (= The book on the table has not been read yet.)
<br>and
<br><font color=red>Buku-buku di atas meja itu belum lagi dibaca.</font>  (= The books on the table have not been read yet.)
<br>with
<br><font color=red>Dia ada banyak buku.</font>  (He has many books.)  Here since we have the word <font color=red>banyak</font> (pronounced as ba-nyak) meaning "many" it would be wrong to say <font color=red>Dia ada banyak buku-buku.</font>  So that is another difference with English.  The singular form of a noun (not the plural) is used when plurality is already implied in the context with such words as "many", "several" or "hundreds of", etc.
<br>A number of compound words are formed with the names of the various parts of the body.  Among them are:
<br><font color=red>jam tangan </font> = a wrist-watch (<font color=red>jam</font> means a "clock" while <font color=red>jam loceng</font> means an "alarm clock").
<br><font color=red>tali leher </font> = a neck-tie (<font color=red>tali</font> means "rope")
<br><font color=red>cermin mata </font> = spectacles (<font color=red>cermin</font> means a "mirror")
<br><font color=red>ubat gigi </font> = toothpaste (<font color=red>ubat</font>, as you would know from the previous lesson, means "medicine").  Thus, literally, toothpaste is medicine for the teeth in Malay!
<br><font color=red>kakitangan </font> = staff, employees (literally translated <font color=red>kakitangan</font> means "feet and hands").  If you like to make use of mnemonics, well just remember that in any office the staff are the ones who have to move their feet about and do things with their hands, certainly not the director!)  But this word has a different meaning in Indonesia.  There it means a "henchman" or "stooge"!  This is a rare example of <em>faux amis</em> in the two almost-similar languages.
<br>Incidentally, your left hand is <font color=red>tangan kiri </font> while your right hand is <font color=red>tangan kanan. </font> 
Please remember that when you are in Malaysia you must always use your right hand (and never your left) when giving (or passing) something to someone.  It is considered impolite to use the left hand when handing over things to someone.
<br>Do you remember what <font color=red>rambut</font> means?  (Just glance up if you don't.)  Well, someone without any <font color=red>rambut</font> is <font color=red>botak</font> (bald-headed).  Thus when you hear <font color=red>Orang itu botak</font> you know it means "That man is bald-headed."


<br>To all intents and purposes the Malay word <font color=red>kaki</font> can mean either leg/legs or foot/feet. Only the context will tell eg. 
<br>1. <font color=red>Dia pergi ke kedai untuk menukar kasut barunya kerana ia terlalu besar untuk kakinya.</font> (He goes to the shop to change his new shoes as they are too big for his feet). Here <font color=red>kasut</font> = foot
<br>2. <font color=red>Dia duduk dengan melunjurkan kakinya.</font> (He sits with his legs outstretched). Here <font color=red>kasut</font> = leg
<br>There is a specific word for thigh, though and that is <font color=red>paha</font> while ankle is <font color=red>buku lali</font> (as you can see <font color=red>buku</font> has nothing to do with a book here!)



<br><br>Finally it might be interesting to note that <font color=red>kaki</font>, apart from meaning "foot" or "leg", has also the connotation of being an "expert" or an enthusiast in whatever comes after.  Thus:
<br><font color=red>kaki minum</font> or <font color=red>kaki botol</font> = a drinker (<font color=red>botol</font> is the Malay spelling for "bottle").  By the way the Malay word for alcohol is <font color=red>minuman keras</font> (literally "hard drinks").
<br><font color=red>kaki gaduh</font> = a quarrelsome chap, someone who will pick up a fight over nothing (<font color=red>bergaduh</font> means "to quarrel")
<br><font color=red>kaki bohong</font> = a liar (<font color=red>bohong</font> means "to lie").  The usual word for a liar is <font color=red>pembohong</font>
<br><font color=red>kaki pancing</font> = a fishing enthusiast, an angler (<font color=red>memancing</font> or <font color=red>mamancing ikan</font> means to fish) 
<br><font color=red>kaki judi</font> = a gambler (the usual word for a "gambler" is <font color=red>penjudi</font> while the verb for "gamble" is <font color=red>berjudi</font>.)
<br><font color=red>kaki perempuan</font> = a womanizer
<br><font color=red>kaki bola</font> = a football fan (the word for "football" is actually  <font color=red>bola sepak</font> as <font color=red>bola</font> alone simply means a "ball".)
<br><br>While the above expressions have to do with a liking for or a tendency towards something, the ones that follow have a meaning much to themselves:
<br><font color=red>kaki lima</font> = pavement ("five-foot-way")
<br><font color=red>kaki ayam</font> = barefooted (easy, this one!  Hens don't wear shoes so someone who is barefooted has a hen's feet!)
<br>Lastly, if you hear a person being mentioned as someone's <font color=red>kaki</font> it simply means that he is his buddy, chum, crony or <font color=red>member</font> (slang).
<br>These various expressions coming from a single word show you how rich and colourful the Malay language is.
</p>

<h3>Jari for fingers and toes</h3><br>
<img class="img-fluid" style="max-width:100%;height: auto;" src="fingers.jpg">
<p>1. <font color=red>ibu jari</font> = the thumb (literally "the mother of fingers") though some call it <font color=red>jari ibu</font>.
<br>2. <font color=red>jari telunjuk</font> = the index finger (literally "the finger that points")
<br>3. <font color=red>jari tengah</font> or <font color=red>jari hantu</font> = the middle finger (<font color=red>tengah</font> means middle while <font color=red>hantu</font> means ghost).  If it helps, think of giving someone the middle finger as scaring the ghost out of him!
<br>4. <font color=red>jari manis</font> = the ring finger ("manis" actually means "sweet").  Yes, what can be sweeter than an engagement ring or a wedding ring, i.e. until the trouble starts!
<br>5. <font color=red>jari kelengkeng</font> = the little finger (it's spelt kelingking in Indonesian).  Forget this one as you are most unlikely to use it though there is a French expression "My little finger tells me" equivalent to "A little birdie tells me" in English ("tells me" is <font color=red>beritahu saya</font>, by the way).  Don't try to translate either of these expressions into Malay though or you'll be "rewarded" with a startled look!  But if you really need to learn this perhaps <font color=red>jari anak</font>, which also means the "little finger" is easier to remember, seeing that <font color=red>anak</font> (child) is at the other end of <font color=red>ibu</font> (mother) used for the thumb, remember?
<br><font color=red>jari</font> is also used for toes.  Just add the word <font color=red>kaki</font> (meaning "foot") to it. Thus
<br><font color=red>jari kaki</font> means toes and <font color=red>ibu jari kaki</font> is the big toe.
</p>



