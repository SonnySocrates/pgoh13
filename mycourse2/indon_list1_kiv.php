<table style="background-color:#EAF2D3;padding-left:20px;padding-right:20px;width:600px;border:1px solid black;margin-left: 95px;"><tr><td>Quite often there is a slight difference in spelling between Malaysian and Indonesian words, particularly in words containing double consonants.  If my memory serves me right Malaysian linguists at some time in the past rejected the use of double consonants, insisting that there should always be a vowel between them.  As a result while the Indonesian spelling is <font color=red>Inggris</font>, the Malaysian spelling is <font color=red>Inggeris</font>.  The same is true for <font color=red>istri</font> (<font color=red>isteri</font> in Malaysia) and <font color=red>Spanyol</font> (<font color=red>Sepanyol</font> in Malaysia).  This is due to the difficulty for some Malaysians to pronounce two consonants together.  However if this is not a problem for you, you can always pronounce <font color=red>Inggeris</font> or <font color=red>isteri</font> not in 3 syllables but in two.
<br>Please bear in mind that what you hear in Indonesia might not always be standard Indonesian but rather one of the numerous dialects (Javanese, Sundanese, Madurese, Minangkabau, Bugis, Acehnese, Balinese, Batak, etc.)</td></tr></table>
