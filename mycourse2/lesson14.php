<p style="font-size:16px;">Possible answers to the question <font color=red>Anda hendak pergi ke mana?</font> are:
<br /><font color=red>Saya hendak pergi ke lapangan terbang.</font> (I want to go to the airport.)
<br /><font color=red>Saya hendak pergi ke stesen kereta api.</font> (I want to go to the railway station.)
<br /><font color=red>Saya hendak pergi ke pasar.</font> (I want to go to the market.)
<br /><font color=red>Saya hendak pergi ke pejabat pos.</font> (I want to go to the post office.)
<br />As to the other question <font color=red>Anda hendak minum apa?</font> see <a href=lesson24minum.php>Lesson 24</a> for possible answers.</p>
<br>
<h3>For those who want to know more</h3>
<br>
<p style="font-size:16px;">Although pedants will say that  <font color=red>hendak</font> should be translated as "wish to" and  <font color=red>mahu</font> (contracted to  <font color=red>mau</font>) as "want to", to all intents and purposes both are often used interchangeably (as in English) and you can feel free to use one or the other so long as the sense of intention of wanting or wishing to do something is there.  So just use either <font color=red>hendak</font> or <font color=red>mahu</font> - whichever comes first to your mind - when saying that you want something or wish to have something.
<br>To express the opposite meaning i.e. when there is unwillingness to do something, simply put the word <font color=red>tidak</font> before either <font color=red>hendak</font> or <font color=red>mau</font> (less colloquially <font color=red>mahu</font>).
<br>You might be interested to know that instead of using two words (<font color=red>tidak hendak</font> or <font color=red>tidak mahu</font>) there is a single Malay word to express an unwillingness to do something and that is the word <font color=red>enggan</font>.  Thus, if someone does not wish to go, you can either say:
<br><font color=red>Dia enggan pergi.</font> (He/She is reluctant to go) or
<br><font color=red>Dia tidak mahu pergi.</font> or
<br><font color=red>Dia tidak hendak pergi.</font>
<br>Similarly if he/she is unwilling to lend you money you can either say:
<br><font color=red>Dia enggan meminjamkan wang kepada saya.</font> or
<br><font color=red>Dia tidak mahu meminjamkan wang kepada saya.</font> or
<br><font color=red>Dia tidak hendak meminjamkan wang kepada saya.</font>
<br>How nice!  Three ways to say the same thing - so learn whichever is easier for you.
</p>
<p style="font-size:16px;">Note that in all the above examples <font color=red>hendak</font> is always followed by a verb as it is used in the sense of wishing or wanting to DO something.
<br>However when the word is used all alone by itself and with a rising intonation you can be sure that the speaker is asking you if you want to have something.  Thus if you should come along while we are eating biscuits we would certainly ask you (for hospitality is the Malaysian way of life <i class="fa fa-smile-o" style="background-color:yellow"></i>&nbsp;!) <font color=red>Hendak?</font>  You should know by now that if you accept the invitation you would say <font color=red>Ya, terima kasih</font> else you would say <font color=red>Tidak, terima kasih</font> (No, thanks).
<br><strong>Note:</strong> In Indonesia <font color=red>terima kasih</font> is often shortened to <font color=red>makasi</font> or <font color=red>makasih</font>.  The first two syllables are simply left out!
