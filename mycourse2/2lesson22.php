<br>



<div class="col-xs-12 col-sm-10 col-md-8">
<h3>For those who want to know more</h3>

<p style="font-size:16px;">When <font color=red>satu</font> is added to another word it is often contracted to <font color=red>se</font> and tagged on to the following word.  Thus "satu" ratus (one hundred) becomes <font color=red>seratus.</font>
<br>This is also the case with one thousand (<font color=red>seribu</font>) and one million (<font color=red>sejuta</font>).
<br>For the billionaires among you (ah, who knows?) the Malay word for "billion" is simply <font color=red>bilion</font>.  That's one word most of us don't have to learn - or use!</p>
</div>

