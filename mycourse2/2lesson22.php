<p>
<u>Before you begin: </u> <br>
If you have mastered counting up to 99 in Lesson 8 this final lesson on numbers in Malay will be plain sailing to you.  You will  
now need to know only three more words (<font color=red>ratus, ribu</font> and <font color=red>juta</font>) to be able to count any number in Malay.  Isn't that fantastic?
<br>Counting 3-digit figures in Malay is like counting them in English, the word "hundred" being replaced by <font color=red> ratus</font>.
</p>

<div class="col-xs-12 col-sm-10 col-md-6" style="border:1px solid black;padding:10px 20px;background-color:yellow">
<p style="font-size:16px;">
193 = seratus sembilan puluh tiga <br>
459 = empat ratus lima puluh sembilan<br>
638 = enam ratus tiga puluh lapan<br>
874 = lapan ratus tujuh puluh empat<br>
241 = dua ratus empat puluh satu<br>
562 = lima ratus enam puluh dua<br>
927 = sembilan ratus dua puluh tujuh<br>
382 = tiga ratus lapan puluh dua<br>
734 = tujuh ratus tiga puluh empat<br>
111 = seratus sebelas</p>
</div>



<p style="float:left;margin-top:20px">
Similarly counting in thousands and millions in Malay is like counting them in English.  You only have to replace "thousand" by <font color=red> ribu</font> and "million" by <font color=red> juta</font>.<br>As examples speak better than words, here are a few numbers for you to practise (in the table below):
</p>

