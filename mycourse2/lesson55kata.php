<br>Passing on messages is such an important aspect of life that I am devoting a whole lesson to it.  Just remember that if you have a message to deliver in Malay (after mentioning the name of the person who came eg. <font color=red>Peter datang tadi.</font> or who phoned eg. <font color=red>Mary telefon tadi.</font>) you always start with <font color=red>Dia kata ...</font> (He said ...)<p>

