<br>There are two words of negation in Malay - <font color=red>bukan</font> and <font color=red>tidak.</font>
<br>But there is a difference between them.  <font color=red>Bukan</font> is mainly used to DENY the truth of something and is therefore used with nouns and pronouns eg. <font color=red>Ini bukan kereta saya</font> (This is not my car) or <font color=red>Bukan dia</font> (Not him)  while <font color=red>tidak</font> (already covered in detail in Lesson 37), is mainly used with adjectives and verbs.  To refresh your memory here are some  sentences that show how <font color=red>tidak</font> is used with adjectives:<br><font color=red>Ini tidak berat</font>. = This is not heavy.<br><font color=red>Dia tidak gemuk.</font> = He is not fat. <br>and with verbs:<br><font color=red>Dia tidak ambil beg saya.</font> = He didn't take my bag.<br>
In the spoken form the word <font color=red>ambil</font> is often pronounced as <font color=red>ambek</font>.
<br><font color=red>Saya tidak tahu dia sudah keluar</font>. = I didn't know that he had left.
<br>You might want to compare the above sentence with <font color=red>Dia ada di luar</font> meaning "He is outside".
<br>Note the difference between <font color=red>Dia tidak bodoh.</font> = He is not stupid (as can be seen from his consistently good results) and <br><font color=red>Dia bukan bodoh tetapi malas.</font> = He is not stupid but lazy  (to explain why he got low marks).  So this is an exception to the general rule which says that <font color=red>tidak</font> and not <font color=red>bukan</font> is used with adjectives.
<br>
<font color=red>Bukan</font> and not <font color=red>tidak</font> is used for colours, eg.:
<br><font color=red>Warna kesukaannya ia-lah biru, bukan merah. </font>= His favourite colour is blue, not red.
<br>Note that <font color=red>bukan</font> is also used as a question tag in the same way that French speakers use "n'est-ce pas".   Eg.:
<br><font color=red> Ini pen anda, bukan?</font> = This is your pen, isn't it?<br> where <font color=red>bukan</font> is added more for confirmation than anything else.  Thus in such questions the person expects you to answer <font color=red>Ya, ini pen saya.</font> (Yes, this is my pen.)   But if this is not the case and it is not your pen, the answer would be: <font color=red> Bukan, ini bukan pen saya.</font> (No, this is not my pen.) 
<br>Another eg.:
<br><font color=red>  Dia kata dia akan datang esok, bukan?</font>  = He said he would be coming tomorrow, didn't he? <br> How would you translate the following sentence in Malay:  "This is the first time I come to Malaysia".  <br> Well, it is translated as <font color=red>Ini kali pertama saya datang ke Malaysia.</font>  An Indonesian is more likely to say <font color=red>Ini pertama kali saya datang ke Malaysia.</font>  But don't worry too much whether to say <font color=red>pertama kali</font> or <font color=red>kali pertama</font>.  You won't be making a mistake whichever order is used.
<br>But how do you deny the above statement (i.e. if this is not the first time you come to Malaysia).  Where would you put <font color=red>bukan</font>?   As in English  <font color=red>bukan</font> is inserted before "first time".  Thus:
<br><font color=red>Ini bukan kali pertama saya datang ke Malaysia.</font> = This is not the first time I come to Malaysia.<br>
If I said <font color=red>Hari ini hari Ahad</font> = Today is Sunday when it is not so, again it is <font color=red>bukan</font color> and not <font color=red>tidak</font> that has to be used.
<br>You will therefore say <font color=red>Hari ini bukan hari Ahad</font> = Today is not Sunday.  Perhaps it will help if you remember that when factual errors have to be corrected it is <font color=red>bukan </font> that has to be used.
<br>In other words when someone says something that is not true you have to use <font color=red>bukan </font> and not <font color=red>tidak</font> (though as you are a foreigner you will be understood even if you should use one for the other so don't worry too much over this.)
<br>By the way Sunday is very often referred to as <font color=red>hari Minggu</font> in both Malaysia and Indonesia.
<br>
Incidentally when you wish to point out to someone that he is not doing things as it should be done you will say <font color=red>Bukan begitu</font> (literally "not like that").<br>
In the rare occasions when <font color=red>bukan</font> is used with an adjective it is usually followed by another clause starting with <font color=red>tetapi</font> as in the following sentence:<br>
<font color=red>Dia bukan sombong tetapi malu.</font> = He is not proud but shy.
<br>If no such distinction is made you will simply say <font color=red>Dia tidak sombong. </font> (He is not proud).
<br>Finally if all the above is quite confusing to you just learn these two little phrases by heart as they will be needed at one time or another in your daily conversations:
<br><font color=red>Bukan dia</font> meaning "Not him (or her)" but someone else
<br>and <font color=red>Bukan itu</font> meaning "Not that one!" (when you are being shown an article in a shop).

