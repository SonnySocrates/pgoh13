<br><br>Even if you yourself prefer to use the full form of the words (these being the words you learnt) the main purpose of this lesson is to make you aware of their contracted forms so you will understand them when you hear them spoken.   As a matter of fact, the short forms are very frequently used in speech.
<br>So do make use of them in conversations.  An example is in the use of <font color="red">saja</font> instead of <font color=red>sahaja</font> in the following answers. The usual meaning of <font color="red">saja</font> is "only" but note how it is used in the following expressions.  Thus, to the question:
<br><font color=red>Apa nak minum?</font> (What do you wish to drink?) the answer could be <font color=red>Apa saja</font> (Anything at all)
<br><font color=red>Bila boleh kita makan bersama?</font> (When can we eat together?) the answer could be <font color=red>Bila-bila saja</font> (Anytime)
<br><font color=red>Di mana anda nak tidur?</font> (Where do you wish to sleep?) the answer could be <font color=red>Mana-mana saja</font> (Anywhere)
<br>Note also the following conversational question forms:
<br><font color=red>Ada pen tak?</font>  (Do you have a pen or not?) to which the answer can be <font color=red>Ada</font> or  <font color=red>Tak ada.</font>
<br><font color=red>Mau keluar tak?</font>  (Do you wish to go out or not?) to which the answer can be <font color=red>Mau</font> or  <font color=red>Tak mau.</font>
<br><font color=red>Dah makan belum?</font>  (Have you eaten?) to which the answer can be <font color=red>Dah</font> or  <font color=red>Belum.</font>
<br><font color=red>Boleh masuk tak?</font>  (Can I come in or not?) to which the answer can be <font color=red>Boleh</font> or  <font color=red>Tak boleh.</font>

