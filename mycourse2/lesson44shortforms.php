The answer to the last question can also be <font color=red>Jauh</font>, <font color=red>Tak jauh</font> or <font color=red>Tak berapa jauh</font> (= Not too far).  Note that <font color=red>jauh</font> (far) is pronounced in two syllables: ja-oo.
<br><br><FONT face=tahoma color=black size=3><u>More sentences:</u>
<br><font color=red>Nak jumpa doktor PUN tak ada masa</font> (I don't even have time to see a doctor.)  Note that there is a pause after the word <font color=red>pun</font> which has got to be stressed to bring out the full meaning of the sentence.  The little word <font color=red>pun</font> has the sense of "<em>even to the extent of</em>" in this sentence.   (By the way <font color=red>pun</font> has got quite a number of other uses and if you'd like to learn more about these I have put them together in one page, following an email inquiry from a student.  Go here for <a href=usesof_pun.php>Various uses of the Malay word <font color=red>pun</font>.</a>)  
<br><font color=red>Suka tak?</font> (Do you like it or not?) to which the answer can be <font color=red>Suka</font>,  <font color=red>Tak suka</font> or <font color=red>Tak berapa suka</font> (= Not too much).
<br>Other examples:
<br><font color=red>tak ada</font> means "don't have"
<br><font color=red>tak mahu</font> means "don't want"
<br><font color=red>tak tahu</font> means "don't know"
<br><font color=red>tak guna</font> means "useless"
<br><font color=red>tak sempat</font> means "don't have enough time"
<br><font color=red>buat tak tahu</font> means "pretend not to know about something"
<br><font color=red>tak nak</font> means "don't want" and comes from <font color=red>tidak hendak</font>
