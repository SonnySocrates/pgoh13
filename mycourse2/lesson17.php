<h3>Asking about the future</h3>
<p style="font-size:16px;">When <font color=red>Bila</font> is used to ask questions about future events the word <font color=red>akan</font>, which indicates a future event (see Lesson 15), is always used with it.  In such a case the above questions become:


<table class="table table-striped">
<tr>
<td style="border-right:1px solid black;border-top:1px solid black;">When will he return?  - Next month.</td>
<td style="border-right:1px solid black;border-top:1px solid black;">Bilakah dia akan balik?  - Bulan depan.</td>
</tr><tr>
<td style="border-right:1px solid black;">When will they arrive?  - Tomorrow.</td>
<td style="border-right:1px solid black;">Bilakah mereka akan tiba?  - Esok.</td>
</tr><tr>
<td style="border-right:1px solid black;">When will he stop working?  - At the end of this month.</td>
<td style="border-right:1px solid black;">Bilakah dia akan berhenti kerja?  - Pada hujung bulan ini.</td>
</tr><tr>
<td style="border-right:1px solid black;">When will he get married?  - Next year.</td>
<td style="border-right:1px solid black;">Bilakah dia akan kahwin?  - Tahun hadapan.</td>  

</tr><tr>
<td colspan=2 bgcolor=beige style="border-top:1px solid black;">Instead of saying <font color=red>Pada hujung bulan ini</font> (= At the end of this month) you can also say <font color=red>Pada akhir bulan ini</font>  as both <font color=red>hujung</font> and <font color=red>akhir</font> mean "end".</td>
