<h3>For those who want to know more</h3>
<br><br>

<p style="font-size:16px;">The written form of the verb <font color=red>kahwin</font> ("to marry") has the <font color=red>ber-</font> prefix added to it (<font color=red>berkahwin</font>).  You might come across another word (<font color=red>bernikah</font>) which means exactly the same thing.
<br>
 The suffix  <font color=red>kah</font>   is often added to  <font color=red>Bila</font>   to indicate that a question is being asked.  It is often omitted by native speakers as the rising intonation alone leaves no doubt that a question is being asked.  Thus:    <font color=red>Bila dia balik?</font>  instead of <font color=red>Bilakah dia balik?</font>   <br>
There are two Malay words widely used for "yesterday", namely   <font color=red>kelmarin</font>   and   <font color=red>semalam</font>.   However as <font color=red>semalam</font> could lead to confusion since it means "last night" in some places, it's better to stick to <font color=red>kelmarin</font> for "yesterday".<br>
"Last week, last month, last year" is usually translated as  <font color=red>minggu lalu, bulan lalu, tahun lalu</font> respectively  though you will also hear  <font color=red>minggu yang lalu, bulan yang lalu, tahun yang lalu</font>  as well as  <font color=red>minggu lepas, bulan lepas, tahun lepas.</font>  
</p>
<span style="font-size:20px;"> <u>Question of priority:</u></span>
<p style="font-size:16px;">When something has got to be done urgently (and in answer to the question <font color=red>Bila...</font>) the usual answers are:  <font color=red>Sekarang juga</font> (Right now) or <font color=red>Dengan segera</font> (Immediately).  When it is not that urgent the answer can be <font color=red>Secepat mungkin</font> (As soon as possible) or <font color=red>Bila-bila saja</font> or <font color=red>Bila-bila masa sahaja</font>, both of which mean "Anytime at all".   You can also say <font color=red>Bila senang</font> or <font color=red>Bila ada masa</font>, both meaning "When you are free".</p>

<span style="font-size:20px;"> <u>Cultural note (1):</u></span>
<br><p style="font-size:16px;">Although dropping by a friend's house unannounced is common practice in a rural environment, most people in the city telephone in advance to fix a time that is convenient to both parties. Incidentally the Malaysian hospitality is such that if you are at a friend's house around lunch time or dinner time you are also invited to stay on for a meal.  The following sentences might come in handy when socializing:
<br> <font color=red>Saya hendak menjemput anda ke rumah saya.</font>    (I would like to invite you to my house.)
<br> <font color=red>Datanglah ke rumah saya bila senang.</font>    (Come to my house when you are free.)
<br>Another way of saying that is <font color=red>Datanglah ke rumah saya bila ada masa.</font>
<br>Note that <font color=red>senang</font> has got two meanings depending on the context.  It can mean "free, not busy" as in the above sentence.  It can also mean "easy, simple" as you will learn in Lesson 47 and again in Lesson 58.
<br>But when you are in Indonesia remember that <font color=red>senang</font> over there usually means "happy" and that it can also be used as a verb to mean "like" eg. <font color=red>Aku senang nonton TV</font> (= I like to watch TV.)
<br>If you are very keen that a friend visit you, you can say to him:
<br> <font color=red>Bilakah anda dapat datang ke rumah saya?</font>   (= When will you be able to come to my house?)
</p>

<span style="font-size:20px;"><u>Cultural note (2):</u></span>
<p style="font-size:16px;">The word <font color=red>balik</font>, you have learnt, means "to return".  While in Malaysia you might often hear the term <font color=red>balik kampung</font>.  It simply means to return to one's hometown (where one's roots are) especially during the Hari Raya holidays or when one takes a week's leave from work.
</p>

