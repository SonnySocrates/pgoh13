<u>MORE COMPLIMENTS:</u>
<br>When you wish to congratulate someone for an achievement you can either say <font color=red>Saya ucapkan tahniah atas kejayaan anda</font> (I congratulate you on your success) or if that is too long for you just say <font color=red>Syabas!</font> (Well done!).  The word is pronounced "sha-bas".
<br>Other commendable qualities are:
<br><font color=red>ikhlas</font> = sincere
<br><font color=red>murah hati</font> = generous (<font color=red>murah</font> as you have learnt in Lesson 33 means "cheap")
<br><font color=red>rendah hati</font> = humble (<font color=red>rendah</font> means "low" - see Lesson 47)
<br><font color=red>sabar</font> or <font color=red>sabar hati</font> = patient but <font color=red>berhati-hati</font> means "careful" or "be careful!"
<br>There are two other words for careful: <font color=red>cermat</font> and <font color=red>teliti</font>
<br><font color=red>(datang) tepat</font> = (to be) punctual
<br><font color=red>setia</font> = loyal
<br><font color=red>riang</font> = cheerful
<br><font color=red>berbudi bahasa</font> = polite
<br>Another word for polite is <font color=red>sopan</font>

<br>You will notice that a number of these qualities contain the word <font color=red>hati</font> which means heart.  Other common expressions with the word <font color=red>hati</font> are: <font color=red>panas hati</font> meaning angry, <font color=red>puas hati</font> meaning satisfied and <font color=red>sakit hati</font> meaning annoyed or hurt (eg. by someone's remarks).
<br>Leaving people aside, when you want to say that a performance or a film is really superb you can say <font color=red>Bagus sekali</font> or <font color=red>Hebat sekali</font> (pronounce the first syllable as "hay", not "her").
<hr color=black>
<br><u>CRITICISMS:</u>
<br>
Now we come to the criticisms.  About the worst criticism one can make of a Malaysian is that of being <font color=red>kurang ajar</font>.  The person to whom this remark is intended would be covered with shame as it means that he has not been properly brought up (is ill-bred or uncouth).  Courtesy plays a very important part in the Malay culture. Thus it would be rude (<font color=red>biadab</font>) to hand over something to a Malay with your left hand or to tap someone on the head or for a son to argue loudly with his parents (though modern-day parents might be more open and allow their children more leeway).

<br>Incidentally I think that the Malay culture has much to offer the rest of the world in terms of respect and good behaviour.  This is because to the Malays <em>tingkah laku</em> (behaviour) is very important. There are numerous other words which denote good behaviour, civility and politeness, such as <em>sopan santun, hormat, beradab, lemah lembut, baik budi</em> in the Malay language. In fact there is a popular Malay pantun containing these 2 lines which encapsule very well the very nature of Malay thinking: <em>Hutang emas dapat dibayar, hutang budi dibawa mati</em> which I paraphrase as "Borrowed gold can be returned and done with but kindness received accompanies one to the grave."

<br />By the way the word <font color=red>kurang</font> by itself means "less".  Thus if you are asking the shopkeeper to reduce the price you might say <font color=red>Boleh kurang sedikit</font>? (Can you lessen a bit?)
<br>To turn words of compliment into words of criticism you only have to add <font color=red>tidak</font> before the adjectives.
<br>Thus <font color=red>Dia boleh dipercayai</font> means "He can be trusted" whereas <font color=red>Dia tidak boleh dipercayai</font> means "He cannot be trusted".
<br>If you had learnt the first part of this lesson well you should now know the meanings of the following: <font color=red>tidak ikhlas, tidak ramah, tidak murah hati</font> and <font color=red>tidak sabar</font>.
<br>
It's time for me to bring up the word <font color=red>setuju</font> which means to agree.  Thus "I agree with you" would be <font color=red>Saya setuju dengan anda</font> or <font color=red>Saya setuju dengan apa yang anda kata itu.</font> (I agree with what you said.)
<br />If you would like to use the verb with the prefix you would say <font color=red>Saya bersetuju dengan anda</font> or <font color=red>Saya bersetuju dengan pendapat anda</font>  (I agree with your opinion.)
<br />Incidentally a useful opening for a sentence is <font color=red>Pada pendapat saya...</font> as it means "In my opinion...".
<br />If you don't agree with what is being said you only have to put <font color=red>tidak</font>, the word of negation, before <font color=red>setuju </font>.  Thus <font color=red>Saya tidak setuju dengan anda.</font> means "I don't agree with you".
<br />You can temper your disagreement by preceding it with <font color=red>Maafkan saya</font>.  Thus to politely object to what someone said you could say <font color=red>Maafkan saya, tetapi saya tidak setuju dengan anda</font>  (Excuse me, but I don't agree with you) or if you prefer, <font color=red>Maafkan saya, tetapi saya tidak setuju dengan pendapat anda.</font>  (Excuse me, but I don't agree with your point of view.)
<br />
If you are now at a stage where you can engage in a simple conversation in Malay you should learn this question and use it at the first opportunity: <font color=red>Adakah anda setuju dengan saya atau tidak?</font> (Do you agree with me or not?)
