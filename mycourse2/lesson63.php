<br>
Note the particular use of <font color="red">betul</font> in the second sentence above.  The word <font color="red">betul</font>, as you  have learnt in Lesson 48, normally means true or correct.  Okay, if it helps you to remember, translate the sentence as "He is stupid, truly".
<br>In the third sentence <font color="red">bau</font> (pronounced as two syllables ba-oo) by itself means "smell"
<br>If you wish to leave no doubt that the person smells terrible you could say:
<br><font color="red">Orang itu berbau busuk</font> since <font color="red">busuk</font> by itself means bad-smelling.
<br>A person who has a bad or wicked heart is referred to as <font color="red">busuk hati</font>.
<br>If you wish to refer to something that smells good you can say <font color="red">berbau harum</font> or <font color="red">berbau wangi</font>.
<br>From this it is easy to remember the Malay word for "perfume".  &nbsp;It is <font color="red">air wangi</font> (fragrant water) or <font color="red">minyak wangi</font> (fragrant oil).
<br>By the way the word <font color="red">minyak</font> alone can mean either cooking oil or car petrol depending on the context.  Engine oil, since it is black in colour, is called <font color="red">minyak hitam</font>. 
<br>A second meaning of <font color="red">busuk</font> is "rotten" or "decayed".  Thus   when you say <font color="red">Buah itu sudah busuk</font> you are referring to a fruit that has become rotten or decayed.  Incidentally the word to use for an object that has become damaged is <font color="red">rosak</font>  eg.  <font color="red">Kereta saya sudah rosak</font>. (My car is out of order).

