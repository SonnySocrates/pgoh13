<center><font face="arial" size="5">Woman, 104, weds man 71 years younger</font></font></center>
<br />
<font face="arial" size="2"><b>By Sebastien Berger, South East Asia Correspondent
<br>From <a href=http://www.telegraph.co.uk/ target="_blank">The Daily Telegraph (news.telegraph)</a> of May 03, 2006</b></font>

<p><table align="left" width="203"><tr><td valign="top"><img src="wife_104years.jpg" width="203"></td></tr><tr><td align="left"><font size="2">- AP photo.</font></td></tr></table><font face="arial" size="2">A woman of 104 expressed her joy yesterday after marrying a man 71 years her junior.
<br />
"I hope this marriage will last," said Wook Kundor, who has had 20 previous husbands. "I ask people to view it in a positive light, as we have done nothing that is forbidden by God." The couple appeared on the front page of Malaysia's Berita Harian newspaper, Wook holding the marriage certificate in her gnarled hands.
<br />
Muhammad Noor Che Nusa, her 33-year-old husband, said that when he first moved into his bride's house in Kuala Berang, in Terengganu state, he felt sorry for her, as she was old, childless and alone. Then mutual respect and friendship grew into love, which he described as "God's will".
<br />
He said: "I know society is cynical but I did not marry Wook for her money. She is poor. Her only asset is her deep religious knowledge. Through her, I can deepen my knowledge of religion."
