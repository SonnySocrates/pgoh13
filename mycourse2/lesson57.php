<p>There's another word that you might want to learn.   It's <font color=red>gila</font> meaning "mad" or "insane".  Thus a madman would be <font color=red>orang gila</font> in Malay.  However, apart from its medical sense, <font color=red>gila</font> can also be used in all cases where we would say "crazy" in English.   Thus "Are you crazy?" would be <font color=red>Gilakah?</font> or <font color=red>Gilakah anda?</font> and someone who is crazy over women can be described as <font color=red>gila perempuan</font>.
<br>I might point out here that many Malaysians who live in villages (<font color=red>kampung</font>) believe in traditional doctors or faith-healers.  So it might  be useful to know the following four words when you hear them spoken.   They are: <font color=red>tabib, bomoh, dukun</font> and <font color=red>pawang</font> (though <font color=red>tabib</font> is sometimes also used when referring to the conventional or trained doctor).  All four terms are also used in Indonesia for the traditional doctor.
</p>

<h3> The five senses</h3>
<p>As we are talking about the parts of the body in this lesson can I bring in the five senses here?
<br />First there is the eye (<font color=red>mata</font>)  which you see (<font color=red>melihat</font>) with.
<br />Other words that are used for seeing are:  <font color=red>tengok</font> (to watch) which you have learnt in Lesson 32 and <font color=red>nampak</font>.
<br />Just as in English there is a slight nuance between "to look" and "to see" so too in Malay.
<br />Thus "Look at that man" is <font color=red>Lihat orang itu</font> while "I can see that man" is <font color=red>Saya boleh nampak orang itu</font>.  Oh by the way, while <font color=red>lihat</font> means to look, <font color=red>lihat-lihat</font> means to look around eg. if a shop assistant asks what you are looking for and you are looking for nothing in particular you can say <font color=red>Saya cuma lihat-lihat</font> (I'm just looking around.)
<br />Then there is the ear (<font color=red>telinga</font>) that you hear (<font color=red>mendengar</font>) with.
<br />Third in line is the nose (<font color=red>hidung</font>) which you smell (or sniff = <font color=red>menghidu</font>) with.
<br />Another word meaning "to smell" is <font color=red>berbau</font>.
<br />Note that the word <font color=red>bau</font> by itself means a smell.
<br />Watch out how you use the word <font color=red>berbau</font> as <font color=red>berbau</font> by itself can also mean smelly.
<br />Thus <font color=red>Orang ini berbau</font> would mean "This man stinks."
<br />So don't get this wrong unless you want to provoke a fight!  And don't think you can get away by saying you're a foreigner if you were to tell someone he is smelly!
<br />By the way <font color=red>bau</font> can only be pronounced ba-oo.
<br />It cannot be pronounced ba-hoo as there is a word <font color=red>bahu</font> meaning shoulders.
<br />I know, you have learnt in Lesson 24 that <font color=red>mahu</font> can also be written <font color=red>mau</font> and thus can be pronounced as ma-hoo or ma-oo.
<br />Similarly <font color=red>tahu</font> (to know) can also be written <font color=red>tau</font> and can thus be pronounced as ta-hoo or ta-oo.
<br />But <font color=red>bau</font>  and <font color=red>bahu</font> are two entirely different words so don't mix them up.
<br />Here is a sentence you can learn using <font color=red>berbau</font> to mean "smell":
<br /><font color=red>Durian berbau wangi untuk orang Malaysia tetapi berbau busuk untuk orang asing</font>.
<br />(= Durians smell nice to Malaysians but smell bad to foreigners.)
<br />Then comes the finger (<font color=red>jari</font>) which you use to touch with (<font color=red>sentuh</font> or <font color=red>menyentuh</font> with the prefix).
<br />And lastly there is the tongue (<font color=red>lidah</font>)  for you to taste (<font color=red>merasa</font>) with.
<br />To sum up, the following sentences in Malay might help you to study the five senses better:
<br /><font color=red>Saya melihat dengan mata.</font>  (I see with my eyes.)
<br /><font color=red>Saya mendengar dengan telinga.</font>  (I hear with my ears.)
<br /><font color=red>Saya berbau/menghidu dengan hidung.</font>  (I smell with my nose.)
<br /><font color=red>Saya menyentuh dengan jari.</font>  (I touch with my fingers.)
<br /><font color=red>Saya merasa dengan lidah.</font>  (I taste with my tongue.)
</p>

