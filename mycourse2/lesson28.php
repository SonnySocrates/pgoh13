<br><p style="max-width:800px;font-family:tahoma;font-size:16px;">
Instead of saying <font color=red>hari Ahad</font> for Sunday you can also say <font color=red>hari minggu</font>.
<br>Although the Malay word for "take" is <font color=red>ambil </font> note that when you "take" a train, a plane, a ship, a taxi, a car or a bus you don't use the word <font color=red>ambil </font> but use the word <font color=red>naik </font> (go up) instead.  The usual meaning of the word <font color=red>naik</font> is "to ascend" or "to rise" eg. 
<br><font color=red>Harga petrol sudah naik. </font>  (The price of petrol has gone up.)
<br>Study the following sentence:
<br>
<font color=red>Untuk pergi ke Pulau Pinang dari Kuala Lumpur anda boleh naik kereta api, kapal terbang, teksi ataupun bas. </font>
<br>It means "To go to Pinang from Kuala Lumpur you can take a train, a plane, a taxi or a bus".
<br>The Malay word for "always" is <font color=red>selalu </font> while "often" is <font color=red>kerap kali </font> or <font color=red>sering kali </font>.
Now that you know the Malay word for "often" you might want to know how to say the opposite i.e. "rarely" (or "seldom").  Well, the word is <font color=red>jarang</font>.
<br>Thus <font color=red>Dia jarang datang ke rumah saya</font> means "He/She seldom comes to my house".
<br>To drive home the point further you can say <font color=red>jarang sekali</font>.
<br>Thus <font color=red>Saya jarang sekali pergi ke dewan tari-menari</font>. = I very rarely go to the dance-hall (or, if you prefer, I hardly ever go to the dance-hall).
