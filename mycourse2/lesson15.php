<h3>For those who want to know more</h3>
<br>
<p style="font-size:16px">As already explained in Lesson 4  the pronoun "dia" (third person singular) can refer to a woman as well as a man.   Thus <font color="red">Dia akan datang esok</font> can mean "He will come tomorrow" or, if we are talking about a woman, "She will come tomorrow".  I might not be repeating this each time in future.  So even when <font color="red">dia</font> is translated as "he" please bear in mind that it could just as well stand for "she" if it is a woman we are talking about.
<br>You might have learnt the word <font color="red">bertemu</font> which is a synonym for <font color="red">berjumpa</font> and therefore means "to meet" someone but it is much more formal than <font color="red">berjumpa</font>.  Thus if President Obama were to meet Prime Minister Najib we would likely use <font color="red">bertemu</font> but if you were to meet your friend, better use <font color="red">berjumpa</font> or simply <font color="red">jumpa</font> (without the prefix which makes it even less formal).  By the way "See you" or "So long" in Malay is <font color="red">Jumpa lagi!</font>
<br>Since we are talking about the future it is time to learn the following:
<br>"Next week" is <font color="red">minggu hadapan</font> or <font color="red">minggu depan</font>,  <font color="red">depan</font> being the short form of <font color="red">hadapan</font>.
<br>"Next month" is <font color="red">bulan hadapan</font> or <font color="red">bulan depan</font>.
<br>"Next year" is <font color="red">tahun hadapan</font> or <font color="red">tahun depan</font>. 
<br>In all the above cases the Indonesians prefer to use the shortened word <font color="red">depan</font>, thus <font color="red">minggu depan, bulan depan</font> and <font color="red">tahun depan</font> respectively for next week, next month and next year.
<br>It will make your learning much easier if you will just remember that since <font color="red">di hadapan</font> means "in front of" so evidently the word <font color="red">hadapan</font> in the above three sentences denote something that is forthcoming, something that has not arrived yet.
 So many ways of saying the same thing!
<br><br>As for "last week", "last month" and "last year" we say <font color="red">minggu lalu, bulan lalu</font> and <font color="red">tahun lalu</font> respectively both in Malaysia and Indonesia. 




<br>If it helps, simply translate <font color="red">lalu</font> above as equivalent to "ago" so the above is translated directly as: a week ago, a month ago and a year ago. You will then know that <font color="red">sehari lalu</font> means "a day ago" and <font color="red">sejam lalu</font> means "an hour ago" (this is often used in newspaper reports) although <font color="red">sejam yang lepas</font> is also often used instead.
<br>You might also hear some people saying <font color="red">minggu lepas, bulan lepas</font> and <font color="red">tahun lepas</font> instead, thus replacing <font color="red">lalu</font> with <font color="red">lepas</font>.  So <font color="red">lalu</font> or <font color="red">lepas</font>?. The choice is yours!

<br>


<br>By the way, "weekend" is <font color="red">hujung minggu</font> which is logical since the word  <font color="red">hujung</font> alone means the "end" (most extreme part) of something.
<br>"Will it rain?" is simply translated as <font color="red"> Akan hujankah?</font>
<br>I hope you will not need to use the next sentence during your stay in Malaysia but learn it anyway in case you have a bad experience.  The sentence is <font color="red">Saya akan panggil polis</font> and it means "I am going to call the police" (and hopefully your knowledge of Malay will deter the criminal!)  In this case it's the immediate future but in Malay it's still the same word <font color="red">akan</font> that is to be used.</p>
